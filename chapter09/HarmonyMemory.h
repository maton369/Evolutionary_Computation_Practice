#ifndef HARMONY_MEMORY_H
#define HARMONY_MEMORY_H

/*
    ============================================================
    HarmonyMemory.h
    ============================================================

    このファイルでは、ハーモニーサーチ
    (Harmony Search, HS)
    における「ハーモニーメモリ」全体を表す
    HarmonyMemory クラスを定義する。

    ------------------------------------------------------------
    ハーモニーサーチの基本思想
    ------------------------------------------------------------

    ハーモニーサーチは、音楽家が即興演奏で
    良いハーモニー（和音の組合せ）を探していく過程を
    最適化アルゴリズムとして抽象化した手法である。

    音楽の比喩では、

        - 1 つのハーモニー
            = 1 個の候補解

        - ハーモニーメモリ
            = これまでに得られた複数の候補解の集合

        - 新しいハーモニーの生成
            = 新しい候補解の生成

        - 良いハーモニーを記憶し続ける
            = 良い候補解を保持し、悪い解を置き換える

    という対応になる。

    したがって Harmony Search の本質は、

        1. 過去の良い解をメモリとして保持する
        2. そのメモリを参照しながら新しい解を作る
        3. 新解が悪い解より良ければメモリを更新する

    という反復構造にある。

    ------------------------------------------------------------
    他のメタヒューリスティクスとの違い
    ------------------------------------------------------------

    GA との違い:
        GA は交叉・突然変異によって次世代を作る。
        HS は「メモリ参照」と「微調整」で新解を作る。

    PSO との違い:
        PSO は速度と位置を持つ粒子群である。
        HS は速度を持たず、記憶済み解の再利用が中心である。

    ABC や BA との違い:
        HS は生物行動よりも「音楽的即興」の比喩を使い、
        解メモリからの値の選択と微調整を重視する。

    Cuckoo Search との違い:
        CS は Levy flight と淘汰が中心である。
        HS はハーモニーメモリからの値選択により新解を作る。

    ------------------------------------------------------------
    HarmonyMemory の役割
    ------------------------------------------------------------

    HarmonyMemory は単なる Harmony の配列ではない。

    HarmonyMemory は、

        - 問題データ Dataset を保持する
        - 現在保持している複数の Harmony を管理する
        - 新しい Harmony を生成・比較する
        - 最良解と最悪解の位置を管理する
        - 必要ならメモリを更新する
        - 最終結果を表示する

    という、Harmony Search 全体の制御装置である。

    これは

        GA の Population
        PSO の Swarm
        ABC の FlowerSet
        CS の NestSet

    に相当する役割と考えると分かりやすい。

    特に HS では、

        「メモリに何が保存されているか」

    が探索の質を直接左右するため、
    HarmonyMemory がアルゴリズムの中心クラスになる。

    ------------------------------------------------------------
    このコードで想定される問題設定
    ------------------------------------------------------------

    これまでの Dataset クラス群との整合性から考えると、
    各 Harmony は

        標準偏回帰係数ベクトル

    を候補解として持ち、
    その評価値は

        標準化済みデータに対する二乗誤差和

    のような「小さいほど良い」量であると考えるのが自然である。

    つまり Harmony Search 全体としては、

        回帰係数ベクトル空間において
        損失を最小化する

    問題を解いていると解釈できる。

    このとき「良いハーモニー」とは、

        value が小さい Harmony

    を意味する。

    ------------------------------------------------------------
    Harmony Search における典型的な更新の考え方
    ------------------------------------------------------------

    Harmony Search では、新しい候補解 newHarmony を作るとき、
    各次元の値を

        1. 既存のハーモニーメモリから選ぶ
        2. 必要なら少し微調整する
        3. ある確率では完全ランダムに生成する

    という形で決めることが多い。

    その後、

        newHarmony が現在メモリ内の最悪解より良ければ
        その最悪解を newHarmony で置き換える

    という流れをとる。

    つまり HS は、

        exploit:
            記憶された良い値を再利用する
        explore:
            微調整やランダム生成で新しい可能性を試す

    をバランスさせる手法である。

    ------------------------------------------------------------
    この設計で best / worst を明示的に持つ意味
    ------------------------------------------------------------

    このクラスは

        best
        worst

    を持っている。

    これは非常に重要である。

    best:
        現在のハーモニーメモリ内で最も良い Harmony の添え字

    worst:
        現在のハーモニーメモリ内で最も悪い Harmony の添え字

    HS では新しい Harmony を作ったあと、
    それが worst より良ければメモリ更新するため、
    worst の追跡は特に重要である。

    また最終結果表示では best を見ればよいので、
    best の保持も自然である。

    したがってこの設計は、

        メモリ内の上端（最良）
        と
        下端（最悪）

    を明示的に管理することで、
    更新ロジックを簡潔にしている。
*/

#include "Harmony.h"

class Harmony;
class Dataset;

class HarmonyMemory {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        fileName で指定されたデータセットを読み込み、
        Dataset を生成し、
        初期ハーモニーメモリを構成する複数の Harmony を用意する。

        典型的にはここで

            - dataset の生成
            - harmony 配列の確保
            - 各 Harmony の初期化
            - best / worst の初期計算
            - newHarmony の作業領域生成

        を行う。

        つまりコンストラクタ終了時点で、
        Harmony Search を開始できる状態が整っているのが自然である。
    */
    HarmonyMemory(char* fileName);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        HarmonyMemory は通常、

            - Dataset
            - Harmony 配列
            - newHarmony

        などを所有するため、
        それらの解放責任を持つ。

        メモリベース探索では複数候補解を保持するので、
        こうした動的メモリ管理は重要である。
    */
    ~HarmonyMemory();

    /*
        --------------------------------------------------------
        update()
        --------------------------------------------------------

        ハーモニーメモリを 1 回更新する。

        これは Harmony Search の 1 ステップ本体に対応する。

        典型的には

            1. newHarmony を生成する
            2. newHarmony の評価値を計算する
            3. newHarmony が worst より良ければ置換する
            4. best / worst を再計算する

        という流れになる。

        つまり update() は

            「メモリを参照して新解を作り、
             メモリを改善する」

        という Harmony Search の中核操作そのものである。
    */
    void update();

    /*
        --------------------------------------------------------
        printResult()
        --------------------------------------------------------

        最終的な最良ハーモニーを表示する。

        今回の問題設定では、

            - 最良の標準偏回帰係数ベクトル
            - それを元スケールへ戻した偏回帰係数
            - 重回帰式

        などを表示することが期待される。

        つまり printResult() は、
        最適化の数値結果を人間が解釈可能な形へ戻す出口である。
    */
    void printResult();

    /*
        dataset:
            問題データを保持する。

            各 Harmony の候補解がどんな意味を持ち、
            その評価値がどう計算されるかは、
            最終的に dataset を通じて決まる。

            したがって dataset は、
            HarmonyMemory が解く最適化問題の定義そのものである。
    */
    Dataset* dataset;

    /*
        harmony:
            現在のハーモニー集合を表す配列。

            各 harmony[i] は 1 個の候補解であり、
            その位置ベクトルと評価値を持つと考えられる。

            この配列全体が、現在のハーモニーメモリを表している。

            Harmony Search ではこのメモリの内容そのものが
            探索性能を左右するため、
            harmony 配列はアルゴリズムの核心である。
    */
    Harmony** harmony;

    /*
        best:
            現在のハーモニーメモリ内で最も良い Harmony の添え字。

            最小化問題であれば、
            最も value が小さい Harmony を指す。

            これを持っておくことで、
            最良解参照や最終結果表示が簡潔になる。
    */
    int best;

private:
    /*
        --------------------------------------------------------
        newHarmony
        --------------------------------------------------------

        新しく生成する候補解を表す作業用 Harmony。

        現在の harmony 配列のどれかをいきなり上書きするのではなく、
        まず newHarmony として仮の候補解を作り、
        それがメモリに入れる価値があるか評価してから採用する。

        これにより

            - 現在のメモリ内容を壊さずに比較できる
            - 候補受理を安全に行える
            - 更新処理を明確に分離できる

        という利点がある。

        つまり newHarmony は、
        Harmony Search における「新しく即興生成されたハーモニー」
        を一時的に保持する作業領域である。
    */
    Harmony* newHarmony;

    /*
        worst:
            現在のハーモニーメモリ内で最も悪い Harmony の添え字。

            HS では通常、

                newHarmony が worst より良ければ
                worst を newHarmony で置き換える

            という形でメモリ更新を行う。

            したがって worst を明示的に持つことは、
            更新判定を O(1) に近い形で簡潔に書くために重要である。

            つまり worst は、
            Harmony Search における
            「メモリから追い出される候補」
            を表す重要な指標である。
    */
    int worst;
};

#endif


/*
    ============================================================
    まとめ
    ============================================================

    この HarmonyMemory 設計は、ハーモニーサーチの全体像を
    かなり素直に表現している。

    - Dataset
      問題定義を担当する

    - Harmony
      1 個の候補解を担当する

    - HarmonyMemory
      候補解メモリ全体を管理し、
      新解生成とメモリ更新を制御する

    という分離ができているため、
    アルゴリズムの見通しが良い。

    特に HS では、

        「これまでの良い解を記憶しておく」

    ことが本質であるため、
    HarmonyMemory が best / worst を明示的に持つ設計は非常に自然である。

    必要なら次に、

        - Harmony.h
        - main.cpp
        - HarmonyMemory.cpp
        - Harmony.cpp

    についても同じ粒度で、
    理論背景つきの詳細コメント版を書ける。
*/
