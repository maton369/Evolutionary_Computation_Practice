//============================================================
// File: main.cpp
//============================================================

#include "HarmonyMemory.h"

/*
    ============================================================
    main.cpp
    ============================================================

    このファイルは、ハーモニーサーチ
    (Harmony Search, HS)
    全体の実行フローを記述する。

    main 自体は短いが、これは設計として望ましい。
    なぜなら、アルゴリズムの詳細は

        - Dataset
        - Harmony
        - HarmonyMemory

    に分担されており、
    main は

        「全体を何回繰り返すか」
        「どのタイミングで結果を表示するか」

    だけを担当しているからである。

    つまり main は、
    ハーモニーサーチの外側の制御を行う最上位の枠組みである。

    ------------------------------------------------------------
    ハーモニーサーチの大まかな流れ
    ------------------------------------------------------------

    ハーモニーサーチでは、
    まず複数の候補解を「ハーモニーメモリ」に保存する。

    その後、反復ごとに

        1. 既存のハーモニーメモリを参照しながら
           新しい Harmony を生成する

        2. その新しい Harmony を評価する

        3. それが現在の最悪解より良ければ
           ハーモニーメモリを更新する

    という流れを繰り返す。

    したがって main のループは、
    この

        「新しいハーモニーを作ってメモリを改善する」

    操作を REPEAT_NUM 回繰り返すことに相当する。

    ------------------------------------------------------------
    このコードが解いている問題
    ------------------------------------------------------------

    これまでの Dataset クラス群との整合性から考えると、
    各 Harmony は

        標準偏回帰係数ベクトル

    を候補解として持っていると解釈するのが自然である。

    そして各 Harmony の value は、
    標準化済みデータに対する

        残差平方和
        あるいは
        二乗誤差和

    のような「小さいほど良い」評価値である。

    したがってハーモニーサーチ全体としては、

        回帰係数ベクトル空間において
        value を最小化する解を探している

    と考えられる。

    ------------------------------------------------------------
    main の処理の全体像
    ------------------------------------------------------------

    この main がしていることを流れ図にすると、次のようになる。

        ┌────────────────────────────────────┐
        │              初期化                 │
        │  ・乱数 seed 設定                  │
        │  ・HarmonyMemory を生成            │
        │  ・初期ハーモニーメモリを準備      │
        └────────────────┬───────────────────┘
                         │
                         ▼
        ┌────────────────────────────────────┐
        │      反復 i = 1 ... REPEAT_NUM     │
        │      hm->update() を実行           │
        └────────────────┬───────────────────┘
                         │
                         ▼
        ┌────────────────────────────────────┐
        │ ハーモニーメモリの更新              │
        │ ・新しい Harmony を生成            │
        │ ・既存メモリと比較                 │
        │ ・必要なら最悪解を置換             │
        │ ・best / worst を更新              │
        └────────────────┬───────────────────┘
                         │
                         ▼
        ┌────────────────────────────────────┐
        │  その時点の最良評価値を表示         │
        │  harmony[best]->value             │
        └────────────────┬───────────────────┘
                         │
                         ▼
        ┌────────────────────────────────────┐
        │             最終結果表示            │
        │  ・最良係数ベクトル                │
        │  ・重回帰式                        │
        └────────────────────────────────────┘

    このように、main は
    アルゴリズム全体の反復の外枠だけを書いており、
    実際の探索ロジックは HarmonyMemory / Harmony 側に委譲している。
*/


int main()
{
    /*
        i は反復回数を表すループ変数である。

        ハーモニーサーチでは、
        各反復で 1 個の新しい Harmony を生成し、
        それによってハーモニーメモリを改善する。

        したがって i は

            「第 i 回のメモリ更新」

        に対応すると考えると分かりやすい。
    */
    int i;

    /*
        HarmonyMemory へのポインタ。

        HarmonyMemory は

            - Dataset
            - Harmony の集合
            - 最良解 best
            - 最悪解 worst
            - 新しい Harmony の作業領域

        を管理する、ハーモニーサーチ全体の中心クラスである。

        main から見ると、
        HarmonyMemory を 1 つ生成すれば、
        アルゴリズム全体を update() によって進められる設計になっている。
    */
    HarmonyMemory* hm;

    /*
        疑似乱数の seed を現在時刻で初期化する。

        ハーモニーサーチは確率的最適化法であり、
        乱数は例えば次の場面で使われる。

            - 初期ハーモニーの生成
            - ハーモニーメモリから値を選ぶかどうか
            - 値調整を行うかどうか
            - ランダムに新しい値を作るかどうか

        そのため seed によって探索経路は変わる。

        実験再現性が必要なら固定 seed を用いることもあるが、
        通常の実行では time(NULL) による時刻 seed で十分である。
    */
    srand((unsigned int)time(NULL));

    /*
        HarmonyMemory を生成する。

        ここで通常はコンストラクタ内部で

            - Dataset の読み込み
            - 初期 Harmony 群の生成
            - 各 Harmony の評価
            - best / worst の初期設定

        が行われる。

        つまりこの 1 行によって、
        ハーモニーサーチを開始するための準備がすべて整う。

        "sampledata.csv" は問題データを表し、
        今回の文脈では

            説明変数と目的変数を持つ回帰データ

        であると考えられる。
    */
    hm = new HarmonyMemory((char*)"sampledata.csv");

    /*
        ハーモニーサーチのメインループ。

        i = 1 から REPEAT_NUM まで、
        ハーモニーメモリを 1 回ずつ更新する。

        ここで呼ばれる

            hm->update();

        が、ハーモニーサーチにおける 1 ステップ本体に相当する。

        その内部では通常、

            1. newHarmony を生成する
            2. newHarmony を評価する
            3. それが worst より良ければ置換する
            4. best / worst を更新する

        という処理が行われる。

        つまりこの for ループは、
        ハーモニーサーチ全体の探索過程そのものを表している。
    */
    for (i = 1; i <= REPEAT_NUM; i++) {

        /*
            ハーモニーメモリを 1 回更新する。

            ここでの update() は、
            音楽的比喩でいえば

                「新しい和音を即興で作ってみて、
                 それが悪い和音より良ければ記憶に残す」

            という操作に対応する。

            最適化の観点では、
                「新しい候補解を作って、メモリを改善する」
            処理である。
        */
        hm->update();

        /*
            現在時点の最良評価値を表示する。

            ここで

                hm->harmony[hm->best]->value

            を表示していることから、
            HarmonyMemory 内では

                best が現在の最良 Harmony の添え字

            を表していると分かる。

            これにより、各反復後に

                - 探索が改善しているか
                - 最良値が下がっているか
                - 停滞していないか

            を観察できる。

            今回の問題設定では value は誤差のような量なので、
            小さくなっていくほど探索が成功していると解釈できる。
        */
        printf("%d回目：最良評価値%f\n", i, hm->harmony[hm->best]->value);
    }

    /*
        最終結果を表示する。

        HarmonyMemory::printResult() の中では通常、

            1. 最良 Harmony の chord を
               元スケールの偏回帰係数へ変換する
            2. 重回帰式を表示する

        といった処理が行われる。

        つまり探索中は

            標準化空間の係数ベクトル

        を扱っていたが、
        最後にそれを

            元のデータスケールで解釈可能な形

        に戻している。

        これは

            探索は数値的に扱いやすい空間で行い、
            結果は人間が理解できる形で出力する

        という設計であり、とても自然である。
    */
    hm->printResult();

    /*
        後始末。

        HarmonyMemory は内部に

            - Harmony 群
            - newHarmony
            - Dataset

        を所有しているため、
        delete hm によってデストラクタが呼ばれ、
        それらも連鎖的に解放される。

        集団型最適化では候補解を多数持つため、
        このようなメモリ管理は重要である。
    */
    delete hm;

    /*
        正常終了を表す 0 を返す。
    */
    return 0;
}


/*
    ============================================================
    まとめ
    ============================================================

    この main.cpp の本質は、
    ハーモニーサーチ全体の外側の流れを最小限のコードで表現している点にある。

        1. 乱数初期化
        2. HarmonyMemory の生成
        3. 反復ループで update() を繰り返す
        4. 各反復の最良値を観測する
        5. 最終結果を表示する
        6. 後始末をする

    つまり main は、
    Harmony Search の理論そのものを細かく実装する場所ではなく、

        「アルゴリズムを何回繰り返すか」
        「結果をどのタイミングで観測するか」

    を決める最上位制御として機能している。

    実際の探索ロジックは HarmonyMemory / Harmony に委譲されているため、
    main は短いが意味的には十分である。

    このような設計にしておくことで、

        - アルゴリズム本体の変更
        - パラメータ調整実験
        - 出力形式の変更

    がしやすくなり、
    教材としても実験コードとしても見通しが良い。
*/
