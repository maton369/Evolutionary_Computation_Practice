#ifndef BAT_H
#define BAT_H

/*
    ============================================================
    Bat.h
    ============================================================

    このファイルでは、コウモリアルゴリズム
    (Bat Algorithm, BA)
    における 1 個のコウモリ Bat を定義する。

    ------------------------------------------------------------
    コウモリアルゴリズムの基本思想
    ------------------------------------------------------------

    コウモリアルゴリズムは、コウモリの反響定位
    (echolocation)
    を模した連続最適化アルゴリズムである。

    自然界のコウモリは、

        - 超音波を発する
        - 反射音から対象物までの位置を推定する
        - 状況に応じてパルス率や音量を変える

    という行動をとる。

    これを最適化へ写像すると、

        - 各コウモリ = 1 個の候補解
        - pos = 現在位置（現在の候補解）
        - velocity = 移動方向と移動量
        - freq = 探索の変化幅を調整する周波数
        - pulse = 局所探索を行うかどうかに関わるパラメータ
        - loudness = 新しい解を受け入れる強さや探索の荒さに関わる量

    と解釈できる。

    ------------------------------------------------------------
    BA の探索の流れ
    ------------------------------------------------------------

    典型的な BA では、各時刻で各コウモリが

        1. 周波数 freq を更新する
        2. その周波数を使って速度 velocity を更新する
        3. 速度に従って位置 pos を更新する
        4. 一定条件で最良解近傍の局所探索を行う
        5. 良い解が見つかったら受理する
        6. 受理に応じて loudness と pulse を更新する

    という流れを繰り返す。

    これにより、

        探索初期:
            大きく広く探索する
        探索後期:
            良い解の近傍を細かく詰める

    という挙動を自然に実現できる。

    ------------------------------------------------------------
    PSO や FA との違い
    ------------------------------------------------------------

    PSO との共通点:
        - pos と velocity を持つ
        - 群れとして連続空間を探索する

    PSO との違い:
        - BA は pulse と loudness によって
          局所探索と受理の性質を時間とともに変える
        - 反響定位になぞらえたパラメータ変化がある

    FA との違い:
        - FA は個体間の相対的な誘引で動く
        - BA は通常、最良解近傍の局所探索と
          速度更新を組み合わせる

    ABC との違い:
        - ABC は食糧源に対する局所変形と abandon が中心
        - BA は速度つき移動とパラメータ収束が中心

    ------------------------------------------------------------
    この問題設定での Bat の意味
    ------------------------------------------------------------

    これまでの Dataset クラス群の流れから考えると、
    この Bat も

        標準偏回帰係数ベクトル

    を位置 pos として持ち、
    evaluate() によって

        標準化済みデータに対する二乗誤差和

    のような量を評価していると考えるのが自然である。

    つまり各 Bat は、

        「ある回帰係数ベクトル候補」

    を表す。

    Population 全体としては、
    その誤差を最小化する方向へ探索していく。

    ------------------------------------------------------------
    このクラスの役割
    ------------------------------------------------------------

    Bat クラスは、1 個の候補解として次を持つ。

        - 現在位置 pos
        - 現在速度 velocity
        - 周波数 freq
        - パルス率 pulse
        - 音量 loudness
        - 現在評価値 value

    さらに、

        - move(t)
          1 時刻ぶん自分を更新する

        - evaluate(argPos)
          与えられた位置の評価値を計算する

    を担当する。

    したがって Bat は、

        候補解
        + 運動状態
        + 反響定位パラメータ
        + 評価関数

    をまとめて持つ、BA の最小単位である。
*/

#include "Dataset.h"
#include "Population.h"

class Population;

/*
    ============================================================
    定数定義
    ============================================================

    TIME_MAX:
        最終時刻。
        コウモリ群の更新を何回繰り返すかを表す。

        BA は GA のような世代交代型というより、
        時刻ごとに群れ全体が少しずつ動く動的系として見るほうが自然である。
        したがって TIME_MAX は
            「何世代」
        より
            「何ステップ動かすか」
        に近い。

    POP_SIZE:
        コウモリ群のサイズ。
        同時に何個の候補解を探索するかを表す。

        大きいほど探索空間の広い領域を観測しやすいが、
        評価コストは増える。

    PULSE_0:
        パルス率の収束値。

        BA では pulse rate が時刻とともに増加することが多く、
        探索後期ほど局所探索が起きやすくなる設計が一般的である。

        この値は、そのとき最終的に近づいていく基準値だと解釈できる。

    PULSE_R:
        パルス率の更新パラメータ。

        時刻 t に応じて pulse をどれくらい速く収束させるかを決める。

        一般に pulse は探索初期に低く、
        後半に高くなることで、
        初期は大域探索、後半は局所探索に寄る。

    LOUD_0:
        音量の初期値。

        BA では loudness は通常、
        探索初期に大きく、時刻とともに減少する。

        直感的には、
        初期ほど大胆に候補を受け入れ、
        後半ほど慎重になる、という挙動に対応する。

    LOUD_R:
        音量の変化率。

        loudness の減衰の速さを決めるパラメータである。

        大きいと音量が比較的保たれ、
        小さいと急速に探索が保守的になる。

    FREQ_MIN, FREQ_MAX:
        周波数の最小値・最大値。

        BA では各コウモリがランダムに周波数を持ち、
        それによって速度更新の大きさが変わる。

        周波数が探索ステップ幅に影響するため、
        このレンジが探索の粗さを決める重要なパラメータになる。

    BEST_RATE:
        良いコウモリと判断する割合。

        これは群れの上位何割を「良い個体」とみなし、
        その近傍を重点的に局所探索するか、
        といった用途に使われると考えられる。

        BA 実装では最良個体 1 個だけを見る場合もあるが、
        上位群を扱うと探索が少し安定しやすいことがある。

    COEF_MIN, COEF_MAX:
        回帰係数探索範囲。

        この問題では各 Bat の位置 pos が
        標準偏回帰係数ベクトルを表していると考えられるので、
        各次元はこの範囲内で初期化・更新される想定である。

    RAND_01:
        0 以上 1 以下の一様乱数。

        周波数の生成、局所探索の揺らぎ、候補受理判定など、
        BA の確率的挙動の基本となる。
*/
#define TIME_MAX 500
#define POP_SIZE 20
#define PULSE_0 0.5
#define PULSE_R 0.9
#define LOUD_0 1.0
#define LOUD_R 0.9
#define FREQ_MIN 0
#define FREQ_MAX 1
#define BEST_RATE 0.2
#define COEF_MIN -1
#define COEF_MAX 1
#define RAND_01 ((double) rand() / RAND_MAX)

class Bat {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        このコウモリが属する Population を受け取り、
        その Population 上で探索する 1 個体として初期化する。

        典型的にはここで

            - 位置 pos の乱数初期化
            - 速度 velocity の初期化
            - 周波数 freq の初期化
            - pulse と loudness の初期化
            - 初期評価値 value の計算

        を行う。

        つまりコンストラクタ終了時点で、
        Bat はすでに「探索可能な 1 個の候補解」として完成していることが期待される。
    */
    Bat(Population* argPop);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        pos, velocity, newPos1, newPos2 のような動的配列を持つ場合は、
        それらの解放責任を持つ。
    */
    ~Bat();

    /*
        --------------------------------------------------------
        move(t)
        --------------------------------------------------------

        1 時刻ぶん、このコウモリを更新する。

        BA の本体に対応する最重要関数である。

        引数 t は現在時刻であり、
        pulse や loudness の時間変化を入れるために必要になる。

        典型的には move(t) の中で

            1. freq を更新する
            2. velocity を更新する
            3. pos を更新する
            4. 条件に応じて良い個体の近傍 newPos1 を作る
            5. 条件に応じてランダム候補 newPos2 を作る
            6. それらを評価し、受理するか決める
            7. 受理したら loudness / pulse を更新する

        という流れになる。

        つまり move(t) は、
        BA における 1 個体の時間発展と
        探索モードの切り替えの両方を実装する関数である。
    */
    void move(int t);

    /*
        pop:
            このコウモリが属する Population。

            Bat 単独では
                - 問題次元数
                - Dataset
                - 群れの良い個体
            を知らないため、
            pop を通じて共有情報へアクセスする。

            つまり Bat は Population に埋め込まれた探索主体である。
    */
    Population* pop;

    /*
        pos:
            現在位置ベクトル。

            この問題では標準偏回帰係数ベクトルの候補を表すと考えられる。
            したがって pos の各成分は回帰係数候補である。

            ベクトル次元数は通常
                pop->dataset->exVarNum
            に一致する。
    */
    double* pos;

    /*
        velocity:
            現在速度ベクトル。

            BA は PSO と同様に位置と速度を持つ型のアルゴリズムである。
            velocity は次時刻の移動量を表し、
            大域探索の動的な挙動を担う。

            ただし BA は PSO と違い、
            これに周波数・局所探索・受理判定が加わる点が特徴である。
    */
    double* velocity;

    /*
        freq:
            現在の周波数。

            BA では周波数が速度更新に影響し、
            探索の変化幅やリズムを調整する。

            周波数が変わることで、
            同じベスト方向を見るとしても
            個体ごとの移動の仕方が変化する。
    */
    double freq;

    /*
        pulse:
            パルス率。

            BA では一般に、
            pulse が高いほど局所探索を行いやすくなる。

            時刻とともに上がる設計が多く、
            それにより探索後半ほど良い解近傍を細かく詰める挙動を作る。

            つまり pulse は、
            「探索の局所化の程度」
            を制御するパラメータと解釈できる。
    */
    double pulse;

    /*
        loudness:
            音量。

            BA では一般に、
            loudness が大きいほど新しい候補を受け入れやすく、
            時刻とともに小さくしていく。

            これにより、
                初期は大胆な探索
                後半は慎重な収束
            が実現される。

            したがって loudness は、
            「受理の寛容さ」や「探索の荒さ」
            を表す重要な内部状態である。
    */
    double loudness;

    /*
        value:
            現在位置 pos における評価値。

            これまでの流れから考えると、
            標準化済みデータに対する二乗誤差和のような
            「小さいほど良い」値であると解釈するのが自然である。

            つまり BA 全体は最小化問題として動いていると読める。
    */
    double value;

private:
    /*
        --------------------------------------------------------
        evaluate(argPos)
        --------------------------------------------------------

        与えられた位置 argPos に対する評価値を計算する。

        この関数が問題固有の目的関数に対応する。
        つまり BA が最終的に何を最適化しているかは、
        この関数で定まる。

        今回の問題設定では、
        argPos を回帰係数ベクトルとみなし、
        標準化済みデータに対する二乗誤差和を返すと考えるのが自然である。

        ここで現在位置 pos ではなく引数 argPos を取っている点は重要である。
        これは

            - 仮の新候補 newPos1
            - 仮の新候補 newPos2

        を現在位置へ代入する前に評価するためだと考えられる。

        つまり evaluate(argPos) は、
        候補受理前の比較用評価関数として設計されている可能性が高い。
    */
    double evaluate(double* argPos);

    /*
        newPos1:
            新しい位置候補 1（良いコウモリの近く）。

            BA では pulse 条件などに応じて、
            最良個体あるいは上位個体の近傍を局所探索することが多い。

            newPos1 はそのような
                exploit 寄りの候補
            を一時的に保持するための作業領域だと考えられる。

            つまり
                「良い個体の近くを細かく調べる」
            ための候補位置である。
    */
    double* newPos1;

    /*
        newPos2:
            新しい位置候補 2（ランダム）。

            こちらは
                explore 寄りの候補
            を表すと考えられる。

            BA では大域移動やランダム揺らぎを使って、
            まだ十分調べていない領域も試す必要がある。

            newPos2 はそのような
                「より自由度の高い新候補」
            を一時的に保持するための作業領域だと解釈できる。

            つまり Bat は move(t) の中で、
                exploit 用候補
                と
                explore 用候補
            の両方を使い分ける設計になっている可能性が高い。
    */
    double* newPos2;
};

#endif
