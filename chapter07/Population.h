//============================================================
// File: Population.h
//============================================================

#ifndef POPULATION_H
#define POPULATION_H

/*
    ============================================================
    Population.h
    ============================================================

    このファイルでは、コウモリアルゴリズム
    (Bat Algorithm, BA)
    における「コウモリ群全体」を表す Population クラスを定義する。

    ------------------------------------------------------------
    コウモリアルゴリズムの基本的な考え方
    ------------------------------------------------------------

    コウモリアルゴリズムは、コウモリの反響定位
    (echolocation)
    を模した連続最適化アルゴリズムである。

    自然界のコウモリは、超音波を発し、その反射から
    周囲の障害物や獲物の位置を推定する。
    最適化アルゴリズムではこの振る舞いを抽象化し、

        - 各コウモリ = 1 個の候補解
        - コウモリの位置 = 探索空間内の現在解
        - 群れの中で良い個体 = 有望な解
        - 周波数・速度・位置更新 = 探索の仕組み
        - 局所探索 = 有望解の近傍探索

    と解釈する。

    ------------------------------------------------------------
    BA の探索の特徴
    ------------------------------------------------------------

    BA は、PSO と似て
        「位置」と「速度」
    を持つ個体群ベース最適化だが、
    PSO と異なり「反響定位」に対応する概念として

        - frequency（周波数）
        - loudness（音の大きさ）
        - pulse rate（パルス放射率）

    を使うことが多い。

    典型的には、

        1. 良い解に向かって大域的に移動する
        2. ある条件で最良解近傍の局所探索を行う
        3. 良い解が見つかるにつれて
           loudness は下がり、
           pulse rate は上がる

    という振る舞いをする。

    直感的には、

        探索初期:
            広く飛び回る
        探索後期:
            良い場所の周辺を細かく詰める

    という exploit / explore の切り替えを、
    反響定位パラメータで実現するアルゴリズムだといえる。

    ------------------------------------------------------------
    このコードで想定される問題
    ------------------------------------------------------------

    これまでの Dataset クラス群の流れから考えると、
    この BA でも各個体の位置は

        標準偏回帰係数ベクトル

    を表し、評価値は

        標準化済みデータに対する二乗誤差和

    のような量だと解釈するのが自然である。

    つまり各 Bat は、
    「ある回帰係数ベクトル候補」を表し、
    Population 全体として誤差を最小化する方向へ探索している。

    ------------------------------------------------------------
    Population クラスの役割
    ------------------------------------------------------------

    Population は単なる Bat の配列ではない。

    Population は、

        - 問題データ Dataset を保持する
        - 複数の Bat を保持する
        - 各時刻でコウモリ群全体を動かす
        - 群れの最良個体を記録する
        - 最終結果を表示する

    という、BA 全体の制御装置である。

    これは

        GA の Population
        ACO の Colony
        ABC の FlowerSet
        PSO の Swarm
        FA の Population

    と同様に、集団メタヒューリスティクスにおける
    「全体管理クラス」に相当する。

    ------------------------------------------------------------
    BA と他アルゴリズムとの違い
    ------------------------------------------------------------

    PSO との違い:
        PSO は pBest / gBest を中心とした
        記憶ベースの速度更新が中心である。

        BA も最良解に向かうが、
        さらに局所探索と反響定位パラメータ
        (loudness / pulse rate) の変化を導入する点が異なる。

    FA との違い:
        FA は個体同士の相対的な明るさ比較による
        ペアワイズ相互作用が中心である。

        BA は通常、
        群れ全体の良い解を参照しながら
        各個体が移動する形を取りやすい。

    ABC との違い:
        ABC は食糧源に対する局所探索と abandonment が中心である。
        BA は位置・速度・局所探索確率を使う連続動的系に近い。

    ------------------------------------------------------------
    このヘッダで読み取れる設計の意図
    ------------------------------------------------------------

    この断片では Population が

        - dataset
        - bat
        - bestPos
        - bestValue

    を持っている。

    したがって設計としては、

        1. コンストラクタで初期コウモリ群を生成
        2. 評価値順に並べ替え
        3. 先頭または best を最良個体として保持
        4. move(t) で各時刻の更新
        5. 最後に最良解を回帰式として表示

    という流れを想定していると考えられる。

    特に `sort(int lb, int ub)` を持っているため、
    個体群を「良い順」に整列させて、
    先頭個体や上位個体を扱いやすくする設計になっていると読める。

    これは GA のソートやエリート保存に近い発想であり、
    BA においても
        「最良個体をすぐ参照できる」
    という利点がある。
*/

#include "Bat.h"

class Bat;
class Dataset;

class Population {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        fileName で指定されたデータセットを読み込み、
        Dataset を生成し、
        その上で探索するコウモリ群を初期化する想定である。

        典型的にはここで

            - dataset の生成
            - bat 配列の確保
            - 各 Bat の初期化
            - 初期評価
            - 必要なら sort()
            - bestPos / bestValue の初期設定

        を行う。

        つまり Population 生成直後に、
        すでに探索を開始できる状態が整っているのが自然である。
    */
    Population(char* fileName);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        Population は通常、

            - Dataset
            - Bat 配列
            - bestPos

        などを所有するため、
        それらの解放責任を持つ。

        集団サイズが大きいことが多いため、
        動的メモリ管理は重要である。
    */
    ~Population();

    /*
        --------------------------------------------------------
        move(t)
        --------------------------------------------------------

        コウモリ群を 1 時刻ぶん更新する。

        引数 t は現在時刻であり、
        BA では時刻に応じて

            - loudness の減衰
            - pulse rate の増加
            - 探索の局所化

        などを行うことが多いので、
        move(t) という形は自然である。

        典型的にはこの関数の中で、

            1. 各 Bat の frequency を決める
            2. velocity を更新する
            3. pos を更新する
            4. 条件に応じて局所探索する
            5. 新しい位置を評価する
            6. 良い解なら採用する
            7. Population 全体の best を更新する
            8. 必要なら sort する

        といった処理が行われる。

        つまり move(t) は、
        BA における 1 ステップの時間発展そのものに対応する。
    */
    void move(int t);

    /*
        --------------------------------------------------------
        printResult()
        --------------------------------------------------------

        最終的な最良コウモリの位置 bestPos を、
        問題文脈に応じて解釈可能な形で表示する。

        今回の問題設定では、
        bestPos は標準偏回帰係数ベクトルだと考えられるので、
        最終的には

            - 元スケールの偏回帰係数へ変換する
            - 重回帰式を表示する

        といった処理が期待される。

        つまり printResult() は、
        最適化の数値結果を人間が理解できる形へ戻す出口である。
    */
    void printResult();

    /*
        dataset:
            問題データを保持する。

            各 Bat の位置 pos が何を意味し、
            その評価値がどう計算されるかは、
            最終的に dataset を通じて定まる。

            したがって dataset は、
            Population が解く最適化問題そのものを表す基盤である。
    */
    Dataset* dataset;

    /*
        bat:
            コウモリ群のメンバを表す配列。

            各 bat[i] は 1 個の候補解であり、
            通常は

                - 現在位置
                - 速度
                - 周波数
                - loudness
                - pulse rate
                - 現在評価値

            などを持つと考えられる。

            この配列全体が、
            現時点での探索状態を表現する。
    */
    Bat** bat;

    /*
        bestPos:
            群れ全体でこれまでに見つかった最良コウモリの位置を保存するベクトル。

            位置ベクトルの次元数は通常 dataset->exVarNum に一致する。
            今回の問題では標準偏回帰係数ベクトルに相当すると考えられる。

            BA では各 Bat が最良解周辺を局所探索することが多いため、
            この bestPos は群れ全体の探索方向を決める重要な共有情報になる。
    */
    double* bestPos;

    /*
        bestValue:
            bestPos に対応する評価値。

            これまでの他のコード群との整合性から考えると、
            おそらく二乗誤差和のような
                「小さいほど良い」
            値であると読むのが自然である。

            したがって Population 全体としては、
            bestValue を最小化する方向へ探索していると考えられる。
    */
    double bestValue;

private:
    /*
        --------------------------------------------------------
        sort(lb, ub)
        --------------------------------------------------------

        コウモリ群を良い順に並び替える。

        この関数を持っていることから、
        Population は個体群を評価値順に整列し、
        最良個体をすぐ参照できる状態にする設計だと考えられる。

        典型的な用途は次の通りである。

            - 最良個体 bat[0] をすぐ参照する
            - bestPos / bestValue を簡単に更新する
            - 集団全体の順位構造を明確にする

        GA ではエリート保存のために頻繁にソートするが、
        BA でも最良個体を明示的に管理するには有効である。

        引数 lb, ub があるので、
        実装としては quicksort のような再帰ソートを想定している可能性が高い。
    */
    void sort(int lb, int ub);
};

#endif
