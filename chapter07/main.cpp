//============================================================
// File: main.cpp
//============================================================

#include "Population.h"

/*
    ============================================================
    main.cpp
    ============================================================

    このファイルは、コウモリアルゴリズム
    (Bat Algorithm, BA)
    全体の実行の流れを記述している。

    main 自体は非常に短いが、
    それは設計上むしろ望ましい。

    なぜなら、アルゴリズムの詳細は

        - Population
        - Bat
        - Dataset

    に分離されており、
    main は「全体の外側の制御」だけを担当しているからである。

    ------------------------------------------------------------
    このプログラム全体の役割分担
    ------------------------------------------------------------

    このコード全体では、各クラスが次の役割を持つと考えられる。

    1. Dataset
       問題データを読み込み、
       標準化や回帰式への変換を担当する。

       つまり
           「何を最適化しているのか」
       を定めるクラスである。

    2. Bat
       1 個のコウモリ、すなわち 1 個の候補解を表す。

       各 Bat は
           - 現在位置 pos
           - 速度 velocity
           - 周波数 freq
           - パルス率 pulse
           - 音量 loudness
           - 評価値 value
       を持ち、自分自身を move(t) によって更新する。

    3. Population
       コウモリ群全体を管理する。

       具体的には
           - Bat 群の保持
           - 群れ全体の最良解 bestPos / bestValue の管理
           - 各時刻での全個体更新
           - 最終結果の表示
       を担当する。

    4. main
       外側の時間ループだけを書く。

       つまり main は
           「アルゴリズム全体を何回進めるか」
       を記述する最上位の司令塔である。

    ------------------------------------------------------------
    コウモリアルゴリズムの大まかな流れ
    ------------------------------------------------------------

    この main が表している BA 全体の流れは、概念的には次のようになる。

        初期化
            ↓
        Population を作る
            ↓
        各時刻 t = 1 ... TIME_MAX で
            Population::move(t)
        を呼ぶ
            ↓
        毎時刻、最良評価値を表示する
            ↓
        最後に最良解を回帰式として表示する

    ASCII 図で描くと次のようになる。

        ┌──────────────────────────────┐
        │ 乱数 seed を初期化する         │
        └──────────────┬───────────────┘
                       │
                       ▼
        ┌──────────────────────────────┐
        │ Population を生成する         │
        │ ・Dataset 読み込み            │
        │ ・Bat 群を初期化              │
        │ ・初期 best を決定            │
        └──────────────┬───────────────┘
                       │
                       ▼
        ┌──────────────────────────────┐
        │ 時刻 t = 1, 2, ..., TIME_MAX  │
        │ について繰り返す               │
        └──────────────┬───────────────┘
                       │
                       ▼
        ┌──────────────────────────────┐
        │ Population::move(t)           │
        │ ・各 Bat を更新                │
        │ ・群れ全体の best を更新       │
        └──────────────┬───────────────┘
                       │
                       ▼
        ┌──────────────────────────────┐
        │ 現在の bestValue を表示する    │
        └──────────────┬───────────────┘
                       │
                       ▼
        ┌──────────────────────────────┐
        │ 最終結果を表示する            │
        │ ・bestPos を回帰式へ変換      │
        │ ・重回帰式を出力              │
        └──────────────────────────────┘

    つまり main は、
    BA の「時間発展の外枠」を最も簡潔な形で表現している。

    ------------------------------------------------------------
    BA を「時刻発展」として見る意味
    ------------------------------------------------------------

    GA では「世代交代」という見方が自然だが、
    BA は PSO と同様に
        「時刻ごとに個体が動いていく」
    と見るほうが自然である。

    そのためここでもループ変数は

        i ではなく t

    になっており、
        時刻 t
    という解釈を明確にしている。

    これはアルゴリズムの本質に合っている。

    BA では各時刻で各 Bat が

        - 周波数を決める
        - 速度を更新する
        - 位置を更新する
        - 局所探索候補を試す
        - 受理判定を行う
        - loudness / pulse を変える

    といった処理を行う。

    それを Population::move(t) にまとめ、
    main では「何時刻進めるか」だけを書くことで、
    コード全体の見通しがよくなっている。
*/


int main()
{
    /*
        ループ変数 t は「時刻」を表す。

        BA は世代交代型というより、
        コウモリ群が時間とともに位置を変えながら
        探索していくアルゴリズムなので、
        t という名前は意味的に自然である。
    */
    int t;

    /*
        Population へのポインタ。

        Population は
            - Dataset
            - Bat 群
            - bestPos / bestValue
        をまとめて管理する、
        BA 全体の制御クラスである。

        main から見ると、
        Population さえ作ればアルゴリズム全体を前進させられる、
        という設計になっている。
    */
    Population* pop;

    /*
        疑似乱数の seed を現在時刻で初期化する。

        コウモリアルゴリズムは確率的最適化法であり、
        乱数はたとえば次のような場面で使われる。

            - 初期位置の生成
            - 初期速度の生成
            - 周波数のランダム決定
            - 局所探索の揺らぎ
            - 候補解の受理判定

        したがって seed が変わると探索の軌跡も変わる。

        実験再現性を厳密に確保したい場合は固定 seed を使うことが多いが、
        普段の実行では time(NULL) を用いた時刻 seed で十分である。
    */
    srand((unsigned int)time(NULL));

    /*
        Population を生成する。

        ここで通常は、Population のコンストラクタ内部で

            1. Dataset を読み込む
            2. Bat 群を生成する
            3. 初期評価を行う
            4. bestPos / bestValue を初期化する

        という処理が行われる。

        つまりこの 1 行によって、
        アルゴリズム開始に必要な準備がすべて整う。

        "sampledata.csv" は問題データを表しており、
        今回の文脈では
            説明変数と目的変数を持つ回帰データ
        であると考えられる。

        各 Bat の位置 pos は、
        そのデータに対する標準偏回帰係数ベクトル候補を表す。
    */
    pop = new Population((char*)"sampledata.csv");

    /*
        BA のメインループ。

        時刻 t = 1 から TIME_MAX まで、
        Population 全体を 1 ステップずつ進める。

        ここで呼ばれる

            pop->move(t)

        は、群れ全体の 1 時刻ぶんの更新に対応する。

        その内部では通常、

            - 各 Bat の周波数更新
            - 速度更新
            - 位置更新
            - 局所探索
            - 候補受理
            - best 更新

        が行われる。

        つまりこの for ループは、
        コウモリ群の時間発展そのものを表している。
    */
    for (t = 1; t <= TIME_MAX; t++) {

        /*
            Population を 1 時刻進める。

            引数 t を渡しているのは、
            BA では時刻に応じて

                - pulse rate が増加する
                - loudness が減衰する

            などの更新を行うことが多いからである。

            つまり move(t) は単なる繰り返し処理ではなく、
            「時刻依存の探索モード変化」も含んだ関数である。
        */
        pop->move(t);

        /*
            現在の最良評価値を表示する。

            bestValue は、
            群れ全体でこれまでに見つかった最良解の評価値である。

            これを毎時刻表示することで、

                - 探索が改善しているか
                - 停滞していないか
                - 収束傾向がどうか

            を簡易的に観察できる。

            今回の問題設定ではおそらく
                二乗誤差和のような損失
            なので、
                bestValue が小さくなっていくほど良い
            と解釈するのが自然である。
        */
        printf("時刻%d：最良評価値%f\n", t, pop->bestValue);
    }

    /*
        最終結果を表示する。

        Population::printResult() の中では通常、

            1. bestPos を Dataset::setCoef() で
               元スケールの偏回帰係数へ変換する
            2. Dataset::printEquation() で
               重回帰式を表示する

        といった処理が行われる。

        つまり最適化中は
            標準化空間の係数ベクトル
        を扱っていたが、
        最後にそれを人間が解釈できる形へ戻している。

        これは
            探索は数値的に扱いやすい空間で行い、
            結果は元の問題の言葉で表示する
        という設計であり、とても自然である。
    */
    pop->printResult();

    /*
        後始末。

        Population は自分の中で

            - Bat 群
            - bestPos
            - Dataset

        を所有しているため、
        delete pop を呼ぶとデストラクタを通じて
        それらも連鎖的に解放される。

        集団メタヒューリスティクスでは個体数が多いことが多いため、
        このようなメモリ管理は重要である。
    */
    delete pop;

    /*
        正常終了を表す 0 を返す。
    */
    return 0;
}


/*
    ============================================================
    まとめ
    ============================================================

    この main.cpp の本質は、
    コウモリアルゴリズム全体の外側の流れを、
    最小限のコードで表現している点にある。

        1. 乱数初期化
        2. Population の生成
        3. 時刻ループで move(t) を繰り返す
        4. 最良評価値を観測する
        5. 最終結果を出力する
        6. 後始末をする

    つまり main は、
    BA の理論そのものを実装する場所ではなく、

        「アルゴリズムを何ステップ進めるか」
        「結果をいつ表示するか」

    を決める最上位制御として機能している。

    実際の探索ロジックは Population / Bat に委譲されているため、
    main は短いが意味的には十分である。

    このような設計にしておくことで、

        - アルゴリズム本体の変更
        - 出力方法の変更
        - 実験条件の変更

    が分離しやすくなり、
    教材としても実験コードとしても見通しが良い。
*/
