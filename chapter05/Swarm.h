#ifndef SWARM_H
#define SWARM_H

/*
    ============================================================
    Swarm.h
    ============================================================

    このファイルでは、粒子群最適化
    (PSO: Particle Swarm Optimization)
    における「粒子群全体」を表す Swarm クラスを定義する。

    ------------------------------------------------------------
    PSO の基本的な考え方
    ------------------------------------------------------------

    PSO では、候補解を「粒子 (particle)」として表現する。
    各粒子は探索空間内のある位置 pos を持ち、
    その位置に対する評価値 value を持つ。

    さらに粒子は「速度 velocity」を持ち、
    その速度に従って次の時刻に移動する。

    PSO の特徴は、各粒子が次の 2 種類の良い位置を参考にしながら
    速度を更新する点にある。

    1. パーソナルベスト (pBest)
       - その粒子自身がこれまでに見つけた最良位置

    2. グローバルベスト (gBest)
       - 粒子群全体がこれまでに見つけた最良位置

    つまり PSO は、

        「自分の成功体験」
        と
        「集団全体の成功体験」

    を同時に参照しながら粒子が動く探索法である。

    ------------------------------------------------------------
    Swarm クラスの役割
    ------------------------------------------------------------

    Swarm は単なる Particle の配列ではない。

    Swarm は、

    - 問題データ Dataset を保持する
    - 複数の粒子 Particle を保持する
    - 全粒子を 1 ステップ進める
    - 粒子群全体の最良解 gBest を管理する
    - 最後に結果を表示する

    という、PSO 全体の制御装置である。

    GA でいう Population、
    ACO でいう Colony、
    ABC でいう FlowerSet

    に相当する役割と考えると分かりやすい。

    ------------------------------------------------------------
    このコードで想定される問題
    ------------------------------------------------------------

    これまでの Dataset クラスの流れから考えると、
    この PSO も

        標準偏回帰係数ベクトル

    を探索し、

        標準化済みデータに対する二乗誤差和

    のような評価値を最小化する構造だと考えられる。

    つまり各粒子の位置 pos は、
    回帰係数ベクトルの候補を表す。

    このとき PSO は、
    「回帰係数空間の中を粒子群が飛び回りながら、
     誤差の小さい位置を探す」
    と解釈できる。
*/

#include "Particle.h"

class Particle;
class Dataset;

class Swarm {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        fileName で指定されたデータセットを読み込み、
        Dataset を生成し、
        その上で探索する粒子群 Particle を初期化する想定である。

        Swarm は PSO の開始時点で

        - 問題定義
        - 初期粒子群
        - グローバルベスト記録領域

        を用意する必要がある。
    */
    Swarm(char* fileName);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        Swarm は通常、
        - Dataset
        - Particle 配列
        - gBestPos
        などを所有するため、
        それらの解放責任を持つ。
    */
    ~Swarm();

    /*
        --------------------------------------------------------
        move()
        --------------------------------------------------------

        粒子群を 1 ステップ進める。

        典型的には、

        1. 各粒子が速度 velocity を更新する
        2. 更新した速度で位置 pos を更新する
        3. 新しい位置を評価する
        4. 必要なら pBest を更新する
        5. 必要なら Swarm 全体の gBest を更新する

        という流れになる。

        つまり move() は、
        PSO の 1 時刻ぶんの状態遷移そのものに対応する。

        main 側でこれを TIME_MAX 回繰り返すことで、
        粒子群が時間発展しながら良い解へ近づいていく。
    */
    void move();

    /*
        --------------------------------------------------------
        printResult()
        --------------------------------------------------------

        最終的な最良解を表示する。

        今回の問題設定では、
        - 最良の標準偏回帰係数ベクトル
        - それを元スケールに戻した重回帰式
        - 最良評価値

        などを表示することが考えられる。
    */
    void printResult();

    /*
        dataset:
            問題データを保持する。

            粒子の位置 pos がどんな意味を持ち、
            その評価値がどう計算されるかは、
            最終的に dataset を通じて決まる。

            したがって dataset は
            「PSO が解く問題の定義」
            に相当する。
    */
    Dataset* dataset;

    /*
        particle:
            粒子群のメンバである。

            各 particle[i] は
            - 現在位置 pos
            - 速度 velocity
            - 現在評価値 value
            - パーソナルベスト pBest

            を持つ。

            この配列全体が、PSO における現在の探索状態を表す。
    */
    Particle** particle;

    /*
        gBestPos:
            粒子群全体でこれまでに見つかった最良位置。

            PSO の大きな特徴は、
            各粒子がこの gBestPos に引き寄せられることである。

            したがって gBestPos は、
            集団全体の「共有された成功体験」
            を表す中心的な情報である。
    */
    double* gBestPos;

    /*
        gBestValue:
            gBestPos に対応する評価値。

            今回の流れでは、これが小さいほど良いと考えるのが自然である。
            つまり PSO 全体としては最小化問題を解いていると解釈できる。
    */
    double gBestValue;
};

#endif
