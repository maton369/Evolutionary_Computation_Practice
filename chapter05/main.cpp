#include "Swarm.h"

/*
    ============================================================
    main.cpp
    ============================================================

    このファイルは、粒子群最適化全体の実行フローを記述する。

    main 自体は短いが、
    その短さは責務分離がうまくできていることを示している。

    実際の役割分担は次の通りである。

    - Dataset
      問題データと回帰式解釈を担当

    - Particle
      1 個の候補解 + 速度 + pBest を担当

    - Swarm
      粒子群全体の移動と gBest 管理を担当

    - main
      外側の時間ループだけを担当

    つまり main は、
    「PSO を何回進めるか」という全体の枠だけを書き、
    詳細処理は各クラスへ委譲している。
*/

int main()
{
    int t;
    Swarm* swarm;

    /*
        疑似乱数の seed を現在時刻で初期化する。

        PSO は
            - 初期位置
            - 初期速度
            - 速度更新時の乱数係数
        に乱数を使う。

        したがって seed によって探索経路が変わる。
        実験再現性が必要なら固定 seed を使うことも多い。
    */
    srand((unsigned int)time(NULL));

    /*
        Swarm を生成する。

        ここで通常は、
        - Dataset の読み込み
        - 初期粒子群の生成
        - グローバルベストの初期化
        が行われる想定である。
    */
    swarm = new Swarm((char*)"sampledata.csv");

    /*
        PSO の時間発展ループ。

        各時刻 t で swarm->move() を呼ぶ。
        これは粒子群全体を 1 ステップ進めることに対応する。

        PSO は世代交代型というより、
        「時刻ごとに全粒子が少しずつ動く」動的系として見るほうが自然である。

        したがって t は世代番号というより「時刻」に近い概念である。
    */
    for (t = 1; t <= TIME_MAX; t++) {
        swarm->move();

        /*
            各時刻のグローバルベストを表示する。

            これにより、
            粒子群全体として最良値がどのように改善していくかを観察できる。
        */
        printf("時刻%d：最良評価値%f\n", t, swarm->gBestValue);
    }

    /*
        最終結果を表示する。

        今回の問題設定では、
        - 最良の標準偏回帰係数
        - 元スケールへ戻した重回帰式
        - 最良評価値
        などが出力されることが期待される。
    */
    swarm->printResult();

    /*
        後始末。
    */
    delete swarm;
    return 0;
}
