#include "Particle.h"

/*
    ============================================================
    Particle.cpp
    ============================================================

    このファイルでは、粒子群最適化
    (PSO: Particle Swarm Optimization)
    における 1 個の粒子 Particle の具体的な振る舞いを実装している。

    ------------------------------------------------------------
    Particle の理論的な意味
    ------------------------------------------------------------

    PSO では、各粒子は「1 個の候補解」であると同時に、
    その候補解が探索空間の中を動いていく「運動体」でもある。

    つまり粒子は、

        - 現在位置 pos
        - 現在速度 velocity
        - 現在評価値 value
        - 自分自身がこれまでに見つけた最良位置 pBestPos
        - そのときの最良評価値 pBestValue

    を持つ。

    GA や ABC では候補解は主に「更新される状態」だったが、
    PSO では候補解が「速度を持って移動する点」として扱われる。
    ここが PSO の最も特徴的な点である。

    ------------------------------------------------------------
    このコードでの pos の意味
    ------------------------------------------------------------

    これまでの Dataset 側の実装から分かるように、
    この問題では

        pos[j]

    は標準化空間上の回帰係数を表している。

    したがって 1 粒子は、

        「ある標準偏回帰係数ベクトルの候補」

    に対応する。

    その良し悪しは、evaluate() の中で

        標準化済み目的変数と
        標準化済み説明変数による線形予測誤差の二乗和

    として測られる。

    つまり粒子は、
    回帰係数空間の中を飛び回りながら、
    二乗誤差が小さくなる位置を探している。

    ------------------------------------------------------------
    この実装が前提としている最適化の向き
    ------------------------------------------------------------

    evaluate() では value を二乗誤差和として計算し、

        if (pBestValue > value)

    のとき pBest を更新している。

    したがってこの実装は

        「value が小さいほど良い」

    という最小化問題を前提としている。

    回帰問題として見れば、
    value は損失関数、あるいは残差平方和に相当する。

    ------------------------------------------------------------
    PSO の速度更新式との対応
    ------------------------------------------------------------

    このコードの move() では、各次元 i に対して

        velocity[i]
          = INERTIA * velocity[i]
          + ACCEL_G * (gBestPos[i] - pos[i]) * RAND_01
          + ACCEL_P * (pBestPos[i] - pos[i]) * RAND_01

    を計算している。

    これは PSO の典型式

        v(t+1)
          = w v(t)
            + c_g r_g (gBest - x(t))
            + c_p r_p (pBest - x(t))

    に対応している。

    ここで

        w   = INERTIA
        c_g = ACCEL_G
        c_p = ACCEL_P

    である。

    つまりこの粒子は、

        - 慣性で今までの進行方向をある程度保ち
        - 群れ全体で良かった位置 gBest に引かれ
        - 自分自身が良かった位置 pBest にも引かれる

    という 3 つの力を受けて移動する。

    これが PSO の中心メカニズムである。
*/


//============================================================
// コンストラクタ
// argSwarm: 属している粒子群
//============================================================
Particle::Particle(Swarm* argSwarm)
{
    int i;

    /*
        この粒子がどの Swarm に属しているかを保存する。

        Particle 単独では
            - 問題の次元数
            - データセット
            - グローバルベスト gBest
        を知らない。

        そのため swarm を通じて

            swarm->dataset
            swarm->gBestPos

        などへアクセスする設計になっている。

        つまり Particle は独立した存在ではなく、
        Swarm に埋め込まれた探索主体である。
    */
    swarm = argSwarm;

    /*
        現在位置ベクトル pos を確保する。

        次元数は説明変数の個数 exVarNum に一致する。
        なぜなら、この問題では pos が
        標準偏回帰係数ベクトルを表しているからである。

        つまり

            pos[0], pos[1], ..., pos[exVarNum-1]

        は各説明変数に対する回帰係数候補である。
    */
    pos = new double[swarm->dataset->exVarNum];

    /*
        速度ベクトル velocity を確保する。

        PSO の特徴は、候補解が単に存在するだけでなく
        「どの方向へどれくらい動こうとしているか」
        も状態として持つ点にある。

        velocity[i] は i 次元目の移動量に対応する。

        これにより粒子は、
            位置だけでなく運動の勢い
        も保持できる。
    */
    velocity = new double[swarm->dataset->exVarNum];

    /*
        初期位置と初期速度を乱数で与える。

        pos は探索範囲 [COEF_MIN, COEF_MAX] の中でランダム初期化する。
        velocity も同じ範囲で乱数初期化している。

        初期位置をばらけさせることで、
        粒子群は探索空間の複数領域を同時に調べられる。

        初期速度をランダムにすることで、
        開始直後から粒子群に多様な移動方向が生まれる。
        これは探索初期の広がりに寄与する。

        なお、理論的には velocity の初期範囲は
        pos と別に小さめへ設定することも多いが、
        このコードでは同じレンジを使っている。
    */
    for (i = 0; i < swarm->dataset->exVarNum; i++) {
        pos[i] = COEF_MIN + (COEF_MAX - COEF_MIN) * RAND_01;
        velocity[i] = COEF_MIN + (COEF_MAX - COEF_MIN) * RAND_01;
    }

    /*
        pBestPos を確保する。

        pBestPos は、この粒子自身がこれまでに経験した最良位置を保存する。

        PSO では各粒子が
            「自分が以前うまくいった場所」
        を記憶しており、
        それが移動式の 1 項を構成する。

        つまり pBestPos は、
        粒子の個人的記憶に相当する。
    */
    pBestPos = new double[swarm->dataset->exVarNum];

    /*
        pBestValue は最初はまだ未設定なので、十分大きい値で初期化する。

        このコードでは最小化問題なので、
        最初に非常に大きい値を入れておけば、
        最初の evaluate() で計算された value は必ず改善として採用される。

        つまり、初回評価時に

            現在位置 = 初期 pBest

        になるようにしている。
    */
    pBestValue = DBL_MAX;

    /*
        初期位置の評価を行う。

        ここで

            - value を計算し
            - 必要なら pBestPos / pBestValue を更新する

        ので、コンストラクタ終了時点で
        この粒子はすでに

            現在位置
            現在評価値
            パーソナルベスト

        をすべて持った状態になっている。
    */
    evaluate();
}


//============================================================
// デストラクタ
//============================================================
Particle::~Particle()
{
    /*
        pos, velocity, pBestPos はいずれも new[] で確保した動的配列なので、
        delete[] で解放する。

        Particle は自分の位置・速度・個人記憶を所有しているため、
        それらの後始末は自分で行う。
    */
    delete[] pos;
    delete[] velocity;
    delete[] pBestPos;
}


//============================================================
// 粒子を移動する
//============================================================
void Particle::move()
{
    int i;

    /*
        この関数は、PSO における「1 粒子の 1 時刻ぶんの更新」を表す。

        各次元 i について

            1. 速度を更新する
            2. 位置を更新する

        その後で

            3. 新しい位置を評価する

        という流れになっている。

        つまり move() は、
        粒子の時間発展そのものである。
    */
    for (i = 0; i < swarm->dataset->exVarNum; i++) {

        /*
            ----------------------------------------------------
            1. 速度更新
            ----------------------------------------------------

            速度更新式:

                velocity[i]
                  = INERTIA * velocity[i]
                  + ACCEL_G * (swarm->gBestPos[i] - pos[i]) * RAND_01
                  + ACCEL_P * (pBestPos[i] - pos[i]) * RAND_01;

            これは PSO の代表的な更新式であり、
            3 つの力の合成とみなせる。

            ----------------------------------------------------
            (a) 慣性項
                INERTIA * velocity[i]
            ----------------------------------------------------

            現在の速度をある程度そのまま維持する項である。

            これにより粒子は、
            すぐに止まるのではなく、ある程度「勢い」を持って動く。

            INERTIA が大きいほど探索は広がりやすく、
            小さいほど収束しやすい傾向がある。

            つまり INERTIA は、
            探索の広がりと安定収束のバランスを決める重要パラメータである。

            ----------------------------------------------------
            (b) グローバルベスト項
                ACCEL_G * (gBestPos[i] - pos[i]) * RAND_01
            ----------------------------------------------------

            群れ全体で最も良かった位置 gBestPos に引かれる力である。

            これは PSO における「社会的影響」を表す。
            粒子は、群れ全体の成功体験を参考にして動く。

            gBestPos[i] - pos[i] は
                現在位置から gBest 方向へのベクトル差
            であり、
            これに係数と乱数を掛けることで、
            どれくらい強く引かれるかを調整している。

            ACCEL_G が大きいほど、
            粒子群は gBest へ強く引き寄せられやすい。

            ----------------------------------------------------
            (c) パーソナルベスト項
                ACCEL_P * (pBestPos[i] - pos[i]) * RAND_01
            ----------------------------------------------------

            この粒子自身がこれまでにうまくいった位置 pBestPos に引かれる力である。

            これは PSO における「個人的記憶」を表す。
            粒子は、自分が以前うまくいった場所へ戻ろうとする。

            ACCEL_P が大きいほど、
            個々の粒子は自分の成功体験を強く重視する。

            ----------------------------------------------------
            (d) 乱数係数 RAND_01
            ----------------------------------------------------

            gBest 項と pBest 項に乱数を掛けることで、
            毎回まったく同じ deterministic な動きではなく、
            確率的な揺らぎを持った探索になる。

            これにより、
            粒子群全体が単調に一直線で収束するのではなく、
            適度なばらつきを持ちながら探索できる。

            これは局所解回避や多様性維持にも寄与する。
        */
        velocity[i] = INERTIA * velocity[i]
                    + ACCEL_G * (swarm->gBestPos[i] - pos[i]) * RAND_01
                    + ACCEL_P * (pBestPos[i] - pos[i]) * RAND_01;

        /*
            ----------------------------------------------------
            2. 位置更新
            ----------------------------------------------------

            新しい速度で現在位置を更新する。

            式:

                pos[i] += velocity[i];

            これは離散時間の運動方程式

                x(t+1) = x(t) + v(t+1)

            に対応する。

            つまり PSO では、
            速度を更新してから、その速度に従って位置を動かす。

            この時点で粒子は新しい候補解へ移動したことになる。
        */
        pos[i] += velocity[i];
    }

    /*
        --------------------------------------------------------
        3. 新しい位置を評価する
        --------------------------------------------------------

        位置を更新しただけでは、
        その新位置が良いかどうかは分からない。

        そこで evaluate() を呼んで

            - 現在位置の評価値 value を計算し
            - 必要なら pBest を更新する

        これにより粒子は
        「新しい位置へ動いた結果、自分にとって良かったか」
        を学習する。

        この pBest の更新が、次回以降の move() の pBest 項に効いてくる。
    */
    evaluate();
}


//============================================================
// 評価値を算出し，パーソナルベストを更新する
//============================================================
void Particle::evaluate()
{
    int i, j;
    double diff;

    /*
        この関数は Particle の中で最も重要である。

        なぜなら、この粒子の現在位置 pos が
        問題に対してどれくらい良いかを定めているからである。

        この実装では value を

            標準化済み目的変数に対する
            線形回帰予測の二乗誤差和

        として計算している。

        つまり PSO が探索しているのは、
        回帰係数ベクトル pos を動かしながら
        誤差を最小化する問題である。
    */

    //--------------------------------------------------------
    // 1. 評価値を算出する
    //--------------------------------------------------------

    /*
        二乗誤差和を作るので、まず 0.0 に初期化する。
    */
    value = 0.0;

    /*
        各データ点 i について残差 diff を計算する。

        resSData[i] は標準化済み目的変数であり、
        exSData[i][j] は標準化済み説明変数である。

        現在の係数ベクトル pos に対する予測値は

            Σ pos[j] * exSData[i][j]

        なので、

            diff = 実測値 - 予測値

        を計算することになる。

        数式で書けば

            diff_i
              = y_i - Σ_j β_j x_ij

        である。
    */
    for (i = 0; i < swarm->dataset->dataNum; i++) {
        diff = swarm->dataset->resSData[i];

        for (j = 0; j < swarm->dataset->exVarNum; j++) {
            diff -= pos[j] * swarm->dataset->exSData[i][j];
        }

        /*
            残差の二乗を加算する。

            よって最終的な value は

                Σ_i (y_i - y_hat_i)^2

            となる。

            これは回帰問題における残差平方和（RSS）に相当し、
            小さいほど良いモデルである。

            二乗を取ることで、
            正負の誤差が打ち消し合わず、
            大きな誤差がより強く罰せられる。
        */
        value += pow(diff, 2.0);
    }

    //--------------------------------------------------------
    // 2. パーソナルベストを更新する
    //--------------------------------------------------------

    /*
        いま計算した value が、これまでの pBestValue より小さければ、
        現在位置 pos はこの粒子にとって新しい自己最高記録である。

        その場合、

            pBestPos   = 現在位置
            pBestValue = 現在評価値

        へ更新する。

        これにより粒子は、
        「自分が過去にうまくいった場所」を覚えていられる。

        PSO の探索性能はこの個人的記憶に強く依存している。
        もし pBest を持たなければ、
        粒子はその時点の gBest に流されるだけになり、
        探索の多様性や自律性が弱くなる。
    */
    if (pBestValue > value) {
        for (i = 0; i < swarm->dataset->exVarNum; i++) {
            pBestPos[i] = pos[i];
        }
        pBestValue = value;
    }
}


/*
    ============================================================
    まとめ
    ============================================================

    この Particle.cpp の本質は、
    PSO における 1 個の粒子を

        「位置を持つ候補解」
        かつ
        「速度と記憶によって動く探索主体」

    として実装している点にある。

    - コンストラクタ
      初期位置・初期速度・初期 pBest を作る

    - move()
      慣性・gBest・pBest に基づいて速度と位置を更新する

    - evaluate()
      現在位置の誤差を計算し、必要なら pBest を更新する

    つまり Particle は、

        現在の候補解
        + その運動状態
        + 個人的な最良記憶

    をまとめて持つ、PSO の中心的な探索単位である。

    Swarm が群れ全体の gBest を管理し、
    Particle が自分の pBest を管理することで、

        個人的記憶
        と
        社会的記憶

    の両方を使った探索が実現される。

    これが PSO の核心であり、
    このファイルはその「粒子側の力学」を表している。
*/
