#ifndef PARTICLE_H
#define PARTICLE_H

/*
    ============================================================
    Particle.h
    ============================================================

    このファイルでは、PSO における 1 個の粒子を表す Particle クラスを定義する。

    ------------------------------------------------------------
    Particle の理論的な意味
    ------------------------------------------------------------

    PSO では 1 個の粒子が 1 個の候補解に対応する。
    ただし、GA の個体や ABC の食糧源と違って、
    Particle は

        - 現在位置 pos
        - 現在速度 velocity

    の両方を持つ。

    つまり Particle は「その場に留まる解」ではなく、
    「探索空間を飛び回る動的な点」である。

    この「位置 + 速度」を持つことが PSO の本質であり、
    粒子は過去の良い位置へ引かれながら慣性的に動く。

    ------------------------------------------------------------
    Particle が持つ 3 種類の重要な状態
    ------------------------------------------------------------

    1. 現在位置 pos
       - いまの候補解

    2. パーソナルベスト pBestPos / pBestValue
       - この粒子自身がこれまでに経験した最良解

    3. グローバルベスト gBest
       - Swarm 全体で共有される最良解
       - これは Particle 自身は持たず、Swarm が保持する

    粒子は自分の pBest と swarm の gBest を見ながら移動する。

    ------------------------------------------------------------
    速度更新の意味
    ------------------------------------------------------------

    PSO の典型的な速度更新式は

        v(t+1)
          = w v(t)
            + c1 r1 (pBest - x(t))
            + c2 r2 (gBest - x(t))

    である。

    ここで

        w   : 慣性係数
        c1  : パーソナルベスト方向への加速係数
        c2  : グローバルベスト方向への加速係数
        r1, r2 : 一様乱数

    である。

    この式は、

        - 今までの進行方向をある程度維持しつつ
        - 自分がうまくいった場所へ戻ろうとし
        - 群れ全体で一番うまくいった場所にも引かれる

    という挙動を表す。

    つまり PSO は、
    物理的な運動のような見た目を持ちながら、
    実際には「記憶と社会的共有」を使った探索になっている。
*/

#include "Dataset.h"
#include "Swarm.h"

class Swarm;

/*
    ============================================================
    定数定義
    ============================================================

    TIME_MAX:
        最終時刻。
        粒子群更新を何回繰り返すかを表す。

    SWARM_SIZE:
        粒子群サイズ。
        同時に何個の候補解を飛ばすかを表す。

    INERTIA:
        慣性係数 w。
        現在の速度をどれだけ保つかを決める。

        大きいほど探索が広がりやすく、
        小さいほど収束しやすい傾向がある。

    ACCEL_P:
        パーソナルベスト方向への加速係数 c1。

    ACCEL_G:
        グローバルベスト方向への加速係数 c2。

    COEF_MIN, COEF_MAX:
        回帰係数探索範囲。
        pos の各成分を初期化・制限する範囲だと考えられる。

    RAND_01:
        0以上1以下の一様乱数。
        速度更新時のランダム係数に使う。
*/
#define TIME_MAX 1000
#define SWARM_SIZE 100
#define INERTIA 0.9
#define ACCEL_P 0.8
#define ACCEL_G 0.8
#define COEF_MIN -1
#define COEF_MAX 1
#define RAND_01 ((double) rand() / RAND_MAX)

class Particle {
public:
    /*
        コンストラクタ。

        自分が属する Swarm を受け取る。
        これにより、
        - 問題次元数
        - データセット
        - グローバルベスト

        などへアクセスできる。
    */
    Particle(Swarm* argPS);

    /*
        デストラクタ。

        pos, velocity, pBestPos などの動的配列を内部で持つなら、
        その解放責任を持つ。
    */
    ~Particle();

    /*
        move():
            粒子を 1 ステップ動かす。

            典型的には

                1. velocity を更新
                2. pos を更新
                3. evaluate() で新位置を評価
                4. 改善していれば pBest を更新
                5. 必要なら swarm の gBest 更新に寄与

            という流れになる。

            つまり Particle::move() は
            「1 個の粒子の時間発展」
            に対応する。
    */
    void move();

    /*
        swarm:
            自分が属する粒子群。

            Particle は単独では問題データや gBest を持たないため、
            swarm を通じて共有情報へアクセスする。
    */
    Swarm* swarm;

    /*
        pos:
            現在位置。

            この問題では、回帰係数ベクトル候補を表していると考えられる。
        */
    double* pos;

    /*
        velocity:
            現在速度。

            PSO では位置だけでなく速度も状態として持つ。
            これが GA や ABC と大きく異なる点である。

            velocity は次時刻の移動量そのものであり、
            探索方向と探索強度を同時に表す。
        */
    double* velocity;

    /*
        value:
            現在位置 pos における評価値。

            これが小さいほど良いと考えるのが自然である。
        */
    double value;

private:
    /*
        evaluate():
            現在位置 pos に対する評価値 value を計算する。

            問題固有の目的関数そのものであり、
            PSO が何を最適化しているかは最終的にここで決まる。
        */
    void evaluate();

    /*
        pBestPos:
            この粒子自身がこれまでに見つけた最良位置。

            PSO の粒子は「自分の成功体験」を記憶しており、
            それが速度更新式の 1 項を構成する。
        */
    double* pBestPos;

    /*
        pBestValue:
            pBestPos に対応する評価値。
        */
    double pBestValue;
};

#endif
