#include "Ant.h"

/*
    ============================================================
    Ant.cpp
    ============================================================

    このファイルでは、アリコロニー最適化（ACO: Ant Colony Optimization）における
    1匹のアリ Ant の振る舞いを実装している。

    Colony が

        - 問題全体の反復を管理し
        - フェロモンを更新し
        - 全アリを束ねる

    のに対して、Ant は

        - 1回の反復で
        - 1本の経路を
        - 局所的な情報にもとづいて構築する

    という役割を担う。

    したがって Ant は、ACO における
    「候補解を逐次的に構築する局所探索エージェント」
    である。

    ------------------------------------------------------------
    この実装が解いている問題の見方
    ------------------------------------------------------------

    このコードは、典型的には巡回セールスマン問題（TSP）のような
    「すべてのノードを1回ずつ訪問し、最後に出発点へ戻る巡回路」
    を構築する問題を想定している。

    route[k] は

        「k番目に訪問したノード番号」

    を表す。

    また totalDis は

        その巡回路の総距離

    を表す。

    ACO では、各アリがこうした巡回路を1本ずつ作り、
    短い経路ほど強いフェロモンを残すことで、
    次の反復でその経路が選ばれやすくなる。

    ------------------------------------------------------------
    ACO における Ant の理論的な役割
    ------------------------------------------------------------

    Ant は全体最適を知らない。
    代わりに、各ステップで

        - 現在地点
        - 未訪問ノード集合
        - 各候補辺のフェロモン量
        - 各候補辺のヒューリスティック情報（通常は距離の逆数）

    だけを見て、次に進むノードを確率的に選ぶ。

    その意味で Ant は、
    大域的探索器ではなく「局所ルールに従う存在」である。

    しかし、複数の Ant が同じ Field 上で行動し、
    その結果を pheromone へ反映することで、
    集団全体としては良い経路を学習していく。

    つまり ACO の面白さは、

        局所的な意思決定
            ↓
        共有記憶への書き込み
            ↓
        後続個体の行動変化

    という間接協調にある。

    このファイルは、そのうち
    「局所的な意思決定」
    と
    「自分の経路結果を共有記憶へ反映する部分」
    を担当している。
*/


//============================================================
// コンストラクタ
// argColony: 属しているコロニー
//============================================================
Ant::Ant(Colony* argColony)
{
    /*
        このアリがどの Colony に属しているかを保存する。

        Ant は単独では問題サイズやフェロモン場を知らず、
        colony を通して

            colony -> field -> nodeNum / distance / pheromone

        にアクセスする。

        つまり Ant は、
        「場に埋め込まれた探索主体」
        として設計されている。
    */
    colony = argColony;

    /*
        route を確保する。

        route は、巡回路そのものを保存する配列であり、
        長さはノード数 nodeNum に等しい。

        route[k] は
            「k番目に訪れたノード」
        を表す。

        TSP 型の問題では、すべてのノードを1度ずつ訪問するため、
        route は全ノード数ぶん必要になる。
    */
    route = new int[colony->field->nodeNum];

    /*
        candidate を確保する。

        candidate は未訪問ノード集合を管理するための補助配列である。

        このコードでは、

            candidate[j] = 1 なら未訪問
            candidate[j] = 0 なら訪問済み

        というフラグ配列として使われている。

        つまり candidate は、
        現時点で「次に進んでよいノードの集合」
        を表している。

        逐次構築型アルゴリズムでは、
        このような未訪問集合管理は非常に重要である。
    */
    candidate = new int[colony->field->nodeNum];

    /*
        出発ノードを 0 に固定する。

        この実装では route[0] = 0 としているため、
        すべてのアリは必ずノード 0 から出発する。

        巡回経路問題では、どのノードを出発点にしても
        巡回路としては同値になることが多いが、
        実装上は基準点を固定しておくと扱いやすい。
    */
    route[0] = 0;

    /*
        candidate[0] = 0 とすることで、
        出発ノード 0 はすでに訪問済みであり、
        次の遷移候補には含めないことを表す。

        これにより route 構築中にノード 0 を途中で再訪することを防ぐ。
    */
    candidate[0] = 0;

    /*
        総移動距離 totalDis を初期化する。

        route がまだ構築されていないので、
        最初は 0.0 である。
    */
    totalDis = 0.0;
}


//============================================================
// デストラクタ
//============================================================
Ant::~Ant()
{
    /*
        route と candidate は new[] で確保した動的配列であるため、
        delete[] で解放する。

        Ant は自分の経路状態と作業領域を所有しているため、
        その後始末は Ant 自身が行う。
    */
    delete[] route;
    delete[] candidate;
}


//============================================================
// 経路を選択する
//============================================================
void Ant::selectRoute()
{
    int i, j, next, next2;
    double denom, r, prob;

    /*
        この関数は、1匹のアリが1本の巡回路を構築する処理である。

        流れとしては

            1. 未訪問ノード集合を初期化する
            2. 現在地点から、次ノード候補の魅力度を集計する
            3. フェロモンベースで確率的に次ノードを選ぶ
            4. もし選べなければランダムに選ぶ
            5. route と totalDis を更新する
            6. 全ノード訪問まで繰り返す
            7. 最後に出発点へ戻る距離を加える

        となっている。

        これは ACO の「解構築フェーズ」をそのまま実装したものと考えられる。
    */


    //--------------------------------------------------------
    // 1. 未訪問ノードを初期化する
    //--------------------------------------------------------
    /*
        ノード 1 以上をすべて未訪問に設定する。

        出発ノード 0 はコンストラクタで candidate[0] = 0 にしてあり、
        すでに訪問済みとして扱っている。

        よってここでは

            1, 2, ..., nodeNum-1

        を未訪問集合として初期化する。

        毎回 selectRoute() の最初にこの初期化が必要なのは、
        Ant オブジェクトを毎反復で再利用しているためである。
    */
    for (i = 1; i < colony->field->nodeNum; i++) {
        candidate[i] = 1;
    }

    /*
        総移動距離をリセットする。

        新しい巡回路を構築するので、
        前回反復の距離を残してはいけない。
    */
    totalDis = 0.0;


    //--------------------------------------------------------
    // 2. route を順に構築していく
    //--------------------------------------------------------
    /*
        route[0] は出発点 0 で固定されている。

        ここでは

            route[1], route[2], ..., route[nodeNum-2]

        を順に決めていく。

        最後の 1 ノードはループを抜けたあとで別処理しているため、
        ここでは nodeNum - 2 回まで回している。
    */
    for (i = 0; i < colony->field->nodeNum - 2; i++) {

        //----------------------------------------------------
        // 2-1. 確率の分母を算出する
        //----------------------------------------------------
        /*
            現在ノード route[i] から、
            まだ訪れていない候補ノード j への魅力度を合計する。

            Colony 側で

                nume[a][b]

            には

                (pheromone)^PHERO_L * (1/distance)^HEU_L

            に相当する値が格納されている。

            よって denom は、
            現在地点から未訪問候補へ進むときの
            確率分布の正規化定数である。

            数式的には、

                denom = Σ nume[current][j]

            （ただし j は未訪問ノード）

            である。

            これにより各候補への選択確率は

                prob(j) = nume[current][j] / denom

            として求められる。
        */
        denom = 0.0;
        for (j = 1; j < colony->field->nodeNum; j++) {
            if (candidate[j] == 1) {
                denom += colony->nume[route[i]][j];
            }
        }


        //----------------------------------------------------
        // 2-2. 次のノードを選択する
        //----------------------------------------------------
        /*
            next は次に進むノード番号を表す。
            まだ決まっていない状態を -1 としている。
        */
        next = -1;

        /*
            ここでは、

                1. 候補の魅力度合計 denom が 0 ではない
                2. RAND_01 <= PHERO_R

            の両方を満たすとき、
            フェロモン・ヒューリスティックにもとづく確率選択を行う。

            つまり PHERO_R は、
            「フェロモンベースの意思決定を使う確率」
            として機能している。

            逆にこれを満たさない場合は、あとでランダム選択へ回る。

            この設計により、

                exploitation（既知の良い経路をたどる）
                と
                exploration（新しい経路を試す）

            のバランスを取っている。

            フェロモンだけに完全依存すると早期収束しやすくなり、
            ランダムだけでは学習が働かない。

            その中間を実現するパラメータが PHERO_R である。
        */
        if ((denom != 0.0) && (RAND_01 <= PHERO_R)) {

            /*
                0以上1以下の乱数 r を用いて、
                離散確率分布から候補ノードを1つサンプリングする。

                これは「ルーレット選択」と同じ発想で、
                各候補の確率 mass を順に引き算していき、
                r が落ちた区間に対応する候補を選ぶ。
            */
            r = RAND_01;

            for (next = 1; next < colony->field->nodeNum; next++) {
                if (candidate[next] == 1) {

                    /*
                        現在ノード route[i] から next へ進む確率。

                        nume は分子であり、
                        denom はその総和なので、
                        prob は正規化された選択確率になる。
                    */
                    prob = colony->nume[route[i]][next] / denom;

                    /*
                        r がこの候補の確率 mass の中に入ったら、
                        その候補を次ノードとして採用する。
                    */
                    if (r <= prob) {
                        break;
                    }

                    /*
                        まだ決まらない場合は、今の候補の mass を引いて
                        次候補へ進む。
                    */
                    r -= prob;
                }
            }

            /*
                浮動小数点誤差などにより、
                まれに最後まで決まらない場合がある。

                その場合は next を -1 に戻し、
                後続のランダム選択へフォールバックする。
            */
            if (next == colony->field->nodeNum) {
                next = -1;
            }
        }


        //----------------------------------------------------
        // 2-3. フェロモンベースで決まらなければランダムに選ぶ
        //----------------------------------------------------
        /*
            next == -1 であれば、
            フェロモンにもとづく確率選択では次ノードが決まらなかったことを意味する。

            このときは未訪問ノードの中から一様ランダムに選ぶ。

            この処理は探索の多様性を保つうえで重要である。

            もし常にフェロモンにもとづく選択しかしないと、
            早い段階で特定経路へ偏りすぎる危険がある。

            ランダム選択は、
            まだ十分学習されていない領域を試す役割を持つ。
        */
        if (next == -1) {

            /*
                残っている未訪問ノード数は

                    nodeNum - i - 1

                個である。

                その中から 0-based の位置 next2 をランダムに選ぶ。
            */
            next2 = rand() % (colony->field->nodeNum - i - 1);

            /*
                未訪問ノードを順に数え上げ、
                next2 番目にあたるものを選ぶ。

                これは「未訪問候補の中から一様ランダムに1個選ぶ」
                ための実装である。
            */
            for (next = 1; next < colony->field->nodeNum; next++) {
                if (candidate[next] == 1) {
                    if (next2 == 0) {
                        break;
                    } else {
                        next2--;
                    }
                }
            }
        }


        //----------------------------------------------------
        // 2-4. 選んだ次ノードを route に反映する
        //----------------------------------------------------
        /*
            次に訪問するノードを route の次位置へ書き込む。
        */
        route[i + 1] = next;

        /*
            そのノードを訪問済みにする。

            これにより以後の遷移候補から除外される。
        */
        candidate[next] = 0;

        /*
            現在ノード route[i] から next への移動距離を
            totalDis に加算する。

            totalDis は最終的にこのアリの route の良し悪しを表す評価値であり、
            putPheromone() でフェロモン追加量を決める基準になる。

            よって route 構築と totalDis 更新は不可分である。
        */
        totalDis += colony->field->distance[route[i]][next];
    }


    //--------------------------------------------------------
    // 3. 最後の 1 ノードを決める
    //--------------------------------------------------------
    /*
        上のループ終了時点では、未訪問ノードがちょうど1つだけ残っている。

        したがってここでは、その最後のノードを探して route の末尾へ入れる。

        ACO の逐次構築では、終盤になると選択の自由度がなくなるため、
        最後の1ノードはこのように決定的に埋まる。
    */
    for (next = 1; next < colony->field->nodeNum; next++) {
        if (candidate[next] == 1) {
            break;
        }
    }

    /*
        末尾ノードとして記録する。
    */
    route[colony->field->nodeNum - 1] = next;

    /*
        直前ノードから最後のノードへの移動距離を加える。
    */
    totalDis += colony->field->distance
        [route[colony->field->nodeNum - 2]][next];

    /*
        巡回路としては最後に出発点 0 へ戻る必要があるため、
        その戻り距離も加算する。

        これで totalDis は

            0 から出発
            → 全ノード訪問
            → 0 に帰還

        までを含む巡回路長になる。

        したがって totalDis は、
        TSP 型問題における完全な閉路コストを表している。
    */
    totalDis += colony->field->distance[next][0];
}


//============================================================
// フェロモンを分泌する
//============================================================
void Ant::putPheromone()
{
    int i;
    double p;

    /*
        この関数は、このアリが作った route の情報を
        共有記憶 pheromone へ反映する処理である。

        ACO の学習メカニズムはここにある。

        良い経路を通ったアリほど、
        その経路上のエッジへ強いフェロモンを残すことで、
        次の反復でその経路が選ばれやすくなる。

        つまり putPheromone() は、
        個体の経験を集団の記憶へ変換する関数である。
    */

    /*
        追加するフェロモン量 p を計算する。

        ここでは

            p = PHERO_Q / totalDis

        としている。

        したがって

            totalDis が短いほど p は大きくなる
            totalDis が長いほど p は小さくなる

        という関係になる。

        これは ACO の典型的な設計であり、
        良い（短い）経路をより強く強化することを意味する。

        PHERO_Q はスケール調整用の定数である。
    */
    p = PHERO_Q / totalDis;

    /*
        route 上の各連続エッジに対して pheromone を加算する。

        ここで注目すべきなのは、
        pheromone 行列を

            小さい方の添字 [a][b]

        に統一して更新している点である。

        つまりこの実装は、
        無向グラフ上のエッジを上三角側（または添字順に整列した側）
        に代表させて管理していると解釈できる。

        そのため

            route[i] < route[i+1]

        かどうかで格納先を分けている。

        これはメモリや更新の一貫性の観点で自然な実装である。
    */
    for (i = 0; i < colony->field->nodeNum - 1; i++) {
        if (route[i] < route[i + 1]) {
            colony->field->pheromone[route[i]][route[i + 1]] += p;
        } else {
            colony->field->pheromone[route[i + 1]][route[i]] += p;
        }
    }

    /*
        最後に、終点から出発点 0 に戻るエッジにもフェロモンを加える。

        巡回路では

            route[last] -> 0

        も経路の一部であるため、
        ここを更新しないと完全な巡回路の評価にならない。

        このコードでは出発点が 0 に固定されているため、
        0 と最後の訪問ノードとのエッジを更新している。
    */
    colony->field->pheromone[0][route[colony->field->nodeNum - 1]] += p;
}


/*
    ============================================================
    まとめ
    ============================================================

    この Ant.cpp の本質は、
    ACO における「1匹のアリの局所意思決定」と
    「その経験の共有記憶への反映」を実装している点にある。

    - コンストラクタ
      route と candidate を準備し、出発点を固定する

    - selectRoute()
      フェロモンと距離情報を使って逐次的に経路を構築する

    - putPheromone()
      構築した経路の良し悪しを pheromone へ書き込む

    つまり Ant は、

        route を作る探索主体
        かつ
        その探索結果を共有学習へ寄与する主体

    である。

    個々のアリは局所ルールしか使わないが、
    その結果が Colony 全体では集団知として蓄積される。

    これが ACO の核心であり、
    このファイルはその「個体側の力学」を表現している。
*/
