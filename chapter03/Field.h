#ifndef FIELD_H
#define FIELD_H

/*
    ============================================================
    Field.h
    ============================================================

    このファイルでは、ACO が探索を行う「場（Field）」を定義する。

    ------------------------------------------------------------
    Field の役割
    ------------------------------------------------------------

    ACO において Field は、

    - ノード集合
    - ノード間距離
    - 各エッジ上のフェロモン量

    を保持する。

    つまり Field は、最適化問題そのものを表現するクラスである。

    たとえば TSP を考えるなら、
    - ノード = 都市
    - distance[i][j] = 都市 i と j の距離
    - pheromone[i][j] = 都市 i から j への辺に蓄積されたフェロモン

    という対応になる。

    Colony や Ant は、この Field の上で行動する。
    したがって Field は「静的な問題構造」と
    「反復に応じて変化するフェロモン状態」の両方を保持する。

    ここが ACO の特徴であり、
    問題の地形そのものに記憶（フェロモン）が埋め込まれる。
*/

#include <time.h>
#include <stdlib.h>
#include <string.h>
#include <float.h>
#include <math.h>

class Field {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        fileName で指定されたファイルから問題データを読み込み、
        nodeNum や distance 行列を初期化する想定である。

        また pheromone 行列もここで初期化されることが多い。
        初期フェロモンは全辺で同じ値にする場合が一般的である。

        これにより探索開始時点では、
        特定の経路に偏らない中立的な状態から始められる。
    */
    Field(char* fileName);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        distance や pheromone は 2 次元配列として動的確保されることが多いため、
        その解放責任を持つ。
    */
    ~Field();

    /*
        --------------------------------------------------------
        nodeNum
        --------------------------------------------------------

        ノード数である。

        TSP なら都市数に対応する。
        route の長さや distance 行列のサイズなど、
        問題全体の基本スケールを決める値である。
    */
    int nodeNum;

    /*
        --------------------------------------------------------
        distance
        --------------------------------------------------------

        ノード間距離行列である。

        distance[i][j] は、ノード i からノード j への距離を表す。

        この値は通常固定であり、
        問題そのもののコスト構造を表現する。

        ACO ではこの距離をもとに、
        ヒューリスティック情報として

            1 / distance[i][j]

        のような値を使うことが多い。

        つまり distance は
        「この辺は短いので魅力的」
        という局所的な判断の根拠になる。
    */
    double** distance;

    /*
        --------------------------------------------------------
        pheromone
        --------------------------------------------------------

        各エッジ上のフェロモン量である。

        pheromone[i][j] は、ノード i から j への遷移が
        過去の探索においてどれだけ有望だったかを表す蓄積情報である。

        距離 distance が静的な問題情報なのに対し、
        pheromone は探索の進行に応じて更新される動的な学習情報である。

        ACO の本質は、
        この pheromone 行列が反復を通して変化し、
        良い経路を強調していくところにある。
    */
    double** pheromone;
};

#endif
