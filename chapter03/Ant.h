#ifndef ANT_H
#define ANT_H

/*
    ============================================================
    Ant.h
    ============================================================

    このファイルでは、ACO における「1匹のアリ」を表すクラスを定義する。

    ------------------------------------------------------------
    ACO における Ant の役割
    ------------------------------------------------------------

    ACO では、各アリは1回の反復で1本の経路を構築する。
    その経路は、フェロモンとヒューリスティック情報に基づいて
    確率的に決まる。

    したがって Ant は、

    - 現在どのコロニーに属しているか
    - どの順番でノードを巡回したか
    - 総移動距離はいくつだったか
    - 未訪問ノードは何か

    を管理する、局所的な探索主体である。

    Colony が「集団全体の制御」を担当するのに対して、
    Ant は「1本の解を構築する主体」である。

    ------------------------------------------------------------
    ACO におけるアリの意味
    ------------------------------------------------------------

    自然界のアリは局所的な情報しか持たないが、
    フェロモンを介して集団として効率的な経路を見つける。

    ACO ではこの考え方を最適化へ移植しており、
    各 Ant もまた「全体最適を直接知らない」。
    代わりに、

    - 現在地点
    - 未訪問ノード
    - フェロモン量
    - 距離などの局所ヒューリスティック

    だけを使って次の一歩を決める。

    その局所的な意思決定が、集団全体では
    良い解の発見につながる点が ACO の面白さである。
*/

#include "Field.h"
#include "Colony.h"

class Colony;

/*
    ============================================================
    定数定義
    ============================================================

    REPEAT_NUM:
        反復回数。
        ACO を何回繰り返すかを表す。

    ANT_NUM:
        アリの数。
        1反復あたりに何本の候補経路を構築するかに対応する。

    PHERO_Q:
        1回の巡回で分泌するフェロモン量の基準値。
        一般には総距離が短い経路ほど、結果的に強いフェロモン更新になるよう使う。

    EVA_R:
        フェロモン蒸発率。
        古い情報を徐々に消すための係数である。

    PHERO_R:
        フェロモンに従って経路選択する確率。
        これが高いほど、既に学習済みの経路をたどりやすくなる。

    PHERO_L:
        フェロモンをどの程度強く考慮するかを決める指数。
        一般的な ACO の alpha に相当する。

    HEU_L:
        ヒューリスティック情報をどの程度強く考慮するかを決める指数。
        一般的な ACO の beta に相当する。

    RAND_01:
        0以上1以下の一様乱数。
        確率的遷移選択に使う。
*/
#define REPEAT_NUM 1000
#define ANT_NUM 100
#define PHERO_Q 10
#define EVA_R 0.05
#define PHERO_R 0.95
#define PHERO_L 1
#define HEU_L 1
#define RAND_01 ((double) rand() / RAND_MAX)

class Ant {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        自分が属する Colony を受け取り、
        そのコロニー上で行動するアリとして初期化する。

        Ant 単体では問題全体を知らず、
        所属 Colony を介して Field やフェロモン情報へアクセスする設計である。

        これは、Ant が「場に依存して行動する主体」であることを表している。
    */
    Ant(Colony* argColony);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        route や candidate など、
        アリが内部で保持する動的配列があればその解放責任を持つ。
    */
    ~Ant();

    /*
        --------------------------------------------------------
        selectRoute()
        --------------------------------------------------------

        1匹のアリが、スタートから順に訪問ノードを選び、
        完全な経路を構築する。

        これは ACO における「1個体の解構築」に相当する。

        一般には各ステップで、

            確率 ∝ (フェロモン)^PHERO_L × (ヒューリスティック)^HEU_L

        に従って次ノードを決める。

        よってこの関数は、
        フェロモンによる集団学習結果と、
        距離ベースの局所的な賢さを合わせて、
        1本の経路を作る処理である。
    */
    void selectRoute();

    /*
        --------------------------------------------------------
        putPheromone()
        --------------------------------------------------------

        自分が通った経路にフェロモンを分泌する。

        ただし実際には、
        フェロモン更新は Colony 側が一括で管理することも多い。
        その場合でも、各アリが
        「自分の経路に対してどれだけ寄与するか」
        を表現するメソッドとして意味がある。

        一般には、短い経路を通ったアリほど
        強いフェロモンを残す設計にすることで、
        良い経路が強化されやすくなる。
    */
    void putPheromone();

    /*
        --------------------------------------------------------
        colony
        --------------------------------------------------------

        自分が所属するコロニーへのポインタである。

        これにより、
        - フィールド情報
        - フェロモン情報
        - 補助配列
        などへアクセスできる。

        Ant は単独では閉じた存在ではなく、
        Colony に埋め込まれた探索エージェントである。
    */
    Colony* colony;

    /*
        --------------------------------------------------------
        route
        --------------------------------------------------------

        このアリが構築した経路である。

        たとえば巡回セールスマン問題 (TSP) を想定すると、
        route[k] は「k 番目に訪問したノード番号」を表す。

        route はアリが出力した候補解そのものであり、
        ACO において最も重要な成果物の1つである。
    */
    int* route;

    /*
        --------------------------------------------------------
        totalDis
        --------------------------------------------------------

        route に沿って移動した総距離である。

        通常、TSP のような問題では
        これが小さいほど良い解となる。

        またフェロモン更新では、
        totalDis が短い経路ほど強いフェロモンを残す設計が一般的である。
        したがって totalDis は、
        解の評価値としても、
        フェロモン更新量を決める基準としても重要である。
    */
    double totalDis;

private:
    /*
        --------------------------------------------------------
        candidate
        --------------------------------------------------------

        まだ訪問していないノードの集合である。

        ACO では通常、
        既に訪問したノードへは再訪しないようにしながら、
        未訪問ノードの中から次ノードを選ぶ。

        そのため candidate は、
        「今後選択可能な行動集合」
        を表す重要な補助データである。

        強化学習風に言えば、
        状態ごとに許される行動集合を表していると見なせる。
    */
    int* candidate;
};

#endif
