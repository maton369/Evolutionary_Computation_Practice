#include "Colony.h"

/*
    ============================================================
    main.cpp
    ============================================================

    このファイルは、ACO 全体の実行の流れを表す。

    処理の大筋は非常に単純である。

    1. 乱数 seed を初期化する
    2. Colony を生成する
    3. 繰り返し:
         - 全アリに経路を選ばせる
         - フェロモンを更新する
    4. 最終的なフェロモン状態を表示する
    5. 後始末をする

    main が短いのは、
    アルゴリズムの詳細が Colony, Ant, Field に分離されているからである。
    これは設計上望ましい。

    main は「何を繰り返すか」を書き、
    各クラスは「その中身をどう実現するか」を担当する。
*/

int main()
{
    int i;
    Colony* colony;

    /*
        疑似乱数生成器の seed を現在時刻で初期化する。

        ACO は確率的アルゴリズムであり、
        経路選択に乱数を使う。

        そのため seed によって探索経路が変わる。
        実験では、
        - 毎回違う結果を見たいなら時刻 seed
        - 再現性を確保したいなら固定 seed
        を使い分ける。
    */
    srand((unsigned int) time(NULL));

    /*
        Colony を生成する。

        "sampledata.csv" は問題インスタンスを表すデータファイルであり、
        ここから Field が構築される想定である。

        Colony 生成時には通常、
        - 問題データ読み込み
        - フェロモン初期化
        - アリ集団の生成
        が行われる。
    */
    colony = new Colony((char*)"sampledata.csv");

    /*
        ACO の反復ループである。

        各反復で

            selectRoute()
            renewPheromone()

        を呼ぶ。

        これは ACO の標準的な

            解構築 → フェロモン更新

        のサイクルに対応している。

        反復を重ねることで、
        良い経路の情報がフェロモンとして蓄積され、
        徐々に有望な経路へ探索が集中していくことが期待される。
    */
    for (i = 1; i <= REPEAT_NUM; i++) {
        colony->selectRoute();
        colony->renewPheromone();
    }

    /*
        最終的なフェロモン状態を表示する。

        これにより、どの経路が強く支持されたかを確認できる。
        実際の用途によっては、
        最良経路そのものを表示する関数を別途持たせることも多い。
    */
    colony->printPheromone();

    /*
        後始末。

        Colony が所有している
        - Field
        - Ant 配列
        - 補助配列
        などもここで連鎖的に解放される想定である。
    */
    delete colony;

    return 0;
}
