#ifndef COLONY_H
#define COLONY_H

/*
    ============================================================
    Colony.h
    ============================================================

    このファイルでは、アリコロニー最適化（ACO）における
    「コロニー（Colony）」全体を表すクラスを定義する。

    ------------------------------------------------------------
    ACO における Colony の役割
    ------------------------------------------------------------

    ACO では、1匹のアリだけで最適化を行うのではなく、
    複数のアリが同じ問題空間の上で繰り返し経路探索を行う。

    そのため Colony は単なる「アリの配列」ではなく、

    - 問題空間（Field）を保持する
    - 複数のアリ（Ant）をまとめて管理する
    - 各反復で全アリに経路を選ばせる
    - 全アリの行動結果をもとにフェロモンを更新する

    という、ACO 全体の進行役を担う。

    進化計算の文脈で言えば、Colony は
    「探索集団全体の状態遷移を管理する器」
    に相当する。

    ------------------------------------------------------------
    ACO の基本的な流れ
    ------------------------------------------------------------

    1. 各アリが経路を構築する
    2. 良い経路を通ったアリほど、より有利なフェロモンを残す
    3. フェロモンが濃い経路は次回以降選ばれやすくなる
    4. ただしフェロモンは蒸発するので、古い情報は弱まる
    5. これを繰り返すことで、短い経路にアリが集まりやすくなる

    したがって Colony は、
    「個々のアリの局所的な行動」を
    「集団全体の学習的な振る舞い」に変換するクラスである。
*/

#include "Ant.h"

class Ant;
class Field;

class Colony {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        fileName で指定されたデータファイルを読み込み、
        問題空間 Field を生成し、
        その上で行動するアリ集団を初期化する想定である。

        ACO では問題データ（ノード間距離など）が固定され、
        その上で反復的に探索を行うため、
        Colony 生成時に Field を準備する設計は自然である。
    */
    Colony(char* fileName);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        Colony は通常、
        - Field
        - Ant 配列
        - 補助配列 nume
        を所有するため、
        それらの解放責任を持つ。
    */
    ~Colony();

    /*
        --------------------------------------------------------
        selectRoute()
        --------------------------------------------------------

        全アリに対して経路選択を行わせる。

        理論的には、ACO の「解構築フェーズ」に対応する。
        各アリは

            フェロモン情報
            +
            ヒューリスティック情報（通常は距離の逆数）

        をもとに、次に進むノードを確率的に選ぶ。

        したがってこの関数は、
        Colony 全体として「今反復での候補解群」を作る処理である。
    */
    void selectRoute();

    /*
        --------------------------------------------------------
        renewPheromone()
        --------------------------------------------------------

        各アリが構築した経路結果をもとに、
        フィールド上のフェロモン量を更新する。

        ACO の学習の核心はここにある。

        - 良い経路はフェロモンが増える
        - 時間とともにフェロモンは蒸発する

        この2つが組み合わさることで、
        「有望な経路の強化」と
        「古い情報の忘却」が両立する。

        これは強化学習でいう報酬反映や価値更新に似た側面を持つ。
    */
    void renewPheromone();

    /*
        --------------------------------------------------------
        printPheromone()
        --------------------------------------------------------

        フェロモン行列を表示する。

        これにより、
        どの経路がどれくらい強く学習されたかを観測できる。

        ACO では最終解だけでなく、
        フェロモン分布そのものが
        「集団がどの経路を有望だと判断したか」
        を表す重要な中間情報である。
    */
    void printPheromone();

    /*
        --------------------------------------------------------
        field
        --------------------------------------------------------

        探索の舞台となる Field である。

        ここには例えば
        - ノード数
        - ノード間距離
        - エッジ上のフェロモン量
        などが保持される。

        Colony はこの Field の上でアリたちを動かす。
        つまり Field は「問題そのもの」であり、
        Colony は「その問題上で探索する主体集団」である。
    */
    Field* field;

    /*
        --------------------------------------------------------
        ant
        --------------------------------------------------------

        コロニーに属するアリたちの配列である。

        各 ant[i] は、
        1回の反復で1本の経路（巡回路など）を構築する。

        集団ベース探索という観点では、
        これら複数アリが同時に解候補を生成することで、
        単一探索より広い領域を観測できる。
    */
    Ant** ant;

    /*
        --------------------------------------------------------
        nume
        --------------------------------------------------------

        経路選択確率の「分子」を保持するための補助配列である。

        ACO では、ノード i から j へ進む確率は一般に

            (フェロモン量)^alpha × (ヒューリスティック値)^beta

        のような形で定義される。

        このとき nume は、その「正規化前の値」を
        一時的に保存するための配列だと考えられる。

        つまり nume は、
        各候補遷移の魅力度を数値化したものを保持する領域である。
    */
    double** nume;
};

#endif
