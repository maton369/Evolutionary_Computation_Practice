//============================================================
// File: main.cpp
//============================================================

#include "NestSet.h"

/*
    ============================================================
    main.cpp
    ============================================================

    このファイルは、カッコウ探索
    (Cuckoo Search, CS)
    全体の外側の実行フローを記述している。

    main 自体は短いが、これは設計として望ましい。
    なぜなら、

        - Dataset は問題定義を担当
        - Nest は 1 個の候補解を担当
        - NestSet は巣集合全体の管理を担当
        - main は外側の反復制御だけを担当

    という責務分離ができているからである。

    ------------------------------------------------------------
    このプログラム全体の意味
    ------------------------------------------------------------

    このコード全体では、
    各 Nest が 1 個の候補解、すなわち

        標準偏回帰係数ベクトル

    を表していると考えられる。

    そして Nest が持つ value は、
    その係数ベクトルに対する

        標準化済みデータ上の二乗誤差和

    のような「小さいほど良い」評価値であると読むのが自然である。

    そのため Cuckoo Search 全体としては、

        回帰係数ベクトル空間において
        value を最小化する解を探す

    ことが目的になる。

    ------------------------------------------------------------
    カッコウ探索の大まかな流れ
    ------------------------------------------------------------

    main が表している探索全体の流れは、概念的には次の通りである。

        1. 巣集合 NestSet を初期化する
        2. 各世代で alternate() を実行する
        3. 各世代の最良評価値を表示する
        4. 最終的な最良解を表示する

    これを図にすると、次のようになる。

        ┌────────────────────────────────────┐
        │              初期化                 │
        │  ・乱数 seed 設定                  │
        │  ・NestSet を生成                  │
        │  ・初期巣集合を準備                │
        └────────────────┬───────────────────┘
                         │
                         ▼
        ┌────────────────────────────────────┐
        │        世代 i = 1 ... GEN_MAX      │
        │        ns->alternate() を実行      │
        └────────────────┬───────────────────┘
                         │
                         ▼
        ┌────────────────────────────────────┐
        │   巣集合の更新                      │
        │  ・新しい卵候補の生成              │
        │  ・既存の巣との比較                │
        │  ・一部の悪い巣の放棄              │
        │  ・巣集合を良い順に並べ替え        │
        └────────────────┬───────────────────┘
                         │
                         ▼
        ┌────────────────────────────────────┐
        │   その世代の最良評価値を表示        │
        │   （nest[0]->value）               │
        └────────────────┬───────────────────┘
                         │
                         ▼
        ┌────────────────────────────────────┐
        │             最終結果表示            │
        │  ・最良係数ベクトル                │
        │  ・重回帰式                        │
        └────────────────────────────────────┘

    この構造から分かるように、
    main はカッコウ探索のアルゴリズム詳細そのものを書く場所ではなく、

        「何回繰り返すか」
        「どのタイミングで結果を表示するか」

    を制御する最上位の枠組みを担当している。
*/


int main()
{
    /*
        i は世代番号である。

        Cuckoo Search は PSO のような「時刻発展」よりも、
        むしろ GA に近く
            「1 回の集団更新を 1 世代」
        と見るほうが自然である。

        そのためここではループ変数に i を使い、
        「第 i 世代」という意味で回している。
    */
    int i;

    /*
        NestSet へのポインタ。

        NestSet は
            - Dataset
            - 巣の集合
            - 巣集合全体の更新処理
            - 最終結果表示

        をまとめて管理する、Cuckoo Search 全体の中心クラスである。

        main から見ると、
        NestSet を 1 つ作れば
        アルゴリズム全体を世代ごとに進められる設計になっている。
    */
    NestSet* ns;

    /*
        疑似乱数の seed を現在時刻で初期化する。

        Cuckoo Search は確率的最適化法であり、
        乱数はたとえば次のような場面で使われる。

            - 初期巣の生成
            - Levy flight による候補生成
            - 比較対象巣の選択
            - 放棄される巣の再初期化

        そのため seed が変わると探索軌跡も変わる。

        実験再現性を厳密に確保したいなら固定 seed を使うこともあるが、
        通常の実行では time(NULL) による時刻 seed で十分である。
    */
    srand((unsigned int)time(NULL));

    /*
        NestSet を生成する。

        ここで通常は NestSet のコンストラクタ内部で、

            - Dataset の読み込み
            - 巣集合 nest の生成
            - 各 Nest の初期評価
            - 良い順への並び替え

        が行われる。

        つまりこの 1 行によって、
        カッコウ探索を始めるための準備がすべて整う。

        "sampledata.csv" は問題データであり、
        今回の文脈では
            説明変数と目的変数を持つ回帰データ
        を表していると考えられる。
    */
    ns = new NestSet((char*)"sampledata.csv");

    /*
        Cuckoo Search のメインループ。

        i = 1 から GEN_MAX まで、
        巣集合全体に対して 1 世代ぶんの更新を行う。

        ここで呼ばれる

            ns->alternate();

        が、Cuckoo Search における 1 世代更新に対応する。

        典型的にはその中で

            1. 新しい卵候補を生成する
            2. 既存の巣と比較して良ければ置換する
            3. 一部の悪い巣を放棄してランダム再生成する
            4. 巣集合を評価値順に並べ替える

        といった処理が行われる。

        つまりこの for ループは、
        カッコウ探索の世代進行そのものを表している。
    */
    for (i = 1; i <= GEN_MAX; i++) {

        /*
            巣集合を 1 世代進める。

            alternate() という名前は、
            「世代交代」あるいは「集合更新」
            に近い意味を持っている。

            GA のような交叉中心の世代交代ではないが、
            集合全体の候補解構成が 1 回変わるため、
            世代更新として理解できる。
        */
        ns->alternate();

        /*
            現在世代の最良評価値を表示する。

            ここで

                ns->nest[0]->value

            を表示していることから、
            NestSet 内では

                sort() 後に nest[0] が現時点の最良巣

            という規約になっていると分かる。

            これは集団メタヒューリスティクス実装として非常に自然であり、
            最良値参照が簡潔になる。

            この出力によって、

                - 探索が進んでいるか
                - 最良値が改善しているか
                - 途中で停滞していないか

            を観察できる。

            今回の問題設定では value は誤差のような量なので、
            小さくなっていくほど探索が成功していると解釈できる。
        */
        printf("%d回目：最良評価値%f\n", i, ns->nest[0]->value);
    }

    /*
        最終結果を表示する。

        NestSet::printResult() の中では通常、

            1. 最良巣の egg を
               元スケールの偏回帰係数へ変換する
            2. 重回帰式を表示する

        といった処理が行われる。

        つまり探索中は
            標準化空間の係数ベクトル
        を扱っていたが、
        最後に人間が解釈できる形へ戻している。

        これは

            探索は数値的に扱いやすい空間で行い、
            結果は元の問題の言葉で表示する

        という設計に対応しており、非常に自然である。
    */
    ns->printResult();

    /*
        後始末。

        NestSet は内部に

            - Nest 群
            - Dataset

        を所有しているため、
        delete ns によってデストラクタが呼ばれ、
        それらも連鎖的に解放される。

        集団サイズが大きい探索手法では、
        このようなメモリ管理は重要である。
    */
    delete ns;

    /*
        正常終了を表す 0 を返す。
    */
    return 0;
}


/*
    ============================================================
    まとめ
    ============================================================

    この main.cpp の本質は、
    カッコウ探索全体の外側の流れを最小限のコードで表現している点にある。

        1. 乱数初期化
        2. NestSet の生成
        3. 世代ループで alternate() を繰り返す
        4. 各世代の最良値を観測する
        5. 最終結果を表示する
        6. 後始末をする

    つまり main は、
    Cuckoo Search の理論そのものを細かく記述する場所ではなく、

        「アルゴリズムを何世代進めるか」
        「結果をどのタイミングで表示するか」

    を決める最上位制御として機能している。

    実際の探索ロジックは NestSet / Nest に委譲されているため、
    main は短いが意味的には十分である。

    このような構造にしておくことで、

        - アルゴリズム本体の修正
        - パラメータ実験
        - 出力方法の変更

    がしやすくなり、
    教材としても実験コードとしても見通しが良い。
*/
