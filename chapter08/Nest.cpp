#include "Nest.h"

/*
    ============================================================
    Nest.cpp
    ============================================================

    このファイルでは、カッコウ探索
    (Cuckoo Search, CS)
    における 1 個の巣 Nest の具体的な振る舞いを実装している。

    ------------------------------------------------------------
    Nest の理論的な位置づけ
    ------------------------------------------------------------

    Cuckoo Search では、1 個の巣が 1 個の候補解を表す。
    このコードでは、その候補解は

        egg

    という配列で表現されている。

    自然界の比喩では、

        巣
            = 候補解を保持する場所

        卵
            = 現在その巣にある解候補

        新しい卵
            = 現在より良いかもしれない新候補

    である。

    したがって Nest クラスは、

        - 現在の候補解 egg
        - 新しく試す候補解 newEgg
        - その評価値 value

    を保持する「1 個体」に相当する。

    ------------------------------------------------------------
    このコードでの egg の意味
    ------------------------------------------------------------

    これまでの Dataset クラスとの整合性から考えると、
    egg は

        標準偏回帰係数ベクトル

    を表している。

    つまり egg の各成分は、
    各説明変数に対応する係数候補である。

    数式的には、
    egg を

    $$
    \boldsymbol{\beta} = (\beta_1, \beta_2, \dots, \beta_p)
    $$

    とみなすと、
    この Nest は 1 本の回帰モデル候補を表していることになる。

    ------------------------------------------------------------
    value の意味
    ------------------------------------------------------------

    evaluate() を見ると、このコードの value は

    $$
    \sum_{i=1}^{n}
    \left(
    y_i - \sum_{j=1}^{p}\beta_j x_{ij}
    \right)^2
    $$

    に対応する形で計算されている。

    つまり value は、

        標準化済みデータに対する残差平方和

    であり、

        小さいほど良い

    最小化問題として解釈するのが自然である。

    したがってこの Nest は、

        「誤差の小さい回帰係数ベクトルを持つ巣ほど良い」

    という意味を持つ。

    ------------------------------------------------------------
    replace(base) の理論的意味
    ------------------------------------------------------------

    replace(base) は、Cuckoo Search の exploit 側の中心処理である。

    ここでは base の卵をもとに新しい卵 newEgg を作る。
    そのとき使われているのが Levy flight である。

    Levy flight は、

        普段は比較的小さな移動をしつつ、
        ときどき非常に大きなジャンプもする

    という重い裾を持つ確率過程である。

    これにより、

        - 良い解の近くを細かく探る
        - しかし局所最適に閉じ込められたときには
          大きく飛んで脱出する

    という性質を自然に実現できる。

    これは Cuckoo Search の最大の特徴の 1 つである。

    ------------------------------------------------------------
    abandon() の理論的意味
    ------------------------------------------------------------

    abandon() は explore 側の中心処理である。

    悪い巣をそのまま残しておくと、
    集団全体が停滞しやすくなる。
    そこで一部の巣を捨てて、
    ランダムな新しい巣へ作り直す。

    これは、

        リスタート
        再初期化
        多様性回復

    の役割を持つ。

    つまり Cuckoo Search は、

        replace()
            で改善候補を作り

        abandon()
            で停滞した候補群を壊して広く探索する

    という 2 本柱で成り立っている。
*/


//============================================================
// コンストラクタ
// argNS: 属している巣の集合
//============================================================
Nest::Nest(NestSet* argNS)
{
    int i;

    /*
        この Nest がどの NestSet に属しているかを保存する。

        Nest 単独では
            - 問題の次元数
            - データセット
            - 集合全体の情報
        を知らない。

        そのため ns を通して

            ns->dataset->exVarNum
            ns->dataset->exSData
            ns->dataset->resSData

        などへアクセスする設計になっている。

        つまり Nest は、
        NestSet に埋め込まれた候補解である。
    */
    ns = argNS;

    /*
        現在の卵 egg を確保する。

        egg は現在この巣が保持している候補解であり、
        この問題では標準偏回帰係数ベクトルを表す。

        次元数は説明変数数 exVarNum に一致する。
    */
    egg = new double[ns->dataset->exVarNum];

    /*
        新しい卵 newEgg を確保する。

        replace(base) の中で、
        現在の egg をいきなり書き換えるのではなく、
        まず newEgg に新候補を作ってから比較する。

        これにより

            - 現在解を安全に保持できる
            - 現在解と新候補を同じ評価関数で比較できる
            - よかったときだけ置換できる

        という利点がある。
    */
    newEgg = new double[ns->dataset->exVarNum];

    /*
        現在の卵 egg をランダム初期化する。

        各成分は探索範囲 [COEF_MIN, COEF_MAX] の一様乱数で与える。

        これにより初期巣集合は探索空間の複数領域へばらける。
        集団最適化では、この初期多様性が非常に重要である。

        もし全個体が似た場所から始まると、
        早い段階で狭い領域に閉じ込められやすい。
    */
    for (i = 0; i < ns->dataset->exVarNum; i++) {
        egg[i] = COEF_MIN + (COEF_MAX - COEF_MIN) * RAND_01;
    }

    /*
        初期卵の評価値を計算する。

        ここでの value は
            現在の egg に対応する損失
        であり、小さいほど良い。

        したがってコンストラクタ終了時点で、
        この Nest は

            - 候補解 egg
            - その評価値 value

        を持った完全な探索個体になっている。
    */
    value = evaluate(egg);
}


//============================================================
// デストラクタ
//============================================================
Nest::~Nest()
{
    /*
        動的確保した egg と newEgg を解放する。

        Nest は
            - 現在の卵
            - 新しい卵候補

        を自分で所有しているため、
        その後始末は Nest 自身が行う。
    */
    delete[] egg;
    delete[] newEgg;
}


//============================================================
// 卵を置換する
// base: もとにする卵の巣
//============================================================
void Nest::replace(Nest* base)
{
    int i;
    double u, v, s, newValue;
    double* tmp;

    /*
        この関数は Cuckoo Search の exploit 側の中心操作である。

        base の egg を基準にして、新しい卵 newEgg を生成する。
        そのとき用いるのが Levy flight である。

        Levy flight の典型式は概念的には

        $$
        x_{\text{new}} = x_{\text{base}} + \alpha \cdot \text{Levy}
        $$

        であり、

            - x_base は基準解
            - alpha はステップ長スケール
            - Levy は重い裾を持つ確率的な飛び方

        を表す。

        このコードはその 1 次元ごとの実装になっている。
    */
    for (i = 0; i < ns->dataset->exVarNum; i++) {

        /*
            Mantegna の方法で Levy flight のステップを作るための
            乱数 u, v を生成する。

            u:
                分散 sigma^2 を持つ正規乱数

            v:
                標準正規乱数

            これを用いて

            $$
            s = \frac{u}{|v|^{1/\beta}}
            $$

            を作る。

            この s が Levy 分布に従うステップ長に相当する。

            重要なのは、
            この分布が通常のガウス分布よりも
            はるかに大きなジャンプを起こしやすい点である。

            これによって

                普段は細かく探索しつつ、
                ときどき大きく飛ぶ

            という Cuckoo Search 特有の探索性が得られる。
        */
        u = RAND_N * SIGMA;
        v = RAND_N;
        s = u / pow(fabs(v), 1 / BETA);

        /*
            基準巣 base の egg をもとに、
            新しい卵 newEgg を生成する。

            式:

            $$
            \text{newEgg}[i] = \text{base->egg}[i] + \alpha s
            $$

            であり、
            alpha は ALPHA に対応する。

            ALPHA が大きいほど一度の移動が大きくなり、
            小さいほど微調整的な探索になる。
        */
        newEgg[i] = base->egg[i] + ALPHA * s;
    }

    /*
        新しい卵 newEgg の評価値を計算する。

        ここでは evaluate(newEgg) によって、
        現在の egg とまったく同じ目的関数で比較できる。
    */
    newValue = evaluate(newEgg);

    /*
        新しい卵のほうが現在の卵より良ければ置換する。

        このコード全体では value が小さいほど良いので、

            newValue < value

        なら改善している。

        実装では egg と newEgg のポインタを入れ替えており、
        大きなベクトルコピーを避けている。
        これは効率的な実装である。

        つまり

            - newEgg が良ければ正式採用
            - そうでなければ現在の egg を維持

        という greedy 更新になっている。
    */
    if (newValue < value) {
        tmp = egg;
        egg = newEgg;
        newEgg = tmp;
        value = newValue;
    }
}


//============================================================
// 巣を放棄する
//============================================================
void Nest::abandon()
{
    int i;

    /*
        この関数は explore 側の中心操作である。

        現在の巣を放棄し、
        egg をランダムに再初期化する。

        これは

            - 悪い解を捨てる
            - 新しい探索領域を導入する
            - 局所最適から脱出する

        ためのリスタート機構である。

        Cuckoo Search では、下位一定割合の巣を abandon() することで
        集団の多様性を保つ。
    */
    for (i = 0; i < ns->dataset->exVarNum; i++) {

        /*
            各次元を探索範囲 [COEF_MIN, COEF_MAX] の一様乱数で
            再初期化する。

            これはコンストラクタ時の初期化と同様であり、
            つまり abandon() は

                「この巣を新しいランダム候補に作り直す」

            操作だといえる。
        */
        egg[i] = COEF_MIN + (COEF_MAX - COEF_MIN) * RAND_01;
    }

    /*
        再初期化後の卵の評価値を計算する。

        こうして abandon() 後も、
        この Nest は

            - 候補解 egg
            - その評価値 value

        を整合的に持つ状態になる。
    */
    value = evaluate(egg);
}


//============================================================
// 評価値を算出する
// argEgg: 評価対象の卵
// 戻り値: 評価値
//============================================================
double Nest::evaluate(double* argEgg)
{
    int i, j;
    double diff;
    double retValue;

    /*
        この関数は、与えられた卵 argEgg の評価値を返す。

        現在の egg そのものではなく引数 argEgg を受け取るのが重要である。
        これにより

            - 現在の egg
            - 新しく作った newEgg

        を同じ評価関数で公平に比較できる。

        つまり evaluate(argEgg) は、
        候補採用前の比較用評価関数として設計されている。
    */

    /*
        二乗誤差和を作るので、最初は 0.0 に初期化する。
    */
    retValue = 0.0;

    /*
        各データ点 i について残差 diff を計算する。

        resSData[i] は標準化済み目的変数、
        exSData[i][j] は標準化済み説明変数である。

        argEgg[j] を回帰係数とみなすと、
        予測値は

        $$
        \sum_j \text{argEgg}[j] \cdot x_{ij}
        $$

        になる。

        したがって diff は

        $$
        y_i - \hat{y}_i
        $$

        に対応する。
    */
    for (i = 0; i < ns->dataset->dataNum; i++) {
        diff = ns->dataset->resSData[i];

        for (j = 0; j < ns->dataset->exVarNum; j++) {
            diff -= argEgg[j] * ns->dataset->exSData[i][j];
        }

        /*
            残差の二乗を足し込む。

            最終的な retValue は

            $$
            \sum_i (y_i - \hat{y}_i)^2
            $$

            となる。

            これは回帰問題における残差平方和であり、
            小さいほど良いモデルである。
        */
        retValue += pow(diff, 2.0);
    }

    /*
        評価値を返す。

        このコード全体では最小化問題なので、
        retValue が小さいほど良い候補解である。
    */
    return retValue;
}


/*
    ============================================================
    まとめ
    ============================================================

    この Nest.cpp の本質は、
    カッコウ探索における 1 個の巣を

        候補解
        + 候補解生成
        + 候補解再初期化
        + 候補解評価

    を持つ探索主体として実装している点にある。

    - コンストラクタ
      初期卵をランダム生成し、その評価を行う

    - replace(base)
      base をもとに Levy flight で新しい卵を作り、
      良ければ置換する

    - abandon()
      巣を放棄してランダム再初期化する

    - evaluate(argEgg)
      与えられた卵の回帰誤差を評価する

    つまり Nest は、

        exploit を担う replace()
        と
        explore を担う abandon()

    の両方を持つ、Cuckoo Search の中心的な個体クラスである。

    NestSet が集団全体の順位と淘汰を管理し、
    Nest が個体レベルの更新を担うことで、
    Cuckoo Search 全体が成立している。
*/
