#ifndef NESTSET_H
#define NESTSET_H

/*
    ============================================================
    NestSet.h
    ============================================================

    このファイルでは、カッコウ探索
    (Cuckoo Search, CS)
    における「巣の集合」全体を表す NestSet クラスを定義する。

    ------------------------------------------------------------
    カッコウ探索の基本的な考え方
    ------------------------------------------------------------

    カッコウ探索は、托卵（たくらん）するカッコウの行動を
    最適化アルゴリズムとして抽象化したものである。

    自然界では、カッコウは自分で巣を作るのではなく、
    他の鳥の巣に卵を産みつける。
    その卵が見つからずに生き残れば、次世代へつながる。

    これを最適化へ写像すると、

        - 巣 (nest) = 候補解
        - 卵 = 新しく生成された候補解
        - 良い卵 = 良い解
        - 発見される／捨てられる巣 = 悪い解の淘汰

    という対応になる。

    したがってカッコウ探索の本質は、

        良い候補解を残し、
        悪い候補解を破棄し、
        新しい候補解を再投入する

    という集団ベース探索である。

    ------------------------------------------------------------
    カッコウ探索の典型的な流れ
    ------------------------------------------------------------

    Cuckoo Search の代表的な 1 世代の流れは、
    概念的には次のようになる。

        1. ある巣から新しい候補解を生成する
           （Levy flight を用いることが多い）

        2. ランダムに選んだ既存の巣と比較する

        3. 新しい候補解のほうが良ければ置き換える

        4. 一部の悪い巣を捨て、新しいランダムな巣に置き換える

        5. 集合全体を良い順に並べ替える

    つまりこのアルゴリズムは、

        exploit:
            良い解の近くを改善する
        explore:
            一部の悪い解を捨てて新しい領域を試す

    の両方をバランスさせる探索法である。

    ------------------------------------------------------------
    他の進化計算・群知能手法との違い
    ------------------------------------------------------------

    GA との違い:
        GA は交叉と突然変異で集団全体を世代交代させる。
        Cuckoo Search は、交叉を使わず、
        個々の候補解の置換と淘汰を中心に動く。

    PSO との違い:
        PSO は速度と位置を持つ粒子が群れ全体最良へ引かれる。
        Cuckoo Search は速度を持たず、
        巣ごとの候補解更新と淘汰で探索する。

    ABC との違い:
        ABC は食糧源に対して収穫バチ・追従バチ・偵察バチが役割分担する。
        Cuckoo Search はより簡潔で、
        「新候補生成」と「悪い解の破棄」に焦点がある。

    FA との違い:
        FA は個体間の相対的な誘引を使う。
        Cuckoo Search は個体間の直接誘引ではなく、
        ランダム比較と淘汰・再生成が中心である。

    ------------------------------------------------------------
    このコードで想定される問題設定
    ------------------------------------------------------------

    これまでの Dataset クラスの流れと整合的に読むと、
    この Cuckoo Search でも各 Nest は

        標準偏回帰係数ベクトル

    を候補解として持ち、
    その評価値は

        標準化済みデータに対する二乗誤差和

    のような量であると考えるのが自然である。

    つまり各 Nest は、

        「ある回帰係数ベクトルを仮定したときの回帰モデル候補」

    を表している。

    このとき NestSet 全体は、
    それらの候補群を管理しながら
    損失最小化を目指すことになる。

    ------------------------------------------------------------
    NestSet クラスの役割
    ------------------------------------------------------------

    NestSet は単なる Nest の配列ではない。

    NestSet は、

        - 問題データ Dataset を保持する
        - 巣の集合 Nest を保持する
        - 世代交代 alternate() を行う
        - 巣を評価値順に並び替える
        - 最終結果を表示する

    という、カッコウ探索全体の制御装置である。

    これは

        GA の Population
        PSO の Swarm
        ABC の FlowerSet
        BA の Population

    に相当する役割と考えると分かりやすい。

    特に Cuckoo Search では、

        - 今の巣集合
        - 良い順の順位構造
        - 淘汰・再生成の管理

    が重要なので、NestSet はアルゴリズムの中心クラスになる。
*/

#include "Nest.h"

class Nest;
class Dataset;

class NestSet {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        fileName で指定されたデータセットを読み込み、
        Dataset を生成し、
        その上で探索を行う巣の集合を初期化する。

        典型的にはここで

            - dataset の生成
            - nest 配列の確保
            - 各 Nest の初期化
            - 初期評価
            - sort() による整列

        を行うことが期待される。

        つまり NestSet 生成直後に、
        すでに探索を開始できる状態になっているのが自然である。
    */
    NestSet(char* fileName);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        NestSet は通常、

            - Dataset
            - Nest 配列

        などを所有するため、
        それらの解放責任を持つ。

        集団メタヒューリスティクスでは個体数が多くなりやすいため、
        メモリ管理は重要である。
    */
    ~NestSet();

    /*
        --------------------------------------------------------
        alternate()
        --------------------------------------------------------

        世代交代を行う。

        ここでの「世代交代」は GA のように
        集団全体を交叉で一括更新するものではなく、
        Cuckoo Search に特有の

            - 新しい候補解の生成
            - 既存の巣との比較
            - 良ければ置換
            - 一部の悪い巣の破棄と再生成
            - 集合全体の再整列

        といった流れをまとめて行う処理だと考えられる。

        したがって alternate() は、
        Cuckoo Search の 1 世代ぶんの更新そのものに対応する。

        名前としては GA 風に見えるが、
        中身としては

            「巣集合を 1 回更新する」

        という意味を持つ。
    */
    void alternate();

    /*
        --------------------------------------------------------
        printResult()
        --------------------------------------------------------

        最終的な良い巣、すなわち最良解を表示する。

        今回の問題設定では、

            - 最良の標準偏回帰係数ベクトル
            - それを元スケールに戻した偏回帰係数
            - 重回帰式

        などを表示することが期待される。

        つまり printResult() は、
        最適化の数値結果を人間が解釈可能な形へ戻す出口である。
    */
    void printResult();

    /*
        dataset:
            問題データを保持する。

            各 Nest の候補解がどんな意味を持ち、
            その評価値がどう計算されるかは、
            最終的に dataset を通じて決まる。

            したがって dataset は、
            NestSet が解く最適化問題そのものを表す基盤である。
    */
    Dataset* dataset;

    /*
        nest:
            巣の集合のメンバを表す配列。

            各 nest[i] は 1 個の候補解であり、
            その位置と評価値を持つと考えられる。

            この配列全体が、
            現在時点での探索状態を表している。

            カッコウ探索では、この集合の中から

                - 良い巣を残す
                - 悪い巣を捨てる
                - 新しい巣を作る

            という操作が繰り返されるため、
            nest 配列が探索の主戦場になる。
    */
    Nest** nest;

private:
    /*
        --------------------------------------------------------
        sort(lb, ub)
        --------------------------------------------------------

        巣を良い順に並び替える。

        Cuckoo Search では、集団中の良い解と悪い解を区別することが重要である。
        そのため sort() によって評価値順に整列しておくと、

            - 最良解をすぐ参照できる
            - 悪い解を末尾側として扱いやすい
            - 破棄・再生成の対象を決めやすい

        という利点がある。

        たとえば整列後に

            nest[0]      : 現時点の最良解
            nest[末尾側] : 淘汰対象候補

        と解釈できるようになる。

        引数 lb, ub を持つため、
        実装としてはクイックソートのような再帰的ソートを想定している可能性が高い。

        この sort() は外部公開 API ではなく、
        NestSet 内部の整合性維持処理なので private に置くのが自然である。
    */
    void sort(int lb, int ub);
};

#endif


/*
    ============================================================
    まとめ
    ============================================================

    この NestSet 設計は、カッコウ探索の全体像をかなり素直に表現している。

    - Dataset
      問題定義を担当する

    - Nest
      1 個の候補解を担当する

    - NestSet
      巣の集合全体を管理し、
      世代更新・淘汰・順位構造を管理する

    という分離ができているため、アルゴリズムの見通しが良い。

    特に Cuckoo Search では、

        良い解を残し、
        悪い解を捨て、
        新しい解を入れる

    という集団操作が重要なので、
    NestSet が中心クラスになるのは自然である。

    必要なら次に、

        - Nest.h
        - NestSet.cpp
        - Nest.cpp
        - main.cpp

    についても同じ粒度で、
    理論背景つきの詳細コメント版を書ける。
*/
