#ifndef FLOWERSET_H
#define FLOWERSET_H

/*
    ============================================================
    FlowerSet.h
    ============================================================

    このファイルでは、人工蜂コロニーアルゴリズム
    (ABC: Artificial Bee Colony algorithm)
    における「食糧源の集合」を表す FlowerSet クラスを定義する。

    ------------------------------------------------------------
    ABC における基本的な考え方
    ------------------------------------------------------------

    ABC では、最適化問題の候補解を「食糧源 (food source)」として表現する。
    そして、その食糧源を複数の蜂が探索・改良していく。

    蜂は大きく 3 種類に分かれる。

    1. 収穫バチ (employed bees)
       - 自分に割り当てられた食糧源の近傍を探索する
       - 今持っている解を局所的に改良する役割

    2. 追従バチ (onlooker bees)
       - 収穫バチの情報を見て、有望な食糧源に集まる
       - 良い解に探索資源を集中する役割

    3. 偵察バチ (scout bees)
       - 長く改善しない食糧源を捨てて、新しい食糧源をランダムに探す
       - 探索の停滞を防ぎ、多様性を回復する役割

    この 3 種類の役割が組み合わさることで、

        局所改善（exploit）
        と
        新規探索（explore）

    のバランスを取るのが ABC の本質である。

    ------------------------------------------------------------
    FlowerSet クラスの役割
    ------------------------------------------------------------

    FlowerSet は単なる Flower の配列ではない。

    FlowerSet は、

    - 問題データ Dataset を保持する
    - 食糧源集合 Flower を保持する
    - 3 種類の蜂フェーズを順番に実行する
    - 全体の最良解を管理する

    という、ABC 全体の制御装置である。

    GA でいう Population、
    ACO でいう Colony に近い立場のクラスと考えると分かりやすい。

    ------------------------------------------------------------
    このコードでの問題設定
    ------------------------------------------------------------

    Dataset クラスのメンバを見ると、
    説明変数・目的変数・偏回帰係数・標準化データなどを持っている。

    したがって、この ABC は一般的な連続最適化の中でも、

        「標準偏回帰係数を探索して、
         ある評価値を最小化または最大化する」

    ような回帰モデル最適化を想定していると考えられる。

    つまり Flower の位置 pos は、
    「回帰係数ベクトルの候補」
    を表すと読める。
*/

#include "Flower.h"

class Flower;
class Dataset;

class FlowerSet {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        fileName で指定されたデータセットを読み込み、
        Dataset を生成し、
        その上で探索する食糧源集合 Flower を初期化する想定である。

        FlowerSet は ABC の実行開始時点で
        - 問題データ
        - 初期食糧源群
        - 最良解記録用領域
        を用意する必要がある。
    */
    FlowerSet(char* fileName);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        FlowerSet は通常、
        - Dataset
        - Flower 配列
        - bestPos
        - newFlower
        - trValue
        などを所有するため、
        それらの解放責任を持つ。
    */
    ~FlowerSet();

    /*
        --------------------------------------------------------
        employedBeePhase()
        --------------------------------------------------------

        収穫バチフェーズを実行する。

        ABC において収穫バチは、
        各食糧源の近傍を探索して、その食糧源を改良しようとする。

        典型的には、

            x_ij' = x_ij + phi_ij * (x_ij - x_kj)

        のような式で新しい候補解を生成し、
        改善していれば置き換える。

        つまりこのフェーズは、
        「各解について局所探索を一回ずつ行うフェーズ」
        である。

        ABC の探索能力のうち、まず exploit 側を担う重要な部分である。
    */
    void employedBeePhase();

    /*
        --------------------------------------------------------
        onlookerBeePhase()
        --------------------------------------------------------

        追従バチフェーズを実行する。

        追従バチは、収穫バチたちの持つ食糧源を見て、
        有望なものほど選びやすくなる。

        つまり、
        「良い解により多くの探索資源を割り当てる」
        役割を持つ。

        これにより、単なる均等探索ではなく、
        良い候補解の周辺を重点的に深掘りすることができる。

        GA における選択圧、
        ACO における高フェロモン経路への集中と似た役割を果たす。
    */
    void onlookerBeePhase();

    /*
        --------------------------------------------------------
        scoutBeePhase()
        --------------------------------------------------------

        偵察バチフェーズを実行する。

        ある食糧源が長い間改善しない場合、
        その食糧源は「見込みが薄い」と判断して破棄し、
        新しいランダムな食糧源へ置き換える。

        これにより、
        - 局所最適への停滞を防ぐ
        - 探索空間の新しい領域を試す
        - 個体群の多様性を維持する
        ことができる。

        つまり scout フェーズは、
        ABC における exploration の核である。
    */
    void scoutBeePhase();

    /*
        --------------------------------------------------------
        saveBestPos()
        --------------------------------------------------------

        現在の食糧源集合の中から最良のものを見つけ、
        bestPos と bestValue に記録する。

        これは探索全体の進捗を追跡するために重要である。
        各反復での最良値推移を見れば、
        収束傾向や停滞状況を観察できる。
    */
    void saveBestPos();

    /*
        --------------------------------------------------------
        printResult()
        --------------------------------------------------------

        最終的な最良食糧源や評価値を表示する。

        実験用途では、
        - 最良パラメータ
        - 最良評価値
        - 必要なら回帰式
        などをここで出力することが考えられる。
    */
    void printResult();

    /*
        dataset:
            問題データを保持する。

            この ABC は一般的なベンチマーク関数ではなく、
            Dataset を通じて実データに対する回帰係数最適化を行う構造になっている。

            したがって dataset は、
            「探索対象の評価関数を決める土台」
            である。
    */
    Dataset* dataset;

    /*
        flower:
            食糧源集合を表す配列。

            各 flower[i] は 1 個の候補解であり、
            その位置 pos が回帰係数ベクトル、
            value がその評価値に対応すると考えられる。

            ABC の集団ベース探索は、この配列全体で表現される。
    */
    Flower** flower;

    /*
        bestPos:
            これまでに見つかった最良食糧源の位置。

            位置ベクトルなので、通常は dataset->exVarNum 個程度の次元を持つ。
    */
    double* bestPos;

    /*
        bestValue:
            bestPos に対応する最良評価値。

            これが小さいほど良いのか、大きいほど良いのかは
            Flower::evaluate() の定義次第だが、
            ABC 全体としてはこの値を追跡しながら探索を進める。
    */
    double bestValue;

private:
    /*
        newFlower:
            新しい候補解を一時的に生成するときの作業用食糧源。

            各フェーズで近傍解を試すとき、
            いきなり元の Flower を壊さず、
            まず一時候補を作って比較するための領域として使われることが多い。

            これは「試しに変えてみて、良ければ採用する」という
            貪欲更新に自然に対応する。
    */
    Flower* newFlower;

    /*
        trValue:
            各食糧源の「変換後評価値」を保持する配列。

            ABC では onlooker bee が食糧源を選ぶ際、
            生の評価値をそのまま確率に使うのではなく、
            確率計算しやすい形へ変換することが多い。

            たとえば
                fitness = 1 / (1 + value)
            のような形で、良い解ほど大きな値になるように変換する。

            trValue はそのような「選択確率計算用の値」を保存する領域だと考えられる。
    */
    double* trValue;
};

#endif
