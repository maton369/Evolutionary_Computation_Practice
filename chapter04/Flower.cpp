#include "Flower.h"

/*
    ============================================================
    Flower.cpp
    ============================================================

    このファイルでは、人工蜂コロニーアルゴリズム
    (ABC: Artificial Bee Colony algorithm)
    における 1 個の食糧源 Flower の具体的な振る舞いを実装している。

    ------------------------------------------------------------
    Flower の理論的な意味
    ------------------------------------------------------------

    ABC では、候補解そのものを「食糧源」とみなす。
    したがって Flower は、

        - 1 個の候補解ベクトル pos
        - その候補解の評価値 value
        - 改善が停滞している度合い visitNum

    を持つ。

    つまり Flower は、
    最適化問題における「1 個体」「1 解候補」に相当する。

    ただし GA の個体と少し違う点は、
    ABC ではこの食糧源に対して

        - 収穫バチが近傍探索を行う
        - 追従バチが有望なものを重点的に利用する
        - 偵察バチが停滞したものを捨てて再初期化する

    という複数の役割が割り当てられる点である。

    ------------------------------------------------------------
    このコードでの pos の意味
    ------------------------------------------------------------

    Dataset の構造を見ると、

        exVarNum   : 説明変数の数
        exSData    : 標準化済み説明変数
        resSData   : 標準化済み目的変数

    を持っている。

    さらに evaluate() では、
    pos[j] を説明変数 exSData[i][j] に掛けて残差を作っている。

    したがって pos は

        「標準化空間における回帰係数ベクトル」

    を表していると解釈できる。

    つまり Flower は、

        ある回帰係数ベクトルを仮定したときの
        回帰モデルの候補

    を表す。

    ------------------------------------------------------------
    このコードが解いている最適化問題
    ------------------------------------------------------------

    evaluate() を見ると、
    各データ点 i に対して

        diff = resSData[i] - Σ pos[j] * exSData[i][j]

    を計算し、その二乗和を value にしている。

    したがってこの Flower は、

        標準化された目的変数に対する
        予測誤差の二乗和

    を評価値として持つ。

    つまりこの問題は、

        二乗誤差和を最小化する回帰係数探索

    になっている。

    ここで value が小さいほど良いので、
    ABC 全体としては最小化問題として動いている。

    ------------------------------------------------------------
    Flower クラスの役割を整理すると
    ------------------------------------------------------------

    1. コンストラクタ
       ランダムな初期係数ベクトルを生成する

    2. change(base)
       既存の食糧源 base の近傍に新しい候補解を作る

    3. renew()
       停滞した食糧源をランダムに再初期化する

    4. evaluate()
       現在の係数ベクトル pos に対する誤差を計算する

    つまり Flower は、

        候補解の保持
        +
        近傍生成
        +
        評価

    を担う、ABC の解表現クラスである。
*/


//============================================================
// コンストラクタ
// argFSet: 属している食糧源集合
//============================================================
Flower::Flower(FlowerSet* argFSet)
{
    int i;

    /*
        この Flower がどの FlowerSet に属するかを保存する。

        Flower 単独では
            問題次元数
            データセット
            他の食糧源
        を知らないため、
        fSet を通して共有情報へアクセスする。

        つまり Flower は独立した存在ではなく、
        FlowerSet に埋め込まれた候補解である。
    */
    fSet = argFSet;

    /*
        位置ベクトル pos を確保する。

        pos の長さは説明変数の数 exVarNum と一致する。
        なぜなら、この問題では pos が
        各説明変数に対応する標準偏回帰係数ベクトル
        を表しているからである。

        つまり

            pos[0], pos[1], ..., pos[exVarNum-1]

        は、回帰モデルの各係数候補である。
    */
    pos = new double[fSet->dataset->exVarNum];

    /*
        各係数を探索範囲 [COEF_MIN, COEF_MAX] の一様乱数で初期化する。

        ここでは

            COEF_MIN + (COEF_MAX - COEF_MIN) * RAND_01

        により、
        各次元を独立にランダム初期化している。

        これは ABC の初期食糧源生成に対応する。

        理論的には、
        初期食糧源をランダムにばらけさせることで、
        探索開始時に空間の複数領域を同時に試せるようにしている。

        もし初期化が偏りすぎると、
        ABC の持つ「集団で広く探索する」利点が弱くなる。
    */
    for (i = 0; i < fSet->dataset->exVarNum; i++) {
        pos[i] = COEF_MIN + (COEF_MAX - COEF_MIN) * RAND_01;
    }

    /*
        まだ改善停滞は起きていないので、訪問回数 visitNum を 0 にする。

        visitNum は
            「改善しないまま何回試されたか」
        を数えるための変数であり、
        scout bee フェーズでの再初期化判定に使われる。
    */
    visitNum = 0;

    /*
        初期化した係数ベクトルに対する評価値を計算する。

        これにより、コンストラクタ終了時点で
        この Flower はすでに「位置」と「評価値」を持った
        完全な候補解になっている。
    */
    evaluate();
}


//============================================================
// デストラクタ
//============================================================
Flower::~Flower()
{
    /*
        pos は new[] で確保した動的配列なので、
        delete[] で解放する。

        Flower は自分の位置ベクトルを所有しているため、
        その後始末は自分で行う。
    */
    delete[] pos;
}


//============================================================
// base の近くの食糧源に変更する
// base: もとにする食糧源の添え字
//============================================================
void Flower::change(int base)
{
    int i, j;

    /*
        この関数は ABC の近傍生成を担当する。

        収穫バチフェーズや追従バチフェーズでは、
        既存の食糧源を少し変形した新しい候補解を作り、
        改善しているかどうかを調べる。

        その「少し変えた候補」を作るのがこの change() である。

        ABC の代表的な近傍生成式は

            x'_ij = x_ij + phi_ij * (x_ij - x_kj)

        であり、

            x_ij   : 基準食糧源 base の j 次元目
            x_kj   : 別の食糧源 k の j 次元目
            phi_ij : [-1, 1] の乱数

        を用いる。

        この式の意味は、
        「他の食糧源との差分方向を使って、基準解を少し動かす」
        ことである。

        これにより、単なるランダム探索ではなく、
        集団内の相対的位置関係を利用した近傍探索が行える。
    */

    /*
        まず、基準となる食糧源 flower[base] の位置を丸ごとコピーする。

        つまり最初は

            this->pos = flower[base]->pos

        の状態にする。

        その後、特定の1次元だけを変更することで、
        元の解に近い新候補を作る。

        これは「基準解の近傍を探索する」という ABC の考え方に対応している。
    */
    for (i = 0; i < fSet->dataset->exVarNum; i++) {
        pos[i] = fSet->flower[base]->pos[i];
    }

    /*
        どの次元を変更するかをランダムに選ぶ。

        ABC では全次元を一気に大きく変えるよりも、
        一部の次元だけを変えて近傍探索することが多い。

        このコードでは 1 次元だけを選び、その次元に変化を与えている。
    */
    i = rand() % fSet->dataset->exVarNum;

    /*
        比較対象となる別の食糧源 j を選ぶ。

        ここで

            j != base

        になるようにしている。

        なぜなら、近傍生成式では

            pos[i] - flower[j]->pos[i]

        という差分を使うため、
        同じ食糧源同士を引いてしまうと変化量が 0 になってしまうからである。

        式

            (base + (rand() % (EBEE_NUM - 1) + 1)) % EBEE_NUM

        は、
        base 以外のどれか 1 個を一様に選ぶための書き方である。
    */
    j = (base + (rand() % (EBEE_NUM - 1) + 1)) % EBEE_NUM;

    /*
        選んだ 1 次元だけを変形する。

        ここで

            rand() / (RAND_MAX / 2.0) - 1

        は、おおよそ [-1, 1] の乱数を作っている。
        これは上で述べた phi_ij に対応する。

        したがって、この更新式は

            pos[i] = pos[i] + phi * (pos[i] - other[i])

        という ABC の典型的な近傍生成そのものである。

        この式の直感的な意味は、

            - 他の食糧源との差が大きいなら大きく動く
            - 差が小さいなら小さく動く
            - phi の符号により、差分方向へ進むことも逆へ進むこともある

        ということである。

        つまりこの近傍生成は、
        集団の分布情報を使った自己適応的な探索幅を持っている。

        これは単純なガウス雑音付加などとは異なり、
        他個体との差分から探索方向を得る点に意味がある。
    */
    pos[i] = pos[i]
           + (rand() / (RAND_MAX / 2.0) - 1)
           * (pos[i] - fSet->flower[j]->pos[i]);

    /*
        新しい候補解として生成されたばかりなので、
        改善停滞回数 visitNum は 0 に戻す。

        この変数は「同じ食糧源が改善なしに何度試されたか」を表すため、
        新しい候補になった時点で初期化するのが自然である。
    */
    visitNum = 0;

    /*
        変更後の係数ベクトルについて評価値を再計算する。

        Flower は
            「位置を持つだけ」
        では不十分で、
            「その位置がどれだけ良いか」
        も常に持っている必要がある。

        そのため change() の最後で evaluate() を呼び、
        pos と value を整合した状態に保つ。
    */
    evaluate();
}


//============================================================
// 新しい食糧源に変更する
//============================================================
void Flower::renew()
{
    int i;

    /*
        この関数は scout bee フェーズで使われる。

        長い間改善しない食糧源は、
        その近傍を探っても有望でないとみなし、
        一度破棄してランダムな新食糧源へ置き換える。

        つまり renew() は、
        局所停滞した候補解を
        「まったく新しい探索開始点」
        に作り直す関数である。

        これは ABC における exploration の核であり、
        多様性回復・局所最適脱出のために重要である。
    */

    /*
        各次元を探索範囲 [COEF_MIN, COEF_MAX] でランダム再初期化する。

        これはコンストラクタの初期化と同じであり、
        つまり renew() は
            「この Flower を初期食糧源として作り直す」
        のに近い処理だといえる。
    */
    for (i = 0; i < fSet->dataset->exVarNum; i++) {
        pos[i] = COEF_MIN + (COEF_MAX - COEF_MIN) * RAND_01;
    }

    /*
        新しく生成し直したので visitNum を 0 に戻す。
    */
    visitNum = 0;

    /*
        新しい位置に対する評価値を計算する。
    */
    evaluate();
}


//============================================================
// 評価値を算出する
//============================================================
void Flower::evaluate()
{
    int i, j;
    double diff;

    /*
        この関数は Flower の中で最も重要である。

        なぜなら、ABC が何を最適化しているかは、
        最終的にこの evaluate() が定義しているからである。

        この実装では value を

            標準化済み目的変数に対する
            線形回帰予測の二乗誤差和

        として計算している。

        したがって ABC は、回帰係数ベクトル pos を動かしながら、
        この誤差を最小化しようとしている。
    */

    /*
        二乗誤差和を作るので、最初は 0.0 に初期化する。
    */
    value = 0.0;

    /*
        各データ点 i について誤差を計算する。

        resSData[i] は標準化済みの目的変数である。
        exSData[i][j] は標準化済みの説明変数 j の値である。

        つまり

            Σ pos[j] * exSData[i][j]

        は、現在の係数ベクトル pos を使った予測値に相当する。
    */
    for (i = 0; i < fSet->dataset->dataNum; i++) {

        /*
            最初に diff を実測値 resSData[i] にしておく。
        */
        diff = fSet->dataset->resSData[i];

        /*
            予測値を引くことで残差

                実測値 - 予測値

            を計算する。

            ここでの予測モデルは

                y_hat = Σ pos[j] * x_j

            であり、標準化空間での線形回帰式になっている。

            したがって diff は

                diff = y - y_hat

            に対応する。
        */
        for (j = 0; j < fSet->dataset->exVarNum; j++) {
            diff -= pos[j] * fSet->dataset->exSData[i][j];
        }

        /*
            残差の二乗を加算する。

            これにより最終的な value は

                Σ (y_i - y_hat_i)^2

            となる。

            これは最小二乗法の目的関数と同じ形であり、
            回帰分析における標準的な誤差評価である。

            二乗を取る理由は、

                - 正負の誤差を打ち消し合わない
                - 大きな誤差をより強く罰する
                - 微分可能で扱いやすい

            といった性質による。
        */
        value += pow(diff, 2.0);
    }

    /*
        以上より、この Flower の value は
        「現在の回帰係数ベクトル pos に対する二乗誤差和」
        を表している。

        value が小さいほど良い解である。

        したがって FlowerSet 側で

            if (oldValue > newValue)

        のような比較をしているのは、
        二乗誤差和最小化として整合している。
    */
}


/*
    ============================================================
    まとめ
    ============================================================

    この Flower.cpp の本質は、
    人工蜂コロニーアルゴリズムにおける 1 個の食糧源を
    「回帰係数ベクトルを持つ候補解」として実装している点にある。

    - コンストラクタ
      ランダムな初期回帰係数ベクトルを生成する

    - change(base)
      既存解 base の近傍を、他の解との差分を使って探索する

    - renew()
      停滞した解を破棄し、新しいランダム解へ置き換える

    - evaluate()
      現在の係数ベクトルに対する二乗誤差和を計算する

    つまり Flower は、

        候補解
        + 近傍生成器
        + 評価関数保持体

    を兼ねた、ABC の中心的な解表現クラスである。
*/
