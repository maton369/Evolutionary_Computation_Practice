#ifndef FLOWER_H
#define FLOWER_H

/*
    ============================================================
    Flower.h
    ============================================================

    このファイルでは、ABC における 1 個の食糧源を表す Flower クラスを定義する。

    ------------------------------------------------------------
    Flower の理論的な意味
    ------------------------------------------------------------

    ABC では、1 個の食糧源が 1 個の候補解に対応する。

    したがって Flower は、

    - 解ベクトルを保持する
    - その評価値を保持する
    - 近傍解へ変化する
    - 改善が止まった回数を記録する

    という役割を持つ。

    GA の Individual、
    PSO の粒子 position、
    ACO で 1 回の route が表す候補解

    などに対応する存在と考えられる。

    ただし ABC では「1つの食糧源を蜂が訪れて改良する」という
    自然界の比喩を使っているため、
    解候補を Flower（花・食糧源）として表している。

    ------------------------------------------------------------
    位置 pos の意味
    ------------------------------------------------------------

    Dataset クラスの構造から考えると、
    pos はおそらく

        標準偏回帰係数ベクトル

    を表している。

    つまり Flower は、
    「この回帰係数を使ったらどれくらい良いモデルになるか」
    を評価される候補解である。

    よって pos の各成分は連続値パラメータであり、
    ABC は連続最適化問題として動いていると考えられる。
*/

#include "Dataset.h"
#include "FlowerSet.h"

class FlowerSet;

/*
    ============================================================
    定数定義
    ============================================================

    REPEAT_NUM:
        反復回数。

    EBEE_NUM:
        収穫バチの数。
        通常は食糧源数と対応することが多い。

    OBEE_NUM:
        追従バチの数。
        有望解の周辺をさらに探索する人数を表す。

    VISIT_MAX:
        ある食糧源が改善しないまま訪問され続けたときの上限回数。
        これを超えると scout bee フェーズで破棄対象になると考えられる。

    COEF_MIN, COEF_MAX:
        標準偏回帰係数の探索範囲。
        pos の各成分はこの範囲内に初期化・更新される想定である。

    RAND_01:
        0以上1以下の一様乱数。
        近傍生成や選択確率に使う。
*/
#define REPEAT_NUM 1000
#define EBEE_NUM 100
#define OBEE_NUM 10
#define VISIT_MAX 10
#define COEF_MIN -1
#define COEF_MAX 1
#define RAND_01 ((double) rand() / RAND_MAX)

class Flower {
public:
    /*
        コンストラクタ。

        自分が属する FlowerSet を受け取る。
        これにより、
        - 問題次元数
        - データセット
        - 他の食糧源
        などへアクセスできる。
    */
    Flower(FlowerSet* argFSet);

    /*
        デストラクタ。

        pos などの動的配列を内部で持つなら、その解放責任を持つ。
    */
    ~Flower();

    /*
        change(base):
            指定された基準食糧源 base の近くに位置を変化させる。

            ABC の近傍生成は一般に
                x'_ij = x_ij + φ_ij (x_ij - x_kj)
            のように、
            現在の食糧源と他の食糧源との差分を使って新候補を作る。

            この関数は、そのような近傍解生成を担当する想定である。

            つまり ABC の局所探索を実現する中心関数の1つである。
    */
    void change(int base);

    /*
        renew():
            新しい食糧源に変更する。

            scout bee フェーズで使われることが多く、
            改善しない食糧源を捨てて、
            ランダムな位置へ再初期化する処理を想定できる。

            これは探索の多様性回復に対応する。
    */
    void renew();

    /*
        fSet:
            自分が属する食糧源集合。

            Flower 単体では問題データや全体の探索情報を持たず、
            fSet を通じて共有情報にアクセスする。
    */
    FlowerSet* fSet;

    /*
        pos:
            食糧源の位置、すなわち候補解ベクトル。

            この問題では回帰係数ベクトルに対応すると考えられる。
    */
    double* pos;

    /*
        value:
            この食糧源の評価値。

            evaluate() によって算出される。
            良いほど小さいのか大きいのかは評価関数の定義に依存する。
    */
    double value;

    /*
        visitNum:
            この食糧源が改善なしに訪問された回数。

            ABC では、長く改善しない食糧源は
            「枯れた食糧源」とみなし、偵察バチが新しいものへ置き換える。

            visitNum はその判定のための重要な状態変数である。
    */
    int visitNum;

private:
    /*
        evaluate():
            現在の pos に対する評価値 value を計算する。

            この関数が問題固有の目的関数に対応する。
            つまり ABC の探索対象が何であるかは、最終的にここで決まる。

            今回は宣言だけにしておき、実装は後で与える前提である。
    */
    void evaluate();
};

#endif
