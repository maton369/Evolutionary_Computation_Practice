#include "Firefly.h"

/*
    ============================================================
    Firefly.cpp
    ============================================================

    このファイルでは、ホタルアルゴリズム
    (Firefly Algorithm, FA)
    における 1 個のホタル Firefly の具体的な振る舞いを実装している。

    ------------------------------------------------------------
    Firefly の理論的な位置づけ
    ------------------------------------------------------------

    ホタルアルゴリズムでは、各ホタルが 1 個の候補解を表す。
    つまり Firefly は、

        - 現在位置 pos
        - その位置の良さを表す intensity

    を持つ探索主体である。

    ここで重要なのは、
    PSO の粒子のように速度を持つわけではなく、
    FA では「より良い個体に直接引き寄せられて位置を更新する」
    という点である。

    したがって Firefly は、

        候補解
        かつ
        他個体との相対比較によって動く探索主体

    として理解するとよい。

    ------------------------------------------------------------
    このコードでの pos の意味
    ------------------------------------------------------------

    これまでの Dataset の構造から考えると、
    pos は

        標準偏回帰係数ベクトル

    を表している。

    つまり各ホタルは、

        「この回帰係数ベクトルを使えばどの程度よい回帰モデルになるか」

    を試していることになる。

    pos の各成分は説明変数 1 個に対応する係数であり、
    次元数は

        pop->dataset->exVarNum

    に一致する。

    ------------------------------------------------------------
    このコードでの intensity の意味
    ------------------------------------------------------------

    evaluate() を見ると、
    intensity は

        標準化済み目的変数に対する
        線形回帰予測の二乗誤差和

    として計算されている。

    つまり数式で書けば

        intensity
        =
        Σ_i ( y_i - Σ_j β_j x_ij )^2

    に対応する。

    したがって、この intensity は名前としては「光の強度」だが、
    数学的には

        損失関数
        または
        残差平方和

    である。

    そのため、この実装では

        intensity が小さいほど良い

    という最小化問題として読まなければならない。

    一般的なホタルアルゴリズムの説明では
    「明るいほど良い」と言うが、
    このコードでは

        誤差が小さい個体ほど良い
        ↓
        より良い個体に引き寄せられる

    と読み替えるのが自然である。

    ------------------------------------------------------------
    Firefly が担当する処理
    ------------------------------------------------------------

    このクラスは主に次の 3 つを担当する。

    1. コンストラクタ
       ランダムな初期位置を生成し、その位置の評価を行う

    2. move(base)
       base が自分より良いなら、その方向へ移動する

    3. evaluate()
       現在位置 pos における損失を計算する

    つまり Firefly は、

        候補解の保持
        + 相対比較にもとづく移動
        + 問題固有の評価

    をまとめて持つ、ホタルアルゴリズムの最小単位である。
*/


//============================================================
// コンストラクタ
// argPop: 属しているホタル群
//============================================================
Firefly::Firefly(Population* argPop)
{
    int i;

    /*
        このホタルがどの Population に属しているかを保存する。

        Firefly 単独では

            - 問題の次元数
            - データセット
            - 他のホタル

        を知らない。

        そのため pop を通して

            pop->dataset->exVarNum
            pop->dataset->exSData
            pop->dataset->resSData

        などへアクセスする設計になっている。

        つまり Firefly は、
        Population に埋め込まれた探索主体である。
    */
    pop = argPop;

    /*
        現在位置ベクトル pos を確保する。

        この問題では pos が標準偏回帰係数ベクトルを表すため、
        ベクトル長は説明変数数 exVarNum に一致する。

        したがって

            pos[0], pos[1], ..., pos[exVarNum-1]

        は各説明変数に対する係数候補である。
    */
    pos = new double[pop->dataset->exVarNum];

    /*
        各次元を探索範囲 [COEF_MIN, COEF_MAX] の一様乱数で初期化する。

        これはホタルアルゴリズムにおける初期個体生成に対応する。

        初期位置をランダムにばらけさせることで、
        探索空間の複数領域を最初から同時に試せる。

        もし初期位置が偏っていると、
        群れ全体が狭い領域しか見られず、
        グローバルに良い解を見つけにくくなる。

        したがってこのランダム初期化は、
        集団探索としての多様性確保に重要である。
    */
    for (i = 0; i < pop->dataset->exVarNum; i++) {
        pos[i] = COEF_MIN + (COEF_MAX - COEF_MIN) * RAND_01;
    }

    /*
        初期位置の intensity を計算する。

        これによりコンストラクタ終了時点で、
        このホタルは

            - 位置
            - その位置の良さ

        を持った完全な候補解になる。
    */
    evaluate();
}


//============================================================
// デストラクタ
//============================================================
Firefly::~Firefly()
{
    /*
        pos は new[] で確保した動的配列なので delete[] で解放する。

        Firefly は自分の位置ベクトルを所有しているため、
        その後始末は自分で行う。
    */
    delete[] pos;
}


//============================================================
// base のほうが良かったら base の方向に移動する
// base: 比較対象のホタル
//============================================================
void Firefly::move(Firefly* base)
{
    int i;
    double dis;

    /*
        この関数はホタルアルゴリズムの中心である。

        FA の基本原理は、

            「自分より良いホタルがいたら、その方向へ引き寄せられる」

        というものである。

        このコードでは intensity が実質的に誤差を表しているため、
        intensity が小さいほど良いと解釈する必要がある。

        したがって

            自分の intensity > base の intensity

        なら、
            自分のほうが悪い
        ので、
            base の方向へ移動する

        という意味になる。

        ここは「光の強度」という名前だけを見ると直感と逆に見えるが、
        実際には最小化問題として整合している。
    */
    if (intensity > base->intensity) {

        //----------------------------------------------------
        // 1. 距離を算出する
        //----------------------------------------------------
        /*
            まず、自分と base の位置ベクトル間のユークリッド距離を計算する。

            数式で書くと

                dis
                =
                sqrt( Σ_k ( base_k - self_k )^2 )

            である。

            この距離は、誘引度を距離に応じて減衰させるために必要である。

            ホタルアルゴリズムでは、
            遠くの明るいホタルよりも、
            近くの明るいホタルのほうが強く影響を与える。

            そのため「誰が良いか」だけでなく、
            「どれくらい離れているか」も重要になる。
        */
        dis = 0.0;
        for (i = 0; i < pop->dataset->exVarNum; i++) {
            dis += pow(base->pos[i] - pos[i], 2.0);
        }
        dis = sqrt(dis);

        //----------------------------------------------------
        // 2. 移動先を決定する
        //----------------------------------------------------
        /*
            各次元について位置を更新する。

            更新式は

                pos[i]
                =
                pos[i]
                + ATTRACT * exp(-ABSORB * dis) * (base->pos[i] - pos[i])
                + RANDOMIZE * (RAND_01 - 0.5)

            である。

            これはホタルアルゴリズムの典型式

                x_i ← x_i + β(r)(x_j - x_i) + α(rand - 0.5)

            に対応している。

            ここで各項の意味は次の通りである。

            ----------------------------------------------------
            (a) 誘引項
                ATTRACT * exp(-ABSORB * dis) * (base->pos[i] - pos[i])
            ----------------------------------------------------

            これは base の方向へ向かう決定論的な移動項である。

            (base->pos[i] - pos[i]) は
                「自分から base へ向かう方向ベクトル」
            の i 次元目に対応する。

            したがってこの項は、
                より良いホタルの方向へ進む
            ことを表している。

            さらに

                ATTRACT * exp(-ABSORB * dis)

            がその強さを決める。

            ATTRACT:
                距離 0 のときの最大誘引度 β0 に相当する。

            exp(-ABSORB * dis):
                距離が大きいほど誘引が弱くなる減衰項である。

            これにより、
                近い良個体には強く引かれ、
                遠い良個体には弱くしか引かれない
            という性質が実現される。

            なお文献では exp(-γ r^2) がよく使われるが、
            このコードは exp(-ABSORB * dis) を使っている。
            これは減衰の具体形が少し異なるだけで、
            「距離で誘引が弱まる」という本質は同じである。

            ----------------------------------------------------
            (b) ランダム項
                RANDOMIZE * (RAND_01 - 0.5)
            ----------------------------------------------------

            これは [-0.5, 0.5] 程度の一様乱数に RANDOMIZE を掛けた項である。

            役割は、
                完全に決定論的に良個体へ吸い寄せられるだけでなく、
                少しランダムに揺らぐ
            ことである。

            このランダム性があることで、

                - 局所最適から抜け出す可能性が生まれる
                - 群れが早く一点へ潰れすぎるのを防ぐ
                - まだ見ていない方向を試す

            といった効果が得られる。

            つまり RANDOMIZE は、
                exploitation（良い解への収束）
                と
                exploration（新しい領域探索）
            のバランスを取るための重要パラメータである。
        */
        for (i = 0; i < pop->dataset->exVarNum; i++) {
            pos[i] = pos[i]
                   + ATTRACT * exp(-ABSORB * dis) * (base->pos[i] - pos[i])
                   + RANDOMIZE * (RAND_01 - 0.5);
        }

        /*
            移動後の新しい位置に対して intensity を再計算する。

            位置だけ更新しても、その場所が良いかどうかは分からない。
            したがって move() の最後には必ず evaluate() を呼び、

                新位置
                ↔ 新 intensity

            の整合を取る必要がある。

            これにより、次の個体比較で最新の良さが使われる。
        */
        evaluate();
    }

    /*
        もし base が自分より良くなければ、何もせずそのまま残る。

        これも FA の重要な性質である。
        すべての個体が常に動くわけではなく、

            「悪い個体が良い個体に近づく」

        という非対称な相互作用になっている。

        これにより群れは徐々に良い解の近くへ集まっていく。
    */
}


//============================================================
// 光の強度を算出する
//============================================================
void Firefly::evaluate()
{
    int i, j;
    double diff;

    /*
        この関数は、現在位置 pos の良さを intensity として計算する。

        この問題では pos が回帰係数ベクトルを表しているため、
        evaluate() は

            「その回帰係数でどれくらい誤差が小さいか」

        を測る役割を持つ。

        つまりホタルアルゴリズムが何を最適化しているかは、
        最終的にこの関数が決めている。
    */

    /*
        intensity を 0 に初期化する。

        ここでは各データ点の誤差二乗を足し込んでいくため、
        最初は 0.0 にする。
    */
    intensity = 0.0;

    /*
        各データ点 i について残差 diff を計算する。

        resSData[i] は標準化済み目的変数、
        exSData[i][j] は標準化済み説明変数である。

        現在の係数ベクトル pos による予測値は

            Σ_j pos[j] * exSData[i][j]

        であるから、

            diff
            =
            実測値 - 予測値

        を作っている。

        つまりこの実装は標準化空間での線形回帰モデル

            y_hat = Σ_j β_j x_j

        を仮定している。
    */
    for (i = 0; i < pop->dataset->dataNum; i++) {
        diff = pop->dataset->resSData[i];

        for (j = 0; j < pop->dataset->exVarNum; j++) {
            diff -= pos[j] * pop->dataset->exSData[i][j];
        }

        /*
            残差の二乗を足し込む。

            したがって最終的な intensity は

                Σ_i (y_i - y_hat_i)^2

            となる。

            これは残差平方和（RSS）に相当する。

            二乗を使う理由は、

                - 正負の誤差を打ち消し合わない
                - 大きな誤差をより強く罰する
                - 最小二乗法として自然

            といった点にある。

            したがってこの実装は、
                二乗誤差和最小化
            を目的関数としている。
        */
        intensity += pow(diff, 2.0);
    }

    /*
        以上より、この intensity は
        名前としては「光の強度」だが、
        実際には回帰誤差の大きさである。

        したがって

            intensity が小さいほど良い

        と読まなければならない。

        Population 側で bestInt を更新するときや、
        move(base) で比較するときも、
        この最小化の向きが前提になっている。

        もしホタルアルゴリズムの説明により忠実に
        「明るいほど大きい intensity」
        にしたいなら、
        たとえば

            intensity = 1 / (1 + error)

        のような変換を使う設計もあり得る。

        しかしこのコードでは、
        intensity をそのまま損失として扱っていると解釈するのが自然である。
    */
}


/*
    ============================================================
    まとめ
    ============================================================

    この Firefly.cpp の本質は、
    ホタルアルゴリズムにおける 1 個体を

        「位置を持つ候補解」
        かつ
        「より良い個体へ引かれて動く探索主体」

    として実装している点にある。

    - コンストラクタ
      ランダムな初期回帰係数ベクトルを生成し、その評価を行う

    - move(base)
      base がより良ければ、その方向へ距離減衰つきで移動し、
      さらにランダム揺らぎも加える

    - evaluate()
      現在位置に対する回帰誤差（二乗誤差和）を計算する

    つまり Firefly は、

        候補解
        + 個体間相互作用による移動
        + 問題固有評価

    をまとめて持つ、FA の中心的な探索単位である。

    PSO が gBest / pBest に基づく「記憶駆動」の探索だとすれば、
    FA は個体同士の相対的な良さと距離に基づく「相互作用駆動」の探索である。

    その違いが、この move() と evaluate() によく表れている。
*/
