#ifndef FIREFLY_H
#define FIREFLY_H

/*
    ============================================================
    Firefly.h
    ============================================================

    このファイルでは、ホタルアルゴリズム
    (Firefly Algorithm, FA)
    における 1 個のホタル Firefly を定義する。

    ------------------------------------------------------------
    ホタルアルゴリズムの基本思想
    ------------------------------------------------------------

    ホタルアルゴリズムは、自然界のホタルが
    「より明るい個体に引き寄せられる」
    という振る舞いを最適化へ応用したメタヒューリスティクスである。

    最適化の文脈では、

        - 各ホタル = 1 個の候補解
        - ホタルの明るさ = その候補解の良さ
        - 暗いホタルは、より明るいホタルへ移動する

    と解釈する。

    したがってこのアルゴリズムは、
    「良い解の方向へ個体を引き寄せる」
    という非常に直感的な仕組みを持つ。

    ------------------------------------------------------------
    最小化問題との対応
    ------------------------------------------------------------

    このコード群全体の流れから考えると、
    問題は回帰係数ベクトルを探索し、
    誤差や損失を最小化するタイプであると解釈するのが自然である。

    その場合、評価値 value は

        小さいほど良い

    という量である。

    しかしホタルアルゴリズムでは、概念的には

        明るいほど良い

    と表現したい。

    そのため実装上は一般に、

        intensity が大きいほど良い

    となるように評価値から変換するか、
    あるいは比較のときだけ
        「value が小さいものを明るいとみなす」
    という読み方をする。

    このクラスの `intensity` は、
    そのような「ホタルの良さ」を表す量である。

    ------------------------------------------------------------
    ホタルアルゴリズムの典型的な移動式
    ------------------------------------------------------------

    ホタル i が、より明るいホタル j に引かれて移動するとき、
    典型的には次のような式が使われる。

    x_i
    =
    x_i
    +
    β(r_ij) (x_j - x_i)
    +
    α (rand - 0.5)

    ここで

        x_i      : ホタル i の現在位置
        x_j      : より明るいホタル j の位置
        r_ij     : 2 個体間距離
        β(r_ij)  : 距離に応じて減衰する誘引度
        α        : ランダム探索の強さ

    である。

    この式の意味は次の通りである。

    1. x_j - x_i
       より良い解の方向を表す

    2. β(r_ij)
       距離が遠いほど引力が弱くなることを表す

    3. α(rand - 0.5)
       完全に決定論的な移動ではなく、
       ランダムな揺らぎも加えて多様性を保つ

    つまり FA は、

        良い解への収束
        +
        ランダム探索による多様性維持

    を同時に実現するアルゴリズムである。

    ------------------------------------------------------------
    このクラスの役割
    ------------------------------------------------------------

    Firefly クラスは、1 個の候補解として次を持つ。

        - 現在位置 pos
        - その位置の良さ intensity
        - 属している個体群 Population への参照

    さらに、

        - move(base)
          より明るいホタル base へ向かって移動する

        - evaluate()
          現在位置の明るさ（良さ）を計算する

    を担当する。

    したがって Firefly は、
    ホタルアルゴリズムにおける最小の探索単位である。
*/

#include "Dataset.h"
#include "Population.h"

class Population;

/*
    ============================================================
    定数定義
    ============================================================

    TIME_MAX:
        最終時刻。
        ホタル群の更新を何回繰り返すかを表す。

        FA は GA のような世代交代型というより、
        時刻ごとに個体群全体が少しずつ動く動的系として見るほうが自然である。
        したがって TIME_MAX は
            「何世代」
        というより
            「何ステップ動かすか」
        の意味に近い。

    POP_SIZE:
        ホタル群のサイズ。
        同時に何個の候補解を持つかを表す。

        大きいほど探索空間の広い領域を同時に観測できるが、
        計算コストは増える。

    ABSORB:
        吸収係数。
        距離が大きくなるにつれて誘引度がどれくらい減衰するかを決める。

        一般的には

            β(r) = β0 * exp(-γ r^2)

        のような形を使い、
        この γ に相当するのが ABSORB であることが多い。

        ABSORB が大きいと、遠距離のホタルの影響は急速に小さくなる。
        つまり局所的な引き寄せが強くなる。

        ABSORB が小さいと、遠くの良いホタルにも比較的引かれやすい。
        つまり大域的な情報共有が強まる。

    RANDOMIZE:
        ランダム性パラメータ α。
        移動時にどれだけランダムな揺らぎを加えるかを決める。

        大きいほど探索の多様性が増し、
        局所最適から抜けやすくなる可能性があるが、
        収束は遅くなりやすい。

        小さいほど良い解へ素直に集まりやすいが、
        探索が早く固まりやすい。

    ATTRACT:
        発光地点における誘引度 β0。
        距離が 0 のときの最大誘引度を表す。

        典型式

            β(r) = β0 * exp(-γ r^2)

        における β0 に相当する。

        つまり「最も強く引かれるときの基準値」である。

    COEF_MIN, COEF_MAX:
        探索する回帰係数の範囲。

        この問題設定ではホタルの位置 pos が
        標準偏回帰係数ベクトルを表すと考えられるので、
        各成分はこの範囲内で初期化・更新される想定である。

    RAND_01:
        0 以上 1 以下の一様乱数。

        ランダム揺らぎの生成や初期位置生成に使う。
*/
#define TIME_MAX 100
#define POP_SIZE 100
#define ABSORB 0.5
#define RANDOMIZE 0.5
#define ATTRACT 1
#define COEF_MIN -1
#define COEF_MAX 1
#define RAND_01 ((double) rand() / RAND_MAX)

class Firefly {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        このホタルが属する Population を受け取り、
        その Population 上で探索する 1 個体として初期化する。

        典型的にはここで

            - 位置 pos の乱数初期化
            - 初期 intensity の計算

        を行う。

        つまりコンストラクタ終了時点で、
        Firefly はすでに
            「位置を持つ候補解」
        として完成していることが期待される。
    */
    Firefly(Population* argPop);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        pos のような動的配列を内部で持つ場合は、
        その解放責任を持つ。
    */
    ~Firefly();

    /*
        --------------------------------------------------------
        move(base)
        --------------------------------------------------------

        より明るいホタル `base` に向かって、このホタルを移動させる。

        理論的には、この関数が FA の本体である。

        move(base) の中では一般に、

            1. 自分と base の距離を計算する
            2. 距離に応じた誘引度 β(r) を計算する
            3. base の方向へ引かれる項を作る
            4. ランダム揺らぎ項を加える
            5. 新しい位置へ更新する
            6. 新位置の intensity を再計算する

        という流れになる。

        ここで重要なのは、
        移動先が固定のベスト解 1 点ではなく、
        「自分より明るいホタル」
        である点である。

        つまりホタルアルゴリズムでは、
        群れの中の相対的な良さに応じて
        個体同士が相互作用しながら動く。

        PSO が gBest を共有して全員がそれを見るのに対して、
        FA は個体間距離と相対的明るさに基づく局所相互作用を使う。
        そこが大きな違いである。
    */
    void move(Firefly* base);

    /*
        pop:
            このホタルが属する Population。

            Firefly 単独では
                - 問題次元数
                - Dataset
                - 他のホタル
            を知らないため、
            pop を通じて共有情報へアクセスする。

            つまり Firefly は Population に埋め込まれた探索主体である。
    */
    Population* pop;

    /*
        pos:
            現在位置ベクトル。

            この問題では、標準偏回帰係数ベクトルの候補を表すと考えられる。
            したがって pos の各成分は、1 つの回帰係数候補である。

            位置ベクトルの次元数は通常
                pop->dataset->exVarNum
            に一致する。
    */
    double* pos;

    /*
        intensity:
            ホタルの光の強度を表す。

            アルゴリズム上は
                「どれだけ良い解か」
            を比較するための量である。

            最小化問題であれば通常、
                value が小さいほど intensity が大きい
            ような変換を内部で行うか、
            少なくとも比較の意味としてはそのように読む。

            したがって intensity は、
            FA における「適応度」や「評価値」に相当する概念である。
    */
    double intensity;

private:
    /*
        --------------------------------------------------------
        evaluate()
        --------------------------------------------------------

        現在位置 pos に対する光の強度 intensity を計算する。

        この関数が問題固有の目的関数に対応する。
        つまり

            このホタルの位置がどれだけ良いか

        を最終的に決めるのはこの evaluate() である。

        今回の問題設定では、
        Dataset を使って回帰誤差を計算し、
        その誤差から intensity を定めることが考えられる。

        たとえば最小化問題なら、

            誤差 value をまず求める
            ↓
            intensity = 1 / (1 + value)

        のように変換すれば、
        誤差が小さいほど強い光になる。

        したがって evaluate() は、
        問題の損失関数と
        ホタルアルゴリズムの「明るさ」表現を結びつける重要な関数である。
    */
    void evaluate();
};

#endif
