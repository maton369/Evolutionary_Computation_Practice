#ifndef POPULATION_H
#define POPULATION_H

/*
    ============================================================
    Population.h
    ============================================================

    このファイルでは、ホタルアルゴリズム
    (Firefly Algorithm, FA)
    における「ホタル群全体」を表す Population クラスを定義する。

    ------------------------------------------------------------
    ホタルアルゴリズムの基本的な考え方
    ------------------------------------------------------------

    ホタルアルゴリズムは、自然界のホタルが
    「より明るい個体に引き寄せられる」
    という振る舞いを最適化へ応用したメタヒューリスティクスである。

    最適化の文脈では、各ホタルは 1 個の候補解を表し、
    その候補解の良さに応じて「明るさ」が決まる。

    基本ルールは非常に単純である。

        1. 良い解ほど明るいホタルとみなす
        2. 暗いホタルは、より明るいホタルへ近づく
        3. ただし距離が遠いほど引力は弱くなる
        4. さらにランダムな揺らぎも加える

    これにより、

        - 良い解へ向かう収束性
        - ランダム探索による多様性

    の両方を実現する。

    ------------------------------------------------------------
    FA を数理的に見ると
    ------------------------------------------------------------

    ホタル i がホタル j に引き寄せられるとき、
    典型的な更新式は

        x_i ← x_i
               + β(r_ij) (x_j - x_i)
               + α (rand - 0.5)

    のように書かれる。

    ここで

        x_i      : ホタル i の現在位置
        x_j      : より明るいホタル j の位置
        r_ij     : 2 個体間の距離
        β(r_ij)  : 距離に応じて減衰する誘引度
        α        : ランダム探索の強さ

    である。

    つまり FA は、

        「良い解へ引かれる決定論的移動」
        と
        「ランダム揺らぎによる探索」

    を同時に持つ連続最適化法だといえる。

    ------------------------------------------------------------
    Population クラスの役割
    ------------------------------------------------------------

    Population は単なる Firefly の配列ではない。

    Population は、

        - 問題データ Dataset を保持する
        - 複数のホタル Firefly を保持する
        - すべてのホタルを 1 ステップ移動させる
        - その時点で最も明るいホタルを管理する
        - 最終結果を表示する

    という、FA 全体の制御装置である。

    これは

        GA の Population
        ACO の Colony
        ABC の FlowerSet
        PSO の Swarm

    に相当する役割と考えると分かりやすい。

    ------------------------------------------------------------
    このコードで想定される問題
    ------------------------------------------------------------

    これまでの Dataset クラスの流れから考えると、
    この FA でも各ホタルの位置は

        標準偏回帰係数ベクトル

    を表し、その評価値は

        標準化済みデータに対する二乗誤差和

    のような量だと考えるのが自然である。

    その場合、

        「明るいホタル」

    とは

        「評価値が小さい、すなわち損失が小さいホタル」

    に対応する。

    ただしホタルアルゴリズムの文献では
    「明るいほど良い」という表現を使うため、
    実装側では

        良さ（目的関数値）
        と
        明るさ（比較用の量）

    の対応を意識する必要がある。

    最小化問題なら一般には

        value が小さいほど intensity が大きい

    ような対応づけをするか、
    あるいは「bright = better」という概念上の説明だけで
    実際の比較は value の大小で行うことが多い。

    この Population クラスの `bestInt` は、
    そうした「最も良い個体の指標」を保存するための値だと考えられる。
*/

#include "Firefly.h"

class Firefly;
class Dataset;

class Population {
public:
    /*
        --------------------------------------------------------
        コンストラクタ
        --------------------------------------------------------

        fileName で指定されたデータセットを読み込み、
        Dataset を生成し、
        その上で探索を行うホタル群を初期化する想定である。

        Population は探索開始時点で

            - 問題定義 Dataset
            - 初期ホタル群
            - 最良ホタル記録領域

        を準備する必要がある。
    */
    Population(char* fileName);

    /*
        --------------------------------------------------------
        デストラクタ
        --------------------------------------------------------

        Population は通常、

            - Dataset
            - Firefly 配列
            - bestPos

        などを所有するため、
        それらの解放責任を持つ。
    */
    ~Population();

    /*
        --------------------------------------------------------
        move()
        --------------------------------------------------------

        ホタル群を 1 ステップ動かす。

        ホタルアルゴリズムでは、各ホタル i について
        より明るいホタル j を探し、
        その方向へ移動させる。

        典型的には

            for each i:
                for each j:
                    if j が i より明るい:
                        i を j に近づける

        のような二重ループ構造になる。

        そのため move() は、
        Population 全体の 1 時刻ぶんの相互作用をまとめて実行する関数だと考えられる。

        FA は PSO のように速度を持たず、
        「より良い個体へ直接位置を更新する」型のアルゴリズムなので、
        この move() が最適化の本体になる。
    */
    void move();

    /*
        --------------------------------------------------------
        printResult()
        --------------------------------------------------------

        最終的な最良ホタルの位置を、
        問題文脈に応じて解釈可能な形で表示する。

        今回の問題設定では、

            - 最良の標準偏回帰係数ベクトル
            - 元スケールに戻した偏回帰係数
            - 重回帰式

        などを表示することが考えられる。
    */
    void printResult();

    /*
        dataset:
            問題データを保持する。

            各ホタルの位置がどんな意味を持ち、
            その評価値がどう計算されるかは、
            最終的に dataset を通じて定まる。

            したがって dataset は、
            「Population が解く最適化問題の定義」
            を表す。
    */
    Dataset* dataset;

    /*
        firefly:
            ホタル群のメンバを表す配列。

            各 firefly[i] は 1 個の候補解であり、
            その位置と明るさ（または評価値）を持つ。

            この配列全体が、
            現時点の探索状態を表現する。

            FA の特徴は、これらの個体が相互作用しながら
            良い解へ集まっていく点にある。
    */
    Firefly** firefly;

    /*
        bestPos:
            現時点、または探索全体を通じて最も良かったホタルの位置を保存するベクトル。

            ベクトルの次元数は、通常 dataset->exVarNum に一致する。

            これは最終的な最良解を保持するための重要な記録領域である。
    */
    double* bestPos;

    /*
        bestInt:
            最も明るいホタルの光の強度を表す。

            ここで「明るさ」は概念的には
                解の良さ
            に対応する。

            ただし実装上は、最小化問題であれば
            「value が小さいほど bright」
            のような対応づけを内部で持つことが多い。

            したがって bestInt は、
            Population 全体における最良ホタルの質を表すスカラー量と解釈できる。

            実装によっては
                intensity = 1 / (1 + value)
            のような変換を行うこともあるし、
            単に「最小 value を持つ個体の指標」として使う場合もある。
    */
    double bestInt;
};

#endif


/*
    ============================================================
    設計レビュー的まとめ
    ============================================================

    この Population 設計は、ホタルアルゴリズムの構造をかなり素直に反映している。

    - Dataset
      問題の意味を与える

    - Firefly
      個々の候補解・移動主体

    - Population
      ホタル群全体の相互作用と最良解管理

    という分離ができているため、
    アルゴリズムの見通しがよい。

    特に FA では
        個体同士の相対的な良さ
        と
        距離に応じた誘引
    が核心なので、
    `Population::move()` が中心になるのは自然である。

    必要なら次に、

        Firefly.h
        Population.cpp
        Firefly.cpp
        main.cpp

    についても同じ粒度で、
    理論背景つきの詳細コメント版を書ける。
*/
