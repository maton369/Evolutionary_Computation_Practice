// File: Individual.cpp
//============================================================

#include "Individual.h"

/*
    ============================================================
    Individual.cpp
    ============================================================

    このファイルでは、進化計算における 1 個体 (Individual) の具体的な振る舞いを実装する。

    Population が「個体群全体の管理」を担当するのに対して、
    Individual は「1つの候補解がどのような構造を持ち、
    どのように評価され、どのように変化するか」を担当する。

    つまり Individual は、
    進化計算における最小単位の探索対象である。

    このコードでは個体は以下の要素から構成される。

    1. chrom
       長さ N の二値染色体である。
       各遺伝子座は 0 または 1 を取り、
       それぞれが候補解の一部を表す。

    2. fitness
       その個体の良さを表す数値である。
       Population 側ではこの fitness をもとに個体同士を比較し、
       良い個体を選択する。

    3. evaluate()
       染色体から適応度を計算する。

    4. crossover()
       2つの親個体から子個体を生成する。

    5. mutate()
       染色体に小さなランダム変化を与える。

    ------------------------------------------------------------
    この問題における適応度関数の特徴
    ------------------------------------------------------------

    各遺伝子 chrom[i] は 0/1 で表されているが、

        chrom[i] * 2 - 1

    と変換することで、

        chrom[i] = 0 なら -1
        chrom[i] = 1 なら +1

    に対応させている。

    さらに各位置 i に対して

        sqrt(i + 1)

    の重みを掛けて和を取っている。

    したがって、この個体は
    「符号付き重み和」
    を表していると解釈できる。

    最後に fabs() を取っているため、この問題は

        重み付き和の絶対値をできるだけ小さくする

    という最小化問題になっている。

    直感的には、
    各重み sqrt(1), sqrt(2), ..., sqrt(N) に対して
    + 側に置くか - 側に置くかを二値遺伝子で決め、
    全体の和ができるだけ 0 に近くなるようにしたい問題とみなせる。

    これは「二分割」や「符号割当て」に似た構造を持つ。

    ------------------------------------------------------------
    crossover を1つだけ残す理由
    ------------------------------------------------------------

    元の断片コードには crossover() が2回書かれていたが、
    どちらも

        void Individual::crossover(Individual* p1, Individual* p2)

    という同じシグネチャを持っていた。

    C++ では、引数リストが同じ関数を2回定義することはできない。
    そのため、どちらか一方だけを残す必要がある。

    今回は、最も基本的で理解しやすい

        一点交叉

    を残した。

    一点交叉は、
    - 実装が簡潔
    - 遺伝的アルゴリズムの基本例としてよく使われる
    - 染色体の前半と後半を親から分けて受け継ぐ構造が分かりやすい

    という利点がある。
*/


//============================================================
// コンストラクタ
//============================================================
Individual::Individual()
{
    int i;

    /*
        初期個体の生成
        --------------
        各遺伝子座 chrom[i] を 0 または 1 でランダム初期化する。

        ここで

            rand() % 2

        を使うことで、
        0 か 1 のどちらかを一様に選んでいる。

        進化計算では、初期個体群の多様性が重要である。
        初期状態でさまざまな染色体を持つ個体が存在することで、
        探索空間の複数の領域を同時に探索しやすくなる。

        もし最初からすべて同じ個体で始めると、
        個体群ベース探索の利点が大きく失われる。
    */
    for (i = 0; i < N; i++) {
        chrom[i] = rand() % 2;
    }

    /*
        fitness はまだ評価していないので 0.0 に初期化しておく。

        実際の適応度値は evaluate() を呼ぶことで計算される。
    */
    fitness = 0.0;
}


//============================================================
// デストラクタ
//============================================================
Individual::~Individual()
{
    /*
        このクラスでは内部で動的確保しているメモリがないため、
        特別な解放処理は不要である。

        ただし、将来的に補助配列や外部リソースを持つ設計へ拡張した場合は、
        このデストラクタで後始末を行うことになる。
    */
}


//============================================================
// 適応度を算出する
//============================================================
void Individual::evaluate()
{
    int i;

    /*
        まず fitness を 0 に戻してから加算を始める。

        evaluate() は何度も呼ばれる可能性があるので、
        前回の値を残したまま加算してしまうと誤った適応度になる。
    */
    fitness = 0.0;

    /*
        各遺伝子座 i について、

            (chrom[i] * 2 - 1) * sqrt((double)i + 1)

        を加算する。

        この式の意味を分解すると以下のようになる。

        --------------------------------------------------------
        1. chrom[i] * 2 - 1
        --------------------------------------------------------
        chrom[i] は 0 または 1 である。

        これを

            0 -> -1
            1 -> +1

        に変換している。

        つまり各遺伝子は、
        「その重みを正側へ足すか負側へ足すか」
        を表していると解釈できる。

        --------------------------------------------------------
        2. sqrt((double)i + 1)
        --------------------------------------------------------
        遺伝子位置 i に応じた重みである。

        i = 0, 1, 2, ... に対して

            sqrt(1), sqrt(2), sqrt(3), ...

        という正の値が対応する。

        したがって後ろの遺伝子ほど、一般には少しずつ大きな重みを持つ。

        --------------------------------------------------------
        3. 全体の和
        --------------------------------------------------------
        これらを全部足すことで、
        各重みを + 側または - 側へ割り当てたときの総和を得る。

        数式で書くと、

            fitness = Σ s_i * sqrt(i+1)

        ただし

            s_i ∈ {-1, +1}

        である。

        chrom[i] がこの s_i を決めている。
    */
    for (i = 0; i < N; i++) {
        fitness += (chrom[i] * 2 - 1) * sqrt((double)i + 1);
    }

    /*
        最後に絶対値を取る。

        これにより、

            和が +10 でも -10 でも評価値は同じ 10

        になる。

        つまりこの問題では、
        総和の符号そのものではなく、
        「0 からどれだけ離れているか」
        だけを評価している。

        したがって最適解は、
        重み付き和が 0 にできるだけ近い個体である。

        Population 側の sort() は fitness の小さい順に並べていたため、
        この実装全体は
        「fitness を最小化する問題」
        として整合している。
    */
    fitness = fabs(fitness);
}


//============================================================
// p1 と p2 から一点交叉で作った子にする
// p1: 親個体1
// p2: 親個体2
//============================================================
void Individual::crossover(Individual* p1, Individual* p2)
{
    int point, i;

    /*
        一点交叉
        --------
        染色体の途中に交叉点 point を1つ選び、

            先頭側     -> 親 p1 から継承
            後半側     -> 親 p2 から継承

        という形で子個体を作る。

        これは遺伝的アルゴリズムにおける最も基本的な交叉法の1つである。

        理論的には、
        親1が持つ良い部分構造と、
        親2が持つ良い部分構造を、
        1本の切れ目で再結合する操作だと解釈できる。

        building block hypothesis の観点では、
        短い有用部分列を保ったまま組み合わせられる可能性があるため、
        交叉は単なるランダム混合ではなく、
        良い部分解の再利用を期待する操作である。
    */

    /*
        交叉点 point を

            0 ～ N-2

        の範囲から選ぶ。

        これにより、少なくとも後半側に1要素は残る。

        もし point = N-1 を許すと、
        全遺伝子を親1から継承して親2の寄与がなくなるため、
        ここではそれを避けている。
    */
    point = rand() % (N - 1);

    /*
        0 ～ point までは親 p1 の遺伝子をコピーする。

        ここでは point 自身も p1 側に含めているため、
        子個体の前半部分は p1 の構造をそのまま受け継ぐ。
    */
    for (i = 0; i <= point; i++) {
        chrom[i] = p1->chrom[i];
    }

    /*
        point+1 ～ N-1 までは親 p2 の遺伝子をコピーする。

        これにより、子個体は
        - 前半は p1
        - 後半は p2
        という構造を持つ。

        個体群の中で異なる有望解が存在するとき、
        その前半の良さと後半の良さを組み合わせられる可能性がある。
    */
    for (; i < N; i++) {
        chrom[i] = p2->chrom[i];
    }

    /*
        ここでは fitness を直接更新していない。

        その理由は、Population::alternate() の最後で
        世代全体をまとめて evaluate() する設計になっているからである。

        つまり crossover() の責務は
        「染色体を作ること」であり、
        「適応度を計算すること」ではない。

        この責務分離により、
        個体生成と個体評価の役割が明確になる。
    */
}


//============================================================
// 突然変異を起こす
//============================================================
void Individual::mutate()
{
    int i;

    /*
        突然変異
        --------
        各遺伝子座について、
        確率 MUTATE_PROB でビット反転を行う。

        つまり、

            0 -> 1
            1 -> 0

        へ変化させる。

        この操作は局所的・確率的なランダム変更であり、
        交叉だけでは得られない新しい遺伝子配置を生み出す役割を持つ。

        理論的な役割は主に次の通りである。

        1. 多様性維持
           個体群が似通いすぎるのを防ぐ。

        2. 局所最適からの脱出
           交叉だけでは到達しにくい解へ移動するきっかけを与える。

        3. 探索空間の連結性確保
           十分長い時間で見れば、さまざまな遺伝子配置へ到達できる可能性を与える。

        一方で、突然変異率が高すぎると
        良い個体の構造まで壊してしまい、
        ほとんどランダム探索のようになってしまう。

        したがって MUTATE_PROB は、
        探索の多様性と収束の安定性のバランスを決める重要パラメータである。
    */
    for (i = 0; i < N; i++) {
        if (RAND_01 < MUTATE_PROB) {
            chrom[i] = 1 - chrom[i];
        }
    }
}
