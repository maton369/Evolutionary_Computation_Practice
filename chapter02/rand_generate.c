/*
    rand() / srand() の基本的な使い方をまとめたサンプルです。

    このコードでは、以下の内容を説明しています。

    1. srand((unsigned int)time(NULL));
       - 疑似乱数列の初期化
       - 毎回異なる乱数列を得るための準備

    2. 0以上N以下の整数
       - rand() % (N + 1)

    3. M以上N以下の整数
       - rand() % (N - M + 1) + M

    4. x以外の 0以上N以下の整数
       - (x + (rand() % N + 1)) % (N + 1)

    5. 0以上1以下の実数
       - (double)rand() / RAND_MAX

    6. M以上N以下の実数
       - (double)rand() / RAND_MAX * (N - M) + M

    注意:
    rand() は「疑似乱数」を生成する関数であり、完全な乱数ではありません。
    研究や進化計算の実験でも簡易な乱数源として使えますが、
    厳密な再現性管理や高品質な乱数が必要な場合は、より良い乱数生成器を使うこともあります。
*/

#include <stdio.h>   /* printf */
#include <stdlib.h>  /* rand, srand, RAND_MAX */
#include <time.h>    /* time */

/*
    指定範囲 [0, N] の整数乱数を返す関数
*/
int rand_int_0_to_N(int N) {
    /*
        rand() % (N + 1)
        ----------------
        rand() が返す大きな整数を (N + 1) で割った余りを取ることで、
        0, 1, 2, ..., N のいずれかを得ます。

        例:
        N = 5 のとき
        rand() % 6 -> 0,1,2,3,4,5 のどれか

        理論的には剰余を使う方法は分布が完全一様にならない場合があります。
        ただし、簡単なサンプルや学習用途ではよく使われます。
    */
    return rand() % (N + 1);
}

/*
    指定範囲 [M, N] の整数乱数を返す関数
*/
int rand_int_M_to_N(int M, int N) {
    /*
        rand() % (N - M + 1) + M
        ------------------------
        まず 0 から (N - M) までの整数を作り、
        それを M だけ平行移動して [M, N] の範囲にします。

        例:
        M = 3, N = 7 のとき
        rand() % 5 -> 0,1,2,3,4
        + 3         -> 3,4,5,6,7

        これも剰余を用いるため、厳密には偏りの可能性があります。
    */
    return rand() % (N - M + 1) + M;
}

/*
    指定範囲 [0, N] のうち、「x以外」を返す関数
*/
int rand_int_0_to_N_except_x(int N, int x) {
    /*
        (x + (rand() % N + 1)) % (N + 1)
        --------------------------------
        これは [0, N] に N+1 個の候補がある中で、
        x だけを除外して残り N 個から選ぶための書き方です。

        仕組み:
        1. rand() % N により 0 から N-1 のどれかを得る
        2. +1 により 1 から N のどれかにする
           → つまり「0以外のずらし量」を作る
        3. x にそのずらし量を足す
        4. % (N + 1) により範囲 [0, N] に折り返す

        これにより、x から 0 だけ動くケースがなくなるため、
        x 自身は絶対に選ばれません。

        例:
        N = 4, x = 2 のとき候補は {0,1,2,3,4}
        ずらし量が 1,2,3,4 のいずれかになるので、
        結果は 3,4,0,1 のどれかになり、2 は出ません。

        進化計算では、
        「親個体 i とは異なる個体を選ぶ」
        「突然変異で現在値とは異なる遺伝子を選ぶ」
        といった用途で似た発想が使えます。
    */
    return (x + (rand() % N + 1)) % (N + 1);
}

/*
    [0, 1] の実数乱数を返す関数
*/
double rand_double_0_to_1(void) {
    /*
        (double)rand() / RAND_MAX
        -------------------------
        rand() は 0 以上 RAND_MAX 以下の整数を返します。
        それを RAND_MAX で割ることで、0以上1以下の実数に正規化します。

        ここで (double) にキャストしている理由:
        rand() と RAND_MAX は整数なので、
        キャストしないと整数除算になってしまい、
        0 か 1 に近い不正な結果になります。

        例:
        rand() = 500, RAND_MAX = 1000 のとき
        500 / 1000        -> 整数除算なら 0
        (double)500 / 1000 -> 0.5

        進化計算では、
        「突然変異を起こす確率 pm と比較する」
        「交叉を実行する確率 pc と比較する」
        といった確率判定に非常によく使います。
    */
    return (double)rand() / RAND_MAX;
}

/*
    [M, N] の実数乱数を返す関数
*/
double rand_double_M_to_N(double M, double N) {
    /*
        (double)rand() / RAND_MAX * (N - M) + M
        ---------------------------------------
        まず [0,1] の実数乱数を作り、
        それを区間の長さ (N - M) 倍して [0, N-M] に拡大し、
        最後に +M して [M, N] に平行移動します。

        数学的には、u を [0,1] の一様乱数とすると、
        x = u * (N - M) + M
        により x は [M,N] の一様乱数になります。

        進化計算では、
        実数値遺伝子の初期化や、
        実数突然変異の摂動値生成などで使いやすい形です。
    */
    return (double)rand() / RAND_MAX * (N - M) + M;
}

int main(void) {
    int N = 10;
    int M = 3;
    int x = 5;

    /*
        srand((unsigned int)time(NULL));
        --------------------------------
        rand() は疑似乱数列を返す関数です。
        そのため、最初に「種(seed)」を設定しないと、
        実行するたびに同じ乱数列になることがあります。

        time(NULL) は現在時刻を秒単位で返します。
        実行時刻は通常毎回異なるため、
        それを seed に使うと毎回異なる乱数列になりやすくなります。

        unsigned int にキャストしているのは、
        srand() が unsigned int 型を引数に取るためです。

        なお、再現性が必要な実験では、
        あえて固定 seed を使うことがあります。
        例:
            srand(42);

        進化計算の研究では、
        「同じ seed で比較実験を行う」ことで公平性を保つことがあります。
    */
    srand((unsigned int)time(NULL));

    printf("0以上%d以下の整数: %d\n", N, rand_int_0_to_N(N));
    printf("%d以上%d以下の整数: %d\n", M, N, rand_int_M_to_N(M, N));
    printf("0以上%d以下で %d 以外の整数: %d\n", N, x, rand_int_0_to_N_except_x(N, x));
    printf("0以上1以下の実数: %f\n", rand_double_0_to_1());
    printf("%.1f以上%.1f以下の実数: %f\n", 2.5, 7.5, rand_double_M_to_N(2.5, 7.5));

    return 0;
}
