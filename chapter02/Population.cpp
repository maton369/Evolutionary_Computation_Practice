void Population::alternate() {
    /*
        TODO:
        1世代分の世代交代を書く場所。

        想定される流れは次の通り。

        --------------------------------------------------------
        1. エリート保存
        --------------------------------------------------------
        すでに sort 済みなら ind[0], ind[1], ... が上位個体である。
        その一部を nextInd へそのままコピーする。

        エリート保存の理論的役割:
        - 最良個体の破壊を防ぐ
        - 各世代で性能が急激に悪化するのを抑える
        - 探索の安定性を高める

        --------------------------------------------------------
        2. 親選択
        --------------------------------------------------------
        select() を使って親インデックスを選ぶ。

        ここで選択圧が導入される。
        良い個体ほど選ばれやすい設計にすると、
        集団全体が高適応度側へ寄る。

        --------------------------------------------------------
        3. 交叉
        --------------------------------------------------------
        選ばれた親から子個体を生成する。

        --------------------------------------------------------
        4. 突然変異
        --------------------------------------------------------
        子個体へ mutate() を適用し、
        多様性を導入する。

        --------------------------------------------------------
        5. 次世代の評価
        --------------------------------------------------------
        nextInd 内の個体群全体を評価する。

        --------------------------------------------------------
        6. ソート
        --------------------------------------------------------
        nextInd を適応度順に並べ、
        最良個体へすぐアクセスできるようにする。

        --------------------------------------------------------
        7. 現世代と次世代の交換
        --------------------------------------------------------
        ind と nextInd を swap する。

        これにより、
        「新しく作った世代」が次のループでの現世代になる。

        --------------------------------------------------------
        この関数の意味
        --------------------------------------------------------
        alternate() は進化計算の1ステップそのものであり、
        集団状態の更新則に相当する。

        main 側でこれを GEN_MAX 回繰り返すことで、
        個体群が時系列的に進化していく。
    */
}

