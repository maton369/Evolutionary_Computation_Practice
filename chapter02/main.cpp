#include "Population.h"
#include <cstdio>
#include <cstdlib>
#include <ctime>

/*
    ============================================================
    main.cpp
    ============================================================

    このファイルは進化計算全体の実行フローを表す。

    ここでは main 自体は短いが、
    その短さこそ設計分離がうまくできている証拠でもある。

    main の責務は、
    「Population を作り、世代交代を繰り返し、結果を見る」
    ことだけである。

    評価・選択・交叉・突然変異の詳細はすべて
    Population / Individual 側へ隠蔽されている。

    これは設計上とても重要で、
    アルゴリズム本体の抽象的な流れと、
    個別の進化操作の実装詳細が分離されている。
*/

int main() {
    /*
        疑似乱数の seed を設定する。

        時刻 seed を使うことで、実行ごとに異なる乱数系列になる。
        ただし、研究・再現実験では固定 seed を使うことも多い。

        進化計算は確率的アルゴリズムなので、
        seed 管理は実験設計の一部である。
    */
    srand((unsigned int) time(NULL));

    /*
        Population の生成。

        ここで初期個体群が構築される想定である。
        つまり main から見ると、
        Population は「進化を始める準備が整った集団」を返す抽象オブジェクトである。
    */
    Population* pop = new Population();

    /*
        世代交代ループ。

        1回の alternate() が 1世代分の更新に対応する。
        よってこの for 文は、
        離散時間で個体群を進化させていく外側ループである。

        毎世代ごとに最良適応度を表示しているため、
        学習曲線・収束傾向の簡易観測もできる。
    */
    for (int i = 1; i <= GEN_MAX; i++) {
        pop->alternate();

        /*
            sort の不変条件として ind[0] が最良個体であるなら、
            ここで最良適応度を即座に取得できる。

            つまりこの1行は、
            Population 内部設計が外側インタフェースにどう現れるかの例である。
        */
        if (pop->ind != nullptr && pop->ind[0] != nullptr) {
            printf("第%d世代：最良適応度 %f\n", i, pop->ind[0]->fitness);
        } else {
            /*
                まだ実装途中なので、安全のため null を考慮している。
                完成版では通常この分岐は不要になる。
            */
            printf("第%d世代：個体群は未初期化です\n", i);
        }
    }

    /*
        最終結果表示。
        実験の最終成果を観測する入口である。
    */
    pop->printResult();

    /*
        後始末。
        Population が所有する個体群も含めて解放される想定である。
    */
    delete pop;
    return 0;
}
