//==============================
// File: Population.h
//==============================
#ifndef POPULATION_H
#define POPULATION_H

#include "Individual.h"
#include <cstdio>

/*
    ============================================================
    Population.h
    ============================================================

    このファイルは、進化計算における「個体群 (Population)」を定義する。

    今回のコード全体で最も重要なのはこの Population の設計である。

    なぜなら、進化計算の本質は
    「個体を1つ改善すること」ではなく、
    「個体群全体を世代ごとに更新しながら探索すること」
    にあるからである。

    ------------------------------------------------------------
    個体群が理論的に果たす役割
    ------------------------------------------------------------

    1. 探索の並列化
       個体群を持つことで、探索空間の複数の場所を同時に調べられる。
       これは局所探索1本よりも広い探索を可能にする。

    2. 選択圧の導入
       個体群の中で良い個体ほど親として選ばれやすくすることで、
       世代が進むにつれて平均性能が上がる方向へ圧力をかける。

    3. 多様性の維持
       個体が複数存在することで、
       異なる部分解・異なる探索方向を同時に保持できる。

    4. 情報の保存と再利用
       良い個体は次世代へ残され、
       それを親としてさらに良い個体を作る。
       つまり Population は、探索履歴のうち有用な部分を
       集団レベルで保持する装置でもある。

    ------------------------------------------------------------
    現世代と次世代を分ける理由
    ------------------------------------------------------------

    本設計では次の2つを持つ。

        Individual** ind;      // 現世代
        Individual** nextInd;  // 次世代

    これは非常に重要な設計である。

    もし現世代をその場で上書きしてしまうと、
    まだ親選択に使うべき個体が途中で失われてしまう。

    そのため、
    「今ある集団を参照しながら、新しい集団を別領域に作る」
    という二重バッファ構造にする。

    これにより、世代交代の意味が明確になる。

        現世代 ind をもとに nextInd を構築する
        ↓
        完成したら ind と nextInd を入れ替える

    この構造は、進化計算の時間発展を実装上きれいに表している。

    ------------------------------------------------------------
    クラス責務
    ------------------------------------------------------------

    Population の責務は以下である。

    - 個体群全体の生成
    - 個体群全体の評価
    - 個体群全体のソート
    - 親選択
    - 子個体生成
    - エリート保存
    - 世代交代
    - 結果出力

    つまり、Population は「進化計算アルゴリズム本体」に対応する。
*/

class Population {
public:
    /*
        --------------------------------------------------------
        コンストラクタ / デストラクタ
        --------------------------------------------------------

        コンストラクタでは通常、
        - 現世代配列 ind の確保
        - 次世代配列 nextInd の確保
        - 初期個体の生成
        - 初期評価
        - 初期ソート
        を行う。

        つまり「探索開始前の初期集団を整える場所」である。

        デストラクタでは、
        動的確保した個体群と配列を適切に解放する。
        Population は多数の Individual を所有するため、
        メモリ管理責務も大きい。
    */
    Population();
    ~Population();

    /*
        --------------------------------------------------------
        alternate()
        --------------------------------------------------------

        世代交代を行う。

        これは Population の中心メソッドであり、
        1世代ぶんの進化操作をまとめて実行する。

        典型的な流れは次の通り。

        1. エリート個体を保存する
        2. 親選択を繰り返して子個体を生成する
        3. 子個体に突然変異を与える
        4. 次世代個体群を評価する
        5. 次世代個体群をソートする
        6. 現世代と次世代を入れ替える

        つまり alternate() は、
        「時刻 t の集団を時刻 t+1 の集団へ写す写像」
        に相当する。

        進化計算を離散時間の力学系として見ると、
        この関数が状態遷移そのものである。
    */
    void alternate();

    /*
        --------------------------------------------------------
        printResult()
        --------------------------------------------------------

        最終結果を表示する。

        通常は、
        - 最良個体の適応度
        - 最良個体の染色体
        - 必要なら平均適応度や統計量
        を出力する。

        進化計算は確率的アルゴリズムなので、
        単に最終解を見るだけでなく、
        世代ごとの推移や複数試行の統計を見ることも重要である。
        その拡張の出発点になるメソッドである。
    */
    void printResult();

    /*
        ind:
            現世代の個体群。

            ソート後は通常、
            ind[0] が最良個体になる想定である。
            main 側で ind[0]->fitness を表示していることからも、
            この不変条件を前提にしていることが分かる。

            つまり sort() の設計は、
            Population の外側から見たインタフェースにも影響している。
    */
    Individual** ind;

private:
    /*
        --------------------------------------------------------
        evaluate()
        --------------------------------------------------------

        個体群全体を評価する。

        Individual::evaluate() が個体単位の評価であるのに対し、
        Population::evaluate() はそれを全個体へ適用する集団版である。

        このように、
        個体レベルの操作を集団へ持ち上げることが
        Population の主要役割の1つである。
    */
    void evaluate();

    /*
        --------------------------------------------------------
        select()
        --------------------------------------------------------

        親個体を1つ選ぶ。

        戻り値が int であることから、
        「親個体のインデックスを返す」設計になっていると考えられる。

        代表的な選択法:
        - ルーレット選択
        - トーナメント選択
        - ランク選択

        この select() は、
        適応度をどの程度強く次世代へ反映させるかを決めるため、
        アルゴリズム全体の選択圧を左右する重要な部分である。
    */
    int select();

    /*
        --------------------------------------------------------
        sort(int lb, int ub)
        --------------------------------------------------------

        個体群を良い順に並べ替える。

        引数 lb, ub があるため、
        配列の部分区間を対象とした再帰的ソート
        (たとえば quicksort 風) を想定していると考えられる。

        ソートの目的は、
        単に見やすくすることではない。

        - エリート保存を簡単にする
        - 最良個体へ O(1) でアクセスできるようにする
        - 個体群の順位ベース操作を可能にする

        つまり sort() は、
        Population 内部の「比較可能な秩序」を作る操作である。
    */
    void sort(int lb, int ub);

    /*
        nextInd:
            次世代の個体群。

            現世代 ind を読みながら、
            新しい集団をこの領域へ構築する。

            世代交代が完了したら ind と nextInd を交換することで、
            時間発展を安全に実装できる。
    */
    Individual** nextInd;
};

#endif
